             '### II. Main Theme and Purpose\n'
             '\n'
             'The **main theme** of Romans is the **righteousness of God** '
             'revealed through the Gospel, leading to salvation for all who '
             'believe, both Jew and Gentile. Paul systematically unpacks the '
             'doctrine of **justification by faith** apart from works of the '
             'law.\n'
             '\n'
             "Paul's **primary purposes** in writing this epistle were "
             'multifaceted:\n'
             '1.  **Systematic Exposition**: To present a comprehensive and '
             'systematic articulation of his Gospel, providing theological '
             'clarity for the diverse Roman congregations.\n'
             '2.  **Preparation for Visit**: To introduce himself and his '
             'message, preparing the Roman believers for his anticipated visit '
             '(Romans 1:10-13; 15:23-24).\n'
             '3.  **Solicit Support**: To gain their partnership and financial '
             'backing for his future missionary endeavors to the western '
             'regions, specifically Spain (Romans 15:24, 28).\n'
             '4.  **Address Tensions**: To foster unity and mutual acceptance '
             'within the Roman church, which was experiencing friction between '
             'Jewish and Gentile Christians (Romans 14:1-15:7).\n'
             '5.  **Defend the Gospel**: To implicitly defend his apostolic '
             'ministry and the nature of his Gospel against potential '
             'misunderstandings or criticisms, particularly concerning the Law '
             'and the inclusion of Gentiles.\n'
             '\n'
             '### III. Structure and Thought Progression\n'
             '\n'
             "Paul's thought progression in Romans is a masterful, logical "
             "argument, moving from universal human depravity to God's "
             "glorious solution in Christ, its implications for the believer's "
             "life, the cosmic scope of God's plan for Israel and the "
             'Gentiles, and finally, its practical outworking in Christian '
             'community and conduct.\n'
             '\n'
             "**I. Introduction and Thesis: The Gospel of God's Righteousness "
             '(Romans 1:1-17)**\n'
             'Paul begins with a salutation, establishing his apostolic '
             'authority (1:1-7) and expressing his long-standing desire to '
             'visit Rome to impart spiritual gifts and receive mutual '
             'encouragement (1:8-15). He then states his thesis: he is "not '
             'ashamed of the gospel, for it is the power of God for salvation '
             'to everyone who believes... For in it the righteousness of God '
             "is revealed from faith for faith, as it is written, 'The "
             'righteous shall live by faith\'" (1:16-17). This sets the stage '
             'for the entire letter.\n'
             '\n'
             "**II. The Universal Need for God's Righteousness: Condemnation "
             'of All Humanity (Romans 1:18-3:20)**\n'
             'Paul systematically establishes the universal problem of sin:\n'
             "*   **Guilt of the Gentiles (1:18-32)**: God's wrath is revealed "
             'against those who suppress the truth evident in creation, '
             'leading to idolatry and moral degradation (1:18-23). God "gave '
             'them over" to impurity, dishonorable passions, and a debased '
             'mind (1:24-32).\n'
             '*   **Guilt of the Jews (2:1-3:8)**: Paul anticipates Jewish '
             "self-righteousness, arguing that God's judgment is impartial "
             '(2:1-11). Possession of the Law or circumcision does not '
             'justify; only obedience does (2:12-29). He addresses objections, '
             "affirming Jewish privileges but asserting God's faithfulness "
             'despite human unfaithfulness (3:1-8).\n'
             '*   **Universal Condemnation (3:9-20)**: He concludes that "all, '
             'both Jews and Greeks, are under sin" (3:9), citing Old Testament '
             "scriptures to prove universal depravity (3:10-18). The Law's "
             'purpose is to expose sin and make all accountable, not to '
             'justify (3:19-20).\n'
             '\n'
             "**III. God's Provision of Righteousness: Justification by Faith "
             '(Romans 3:21-5:21)**\n'
             "Having established the problem, Paul presents God's gracious "
             'solution:\n'
             "*   **Righteousness Through Faith in Christ (3:21-31)**: God's "
             'righteousness is now revealed apart from the Law, "through faith '
             'in Jesus Christ for all who believe" (3:21-22). This is a free '
             'gift, "justified freely by his grace through the redemption that '
             'came by Christ Jesus, whom God presented as a propitiation by '
             'his blood, to be received by faith" (3:24-25). This excludes '
             'boasting and confirms God as the God of both Jews and Gentiles '
             '(3:27-31).\n'
             '*   **Abraham: Example of Faith (4:1-25)**: Paul uses Abraham to '
             "prove that justification by faith is God's consistent method, as "
             'Abraham "believed God, and it was credited to him as '
             'righteousness" (4:3, quoting Genesis 15:6) before circumcision '
             'or the Law.\n'
             '*   **Blessings of Justification (5:1-11)**: Justification '
             'brings "peace with God" (5:1), access to grace, hope in God\'s '
             "glory, and even rejoicing in suffering (5:2-5). God's love is "
             'demonstrated in Christ dying for us "while we were still '
             'sinners" (5:8).\n'
             '*   **Adam and Christ: Two Heads of Humanity (5:12-21)**: Paul '
             "draws a parallel: Adam's one trespass brought sin and death to "
             "all, while Christ's one righteous act brings justification and "
             'life to all who believe (5:12-19). "Where sin increased, grace '
             'increased all the more" (5:20).\n'
             '\n'
             '**IV. New Life in Righteousness: Sanctification and Freedom from '
             'Sin (Romans 6:1-8:39)**\n'
             'Paul addresses the implications of salvation for daily living:\n'
             "*   **Freedom from Sin's Dominion (6:1-23)**: Grace does not "
