             '### IV. Key Theological Messages and Recurring Motifs\n'
             '\n'
             'Romans is rich with **recurring motifs and keywords** that build '
             'its theological messages. Understanding these terms, often '
             'illuminated by biblical Greek, is crucial:\n'
             '\n'
             '1.  **Sin ( - *hamartia*)**: Not just acts, but a '
