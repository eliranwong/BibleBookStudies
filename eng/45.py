             "highlights God's wisdom in exposing humanity's need for a "
             'Savior. OT: Exodus 20, Deuteronomy 6.\n'
             '3.  **Faith ( - *pistis*)**: Active trust and reliance on '
             'God and His promises in Christ, the sole means of receiving '
             "God's righteousness (Romans 1:17, 3:28). It reveals God's grace "
             "and impartiality. OT: Abraham's faith (Genesis 15:6, quoted in "
             'Romans 4:3), Habakkuk 2:4 (quoted in Romans 1:17).\n'
             "4.  **Righteousness ( - *dikaiosyn*)**: God's "
             'attribute of justice and His saving activity by which He '
             'declares sinners righteous (Romans 1:17, 3:21-26). It reveals '
             "God's profound justice and ingenious solution to sin. OT: Isaiah "
             '45:21.\n'
             '5.  **Justification ( - *dikaisis* /  - '
             "*dikaio*)**: God's forensic act of declaring a sinner righteous "
             '(Romans 3:24, 5:1). It is a free gift by grace through faith, '
             'not works. OT: Psalm 32:1-2 (quoted in Romans 4:7-8).\n'
             "6.  **Grace ( - *charis*)**: God's unmerited favor, the "
             "source of salvation (Romans 3:24, 11:6). It reveals God's "
             "boundless love and generosity. OT: God's covenant with Abraham, "
             'deliverance from Egypt.\n'
             '7.  **Spirit ( - *pneuma*)**: The Holy Spirit, the '
             'indwelling power for new life and sanctification (Romans '
             '8:1-17). He empowers, assures, and intercedes. OT: Ezekiel '
             '36:26-27.\n'
             '8.  **Redemption ( - *apolytrosis*) & Propitiation '
             "( - *hilastrion*)**: Being set free from sin's "
             "bondage and satisfying God's wrath through Christ's sacrifice "
             '(Romans 3:24-25). It reveals the cost of sin and the value of '
             "Christ's work. OT: Levitical sacrificial system.\n"
             '9.  **Jew / Gentile ( /  - *Ioudaios / Helln*)**: '
             "The universal scope of God's salvation, breaking down ethnic "
             "barriers (Romans 3:29-30, 10:12). It reveals God's universal "
             'love and impartiality. OT: Genesis 12:3, Isaiah 49:6.\n'
             '10. **Death ( - *thanatos*) & Life ( - *z*)**: The '
             'consequence of sin vs. the gift of God through Christ (Romans '
             '5:12-21, 6:23). It highlights God as the conqueror of death. OT: '
             'Genesis 2:17.\n'
             '11. **Adam and Christ (   - *Adam kai '
             'Christos*)**: Two representative heads of humanity, contrasting '
             "Adam's condemnation with Christ's justification (Romans "
             "5:12-21). It reveals God's grand redemptive plan. OT: Genesis "
             '3.\n'
             '\n'
