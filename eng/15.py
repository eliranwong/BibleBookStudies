             '## VI. Canonical Context and Intertextual Links\n'
             '\n'
             'Ezra is strategically placed in the biblical canon, linking past '
             'judgment with future hope and setting the stage for the New '
             'Testament.\n'
             '\n'
             '*   **Direct Continuation of 2 Chronicles:** Ezra begins with a '
             'near-verbatim repetition of 2 Chronicles 36:22-23, signaling a '
             'direct narrative and theological continuity. This highlights '
             "God's faithfulness to His covenant, even through judgment.\n"
             '*   **Complementary to Nehemiah:** Ezra and Nehemiah were '
             'originally one book. Ezra focuses on Temple rebuilding and legal '
             'reform, while Nehemiah focuses on wall rebuilding and further '
             'social reforms. Together, they present a comprehensive picture '
             'of physical and spiritual restoration.\n'
             '*   **Fulfillment of Prophecy:** Ezra directly fulfills '
             "Jeremiah's 70-year exile prophecy (Jeremiah 25:11-12; 29:10) and "
             "Isaiah's prophecy about Cyrus (Isaiah 44:28; 45:1-4), "
             "reinforcing God's reliability.\n"
             '*   **Connection to Prophetic Books:** Haggai and Zechariah are '
             'explicitly mentioned (Ezra 5:1-2) as motivating the Temple '
             "rebuilding. Ezra's reforms also set the stage for the spiritual "
             'challenges addressed by Malachi, the last prophet of the Old '
             'Testament.\n'
             '*   **Redemptive-Historical Relationship to the New '
             'Testament:**\n'
             "    *   **Restoration:** Ezra's physical return foreshadows the "
             'greater spiritual return from the exile of sin accomplished '
             'through Christ (Luke 19:10; Jeremiah 31:31-34).\n'
             '    *   **Temple:** The Second Temple points to Jesus as the '
             'true Temple (John 2:19-21) and the Church as the spiritual '
             'Temple (1 Corinthians 3:16; Ephesians 2:19-22).\n'
             "    *   **Law:** Ezra's enforcement of the Law highlights its "
             'importance, but Jesus ultimately fulfills it (Matthew 5:17), and '
             'the Spirit empowers believers to live its righteous requirements '
             '(Romans 8:4).\n'
             '    *   **Purity:** The "holy race" concept transforms from '
             'ethnic to spiritual in the New Testament, with believers called '
             'a "holy nation" (1 Peter 2:9) and urged to spiritual separation '
             '(2 Corinthians 6:14-18).\n'
             "    *   **Anticipation:** Ezra's incomplete restoration under "
             'foreign rule creates an anticipation for the ultimate, complete '
             "restoration in Christ's Kingdom (Revelation 21:1-4).\n"
