             '\n'
             '**Fulfillment of the Old Testament:**\n'
             '*   **Abrahamic Covenant:** Paul uses Abraham as the prime '
             'example of justification by faith (Galatians 3:6, quoting '
             "Genesis 15:6), demonstrating that God's original plan for "
             'salvation predated the Law. The promise to bless "all nations" '
             'through Abraham (Galatians 3:8, quoting Genesis 12:3) is '
             'fulfilled in the inclusion of Gentiles through Christ.\n'
