             "redeemed humanity from the Law's curse (Galatians 3:13, quoting "
             'Deuteronomy 21:23).\n'
             '*   **Prophecies of the Spirit:** The reception of the Spirit by '
             'faith (Galatians 3:2, 14) fulfills Old Testament prophecies of a '
             'New Covenant where God would put His Spirit within His people, '
             'enabling obedience (Ezekiel 36:26-27).\n'
             '*   **Slavery and Freedom:** The allegory of Hagar and Sarah '
             '(Galatians 4:21-31, from Genesis 16, 21) illustrates the '
             'contrast between the Law (slavery) and the Promise (freedom), '
             'echoing the Exodus narrative of liberation.\n'
             '*   **New Creation:** The concept of "new creation" (Galatians '
             '6:15) points to prophetic visions of a renewed world and people '
             'of God (Isaiah 65:17).\n'
