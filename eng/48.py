             '1.  **Gospel (εὐαγγέλιον - *euangelion*)**: The central truth of '
             'salvation through Christ, fiercely defended by Paul as singular '
             "and non-negotiable (Galatians 1:6-9). It defines God's "
             'redemptive work throughout the Bible (Romans 1:16-17).\n'
             '2.  **Law (νόμος - *nomos*)**: The Mosaic Law, presented as '
             'unable to justify (Galatians 2:16), bringing a curse (Galatians '
             '3:10), and serving as a temporary "guardian" until Christ '
             '(Galatians 3:24). It reveals sin but cannot save (Romans 3:20).\n'
             '3.  **Faith (πίστις - *pistis*)**: The sole means of '
             'justification and receiving the Spirit (Galatians 2:16; 3:2). It '
             'is active trust in God, exemplified by Abraham (Galatians 3:6; '
             'Genesis 15:6).\n'
             '4.  **Justification / Righteousness (δικαιόω - *dikaioō* / '
             'δικαιοσύνη - *dikaiosynē*)**: Being declared righteous by God, '
             'achieved only by faith in Christ (Galatians 2:16). This is how '
             'sinful humanity stands before a holy God (Romans 3:21-26).\n'
             "5.  **Grace (χάρις - *charis*)**: God's unmerited favor, the "
             'source of salvation, contrasted with legalism (Galatians 1:6; '
             "2:21). It is the foundation of God's interaction with humanity "
             '(Ephesians 2:8-9).\n'
             '6.  **Spirit (πνεῦμα - *pneuma*) / Flesh (σάρξ - *sarx*)**: Two '
             "opposing forces in the believer's life. The Spirit empowers new "
             'life and produces "fruit" (Galatians 5:16-23), while the flesh '
             'represents fallen human nature (Galatians 5:19-21). This '
             'highlights the need for internal transformation (Ezekiel '
             '36:26-27).\n'
             '7.  **Freedom (ἐλευθερία - *eleutheria*) / Slavery (δουλεία - '
             "*douleia*)**: Believers are freed from the Law's curse and "
             'bondage to serve God in love (Galatians 5:1, 13). This echoes '
             "Israel's Exodus and Christ's liberation from sin (John 8:36).\n"
             "8.  **Abraham (Ἀβραάμ - *Abraam*)**: Paul's key Old Testament "
             'witness for justification by faith, showing it predates the Law '
             'and circumcision (Galatians 3:6-9; Genesis 15:6).\n'
             '9.  **Cross (σταυρός - *stauros*)**: The ultimate symbol of '
             "Paul's gospel, representing Christ's atoning sacrifice and the "
             "believer's identification with His death (Galatians 6:14; 2:20). "
             'It is the core of Christian theology (1 Corinthians 1:18).\n'
