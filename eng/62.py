             'focal point of the entire biblical narrative, the fulfillment of '
             'Old Testament prophecy, and the central figure of the New '
             'Testament. He is the promised Messiah (*Christos* in Greek, '
             'meaning "Anointed One") (Matthew 16:16), the unique Son of God '
