             "basis for the church's mission (Matthew 28:18).\n"
             '\n'
             '5.  **The Paradox of Revelation: The Messianic Secret and '
             "Disciples' Failure:**\n"
             "    *   **Message:** Jesus' true identity is revealed gradually "
             'and often hidden, particularly until his suffering and '
             'resurrection. His closest disciples repeatedly misunderstand and '
             "fail him, highlighting human weakness and God's grace (Mark "
             '8:30, 14:50, 16:7).\n'
             '    *   **Significance:** This speaks to the nature of divine '
             "wisdom (Job 11:7-8, 1 Corinthians 1:18-25) and God's patience "
             'with imperfect followers, offering hope and humility to '
             'believers (1 Peter 1:10-12).\n'
