             'first of three explicit passion predictions, redefining '
             'messiahship as suffering and death (8:31).\n'
             '    *   **Costly Discipleship:** This is directly linked to the '
             'call for followers to "deny themselves and take up their cross '
             'and follow me" (8:34).\n'
             "    *   **Disciples' Failure:** The disciples continue to "
             'misunderstand, arguing about greatness (9:33-34) and seeking '
             "positions of honor (10:35-40), prompting Jesus' definitive "
             'teaching on servant leadership: "the Son of Man did not come to '
             'be served, but to serve, and to give his life as a ransom for '
             'many" (10:45).\n'
             '    *   **Symbolic Healing:** The healing of blind Bartimaeus '
