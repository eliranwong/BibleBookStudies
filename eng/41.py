             '11:1-16:8):** The narrative accelerates towards its climax in '
             'Jerusalem.\n'
             '    *   **Confrontation:** Jesus directly confronts the '
             'religious authorities through the Triumphal Entry (11:1-11), '
             'cleansing the Temple (11:15-17), and parables of judgment '
             '(12:1-12).\n'
             '    *   **Eschatological Discourse:** He provides an '
             'eschatological framework for understanding the coming '
             'destruction and his ultimate return (Mark 13).\n'
             "    *   **Passion Narrative:** Mark meticulously details Jesus' "
             'betrayal, trials, suffering, and death (Mark 14-15), emphasizing '
             "his isolation and ultimate sacrifice. The centurion's confession "
             'at the cross, "Surely this man was the Son of God!" (15:39), is '
             "the theological climax, revealing Jesus' true identity through "
             'his ultimate act of self-sacrifice.\n'
