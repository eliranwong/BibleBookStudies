             'sacrifice (Mark 8:34, 10:45).\n'
             '    *   **Significance:** This defines Christian living as '
             'active, sacrificial obedience, rooted in Old Testament covenant '
             'loyalty (Deuteronomy 6:5) and echoed in New Testament ethics '
             '(Galatians 2:20, Romans 12:1).\n'
             '\n'
             "4.  **Jesus' Divine Authority and Power:**\n"
             '    *   **Message:** Jesus possesses unparalleled authority over '
             'teaching, demons, sin, disease, nature, and death, demonstrating '
             "his divine nature and his decisive action in inaugurating God's "
             'Kingdom (Mark 1:22, 2:10, 4:39).\n'
             "    *   **Significance:** This confirms Jesus' deity and his "
             "role as the agent of God's redemptive plan, reflecting God's own "
