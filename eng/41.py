             "    *   **Message:** God's sovereign rule has decisively broken "
             "into the world through Jesus' ministry, demanding an immediate "
             'response of repentance and faith (Mark 1:15).\n'
             '    *   **Significance:** This fulfills Old Testament prophecies '
             "of God's Kingdom (Daniel 2:44, Psalm 103:19) and establishes it "
             'as both a present reality and a future hope, calling for '
             'allegiance (Matthew 6:10, Acts 28:31).\n'
