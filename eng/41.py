             "evil (4:35-5:43). Crucially, Mark highlights the disciples' "
             'persistent spiritual dullness and misunderstanding, even after '
             'witnessing profound miracles (e.g., "their hearts were '
             'hardened," 6:52; "Do you still not understand?" 8:17). This sets '
