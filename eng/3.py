             "God's character and truth (Leviticus 8-10).\n"
             '    *   **Gratitude for Christ:** Live with profound gratitude '
             'for Jesus as our perfect sacrifice and High Priest, who grants '
             'us direct access to God (Leviticus 16; Hebrews 4:14-16).\n'
