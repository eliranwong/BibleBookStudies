             'word, trusting in His promises of blessing (Leviticus 26).\n'
             "    *   **Trust in God's Provision:** Build rhythms of rest and "
             "renewal into life, trusting God's provision as we obey His "
             'commands (Leviticus 25).\n'
             '    *   **Integrity of Commitments:** Be faithful in all '
