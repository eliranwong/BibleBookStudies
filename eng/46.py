             'is to "unite all things in him, things in heaven and things on '
             'earth" (Ephesians 1:10). The church, as the body of Christ, is '
             'meant to be a visible manifestation of this cosmic unity, a new '
             'humanity reconciled to God and to one another. Divisions obscure '
             "this testimony and hinder the advancement of God's kingdom.\n"
             '\n'
