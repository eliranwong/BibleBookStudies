             "preparing for God's intervention by emphasizing divine "
             'transcendence and the limits of human comprehension.\n'
             "5.  **God's Speeches (Chapters 38-41):** God answers Job from a "
             'whirlwind, not with explanations for his suffering, but with a '
             'majestic display of divine power and wisdom in creation. He '
             "challenges Job's ignorance and authority, re-framing the "
             'discussion from "why" Job suffered to "who" God is.\n'
             "6.  **Job's Response (Job 42:1-6):** Job's thought culminates in "
             'humble submission and repentance, not for specific sins, but for '
             'presuming to question God\'s wisdom ("I had heard of you by the '
             'hearing of the ear, but now my eye sees you," Job 42:5).\n'
             '7.  **Epilogue (Job 42:7-17):** God rebukes the friends for '
             'their false counsel, vindicates Job, and restores his fortunes '
             "twofold, affirming God's ultimate compassion and Job's "
             'integrity.\n'
