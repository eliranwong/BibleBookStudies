             '#### 二、 文學形式、結構與思維進程\n'
             '\n'
             '《哈巴谷書》的文學形式獨特，主要呈現為**先知與上帝之間的對話**，並以**先知禱告詩歌**作結。\n'
             '\n'
             '1.  **結構：**\n'
             '    *   **I. 引言：先知的默示與負擔 (1:1)**\n'
             '    *   **II. 哈巴谷的第一次抱怨與上帝的回應 (1:2-11)**\n'
             '        *   哈巴谷對猶大國內不公的抱怨 (1:2-4)\n'
             '        *   上帝的回應：興起殘暴的迦勒底人 (1:5-11)\n'
             '    *   **III. 哈巴谷的第二次抱怨 (1:12-17)**\n'
             '        *   質疑上帝的聖潔與公義 (1:12-13a)\n'
             '        *   抱怨上帝為何使用比猶大更惡的迦勒底人 (1:13b-17)\n'
             '    *   **IV. 上帝對哈巴谷第二次抱怨的回應與五個「禍哉」 (2:1-20)**\n'
             '        *   先知等候上帝的回應 (2:1)\n'
             '        *   上帝的指示與核心信息：「惟義人必因信得生」 (2:2-4)\n'
             '        *   對迦勒底人的五個「禍哉」宣告 (2:5-19)\n'
             '        *   上帝的至高主權與全地靜默 (2:20)\n'
             '    *   **V. 哈巴谷的禱告：信心的詩歌 (3:1-19)**\n'
             '        *   禱告的引言與懇求 (3:1-2)\n'
             '        *   回顧上帝在歷史中的大能作為 (3:3-15)\n'
             '        *   先知在上帝面前的恐懼與信心 (3:16-19)\n'
             '\n'
             '2.  **思維進程：**\n'
             '    《哈巴谷書》的思維進程是一場深刻的神學對話，記錄了先知從困惑、質疑到最終信靠、讚美的靈性旅程。\n'
             '    *   **第一階段（1:2-4）：** '
             '先知對國內不公的困惑與抱怨。他目睹「強暴」、「奸惡」、「爭端」和「紛爭」，質疑上帝為何沉默：「耶和華啊，我呼求你，你不應允，要到幾時呢？」(1:2)。\n'
             '    *   **第二階段（1:5-17）：** '
             '上帝的首次回應與先知更深的困惑。上帝宣告將興起殘暴的迦勒底人（1:5-11），這使哈巴谷陷入更深的神學危機，質疑上帝的聖潔與公義：「你眼目清潔，不看邪僻，不看奸惡；為何看著行詭詐的人不理呢？」(1:13)。\n'
             '    *   **第三階段（2:1-20）：** '
             '上帝的第二次回應——公義的原則與惡者的結局。先知採取「守望」姿態（2:1），上帝指示他寫明默示，並揭示核心真理：「惟義人必因信得生。」(2:4)。隨後，上帝發出針對迦勒底人的五個「禍哉」宣告，保證所有不義都將受審判，並以「耶和華在他的聖殿中；全地的人都當在他面前肅敬靜默。」(2:20) '
             '宣告其至高主權。\n'
             '    *   **第四階段（3:1-19）：** '
             '先知的禱告與信心的昇華。哈巴谷回顧上帝在歷史中的大能作為（3:3-15），儘管預見極度匱乏（3:17），他卻選擇在上帝裡面歡欣：「然而，我要因耶和華歡欣，因救我的上帝喜樂。」(3:18)，並從上帝得著力量（3:19）。\n'
             '\n'
             '#### 三、 關鍵主題與神學信息\n'
             '\n'
             '1.  **上帝的公義與神義論：**\n'
             '    *   **信息闡述：** '
             '本書核心探討在邪惡和苦難面前，如何理解上帝的公義和良善。先知勇敢地向上帝提出質疑，代表了人類在面對不公時的普遍困惑。\n'
             '    *   **神學意義：** '
             '肯定了上帝的絕對公義和最終審判。儘管上帝的作為看似難以理解，但祂始終掌管歷史，並將在祂的時間裡施行公義的審判。祂對罪惡零容忍，且其公義超越人類有限視角。\n'
             '    *   **希伯來文洞察：** 「耶和華啊，我呼求你，你不應允，要到幾時呢？」（哈巴谷書1:2，希伯來文「עד '
             "אנה」*ad 'anah*，典型的哀歌開場）。「你眼目清潔，不看邪僻，不看奸惡」（哈巴谷書1:13，希伯來文「טָהוֹר "
             "עֵינַיִם」*tahor 'einayim*），突顯了先知對上帝聖潔屬性與其行動之間矛盾的困惑。\n"
             '\n'
             '2.  **「惟義人必因信得生」：**\n'
             '    *   **信息闡述：** '
             '這是本書的**核心信息**（哈巴谷書2:4），強調在審判和苦難中，義人將因其對上帝的堅定信實和持續信靠而得以存活和興盛。\n'
             '    *   **神學意義：** '
             '確立了信心（或信實）作為人類與上帝建立正確關係的基礎，以及在審判中得以存活的關鍵。它將生命的焦點從外部環境轉向內在的信仰狀態。\n'
             '    *   **希伯來文洞察：** '
             '「אֱמוּנָה」（*emunah*）在新約中常譯為「信心」，但在舊約語境中，它更強調**「信實」、「忠誠」、「堅定」和「可靠性」**。這是一種在上帝應許尚未完全實現時，仍選擇堅忍不拔地信靠祂的生命態度。\n'
             '\n'
             '3.  **上帝對列國的普世主權與最終審判：**\n'
             '    *   **信息闡述：** '
             '上帝是歷史的掌管者，祂興起迦勒底人作為審判工具（哈巴谷書1:6），但最終也會對迦勒底人發出五個「禍哉」的宣告（哈巴谷書2:6-19），預言他們因其貪婪、殘暴、不義和偶像崇拜而將遭受報應。\n'
             '    *   **神學意義：** '
             '確立了上帝的至高無上和絕對權柄。任何人類的權力，無論多麼強大，都最終服從於上帝的旨意。祂是萬國的審判者，最終的勝利屬於上帝。\n'
             '    *   **希伯來文洞察：** '
             '「מֵקִים」（*meqim*，我必興起）強調上帝的直接干預。重複出現的「הוֹי」（*hoy*，禍哉）是上帝對列國罪惡的審判宣告。\n'
             '\n'
             '4.  **在絕境中以上帝為樂：**\n'
             '    *   **信息闡述：** '
             '先知在預見極度匱乏和毀滅（哈巴谷書3:17）之後，卻發出震撼的信心宣告：「然而，我要因耶和華歡欣，因救我的上帝喜樂。」（哈巴谷書3:18）。\n'
             '    *   **神學意義：** '
