             '### 二、 主要信息 (Primary Messages)\n'
             '\n'
             '#### 1. 上帝對其應許的信實\n'
             '\n'
             '*   **詳細闡述：** '
             '約書亞記最核心的信息是上帝對祂所立之約的絕對信實。從亞伯拉罕時代開始，上帝就應許將迦南地賜給他的後裔。約書亞記詳細記錄了這一應許是如何在約書亞的領導下得以實現的。書末的總結（約書亞記21:43-45）是這一信息的最高潮：「這樣，耶和華將從前向他們列祖起誓應許的全地，賜給以色列人，他們就得了為業，住在其中。耶和華使他們四境平安，正如從前向他們列祖起誓所應許的...耶和華應許賜福給以色列家的一切話，一句也沒有落空，都應驗了。」\n'
             '*   **希伯來文研究：**\n'
             '    *   **אָמַן (*aman*)：** '
             '「堅定」、「信實」。這是「阿們」（Amen）的詞根，強調上帝的本性是信實可靠的。\n'
             '    *   **דָּבָר (*davar*)：** '
             '「話語」、「事情」。約書亞記21:45強調「一句話也沒有落空」，表明上帝的每一個應許，無論大小，都必會成就。\n'
             '    *   **圖示：** 想像一份由最高權威發出的、經過時間考驗的契約，其中所有的條款都按時、按質、按量地得到履行。\n'
             '*   **神學意義：**\n'
             '    *   **上帝的屬性：** 信實是上帝本質的一部分，祂永不說謊，永不背棄祂的約。\n'
             '    *   **歷史的意義：** 人類歷史是上帝實現其救贖計劃的舞台。\n'
             '    *   **盼望的基礎：** 上帝的信實是其子民在任何困境中都能持有盼望的基礎。\n'
             '*   **新約貢獻：**\n'
             '    *   '
             '耶穌基督是上帝所有應許的「是」（哥林多後書1:20：「因為神的應許，不論有多少，在基督都是「是」的，所以藉著他也都是「阿們」的，叫榮耀歸與神。」）。\n'
             '    *   新約強調上帝對新約的信實，透過基督的寶血，祂的應許在信徒身上得以實現（希伯來書8:6-13）。\n'
             '*   **對與上帝關係的影響：** '
             '極大地堅固我們對上帝的信心和信任。在面對不確定和等待時，提醒我們上帝是信實的，祂的應許終將實現，從而帶來平安和忍耐。\n'
             '\n'
             '#### 2. 絕對順服上帝命令的重要性\n'
             '\n'
             '*   **詳細闡述：** '
             '這是約書亞記貫穿始終的實踐性信息。從約書亞蒙召時的指示（1:7-8），到耶利哥的奇蹟式勝利（6章），再到艾城的失敗與成功（7-8章），以及約書亞在示劍的告別演說（24章），無不強調對上帝命令的絕對順服是成功的關鍵。\n'
             '*   **希伯來文研究：**\n'
             "    *   **שָׁמַע (*shama'*)：** "
             '「聽從」、「順服」。在希伯來文化中，「聽」不僅是接收信息，更包含著「遵從」的行動。\n'
             '    *   **צָוָה (*tzavah*)：** 「命令」、「吩咐」。上帝的命令具有絕對的權威性，要求無條件的執行。\n'
             '    *   **圖示：** 想像一支訓練有素的軍隊，士兵們對指揮官的命令絕對服從，因為他們知道這是勝利的唯一途徑。\n'
             '*   **神學意義：**\n'
             '    *   **上帝的主權與權威：** 上帝是至高無上的，祂的命令不容質疑。\n'
             '    *   **人對上帝的回應：** 順服是人對上帝恩典和盟約的正確回應。\n'
             '    *   **順服的祝福：** 順服帶來上帝的同在、引導和祝福。\n'
             '*   **新約貢獻：**\n'
             '    *   耶穌基督是順服的典範，祂「存心順服，以至於死，且死在十字架上」（腓立比書2:8）。\n'
             '    *   新約信徒被呼召效法基督的順服，將自己獻上作為活祭（羅馬書12:1-2）。\n'
             '*   **對與上帝關係的影響：** '
             '培養謙卑和順服的心，願意將自己的意志降服於上帝的旨意。激勵我們積極尋求並遵行上帝的話語，因為這是與祂建立親密關係的基礎。\n'
             '\n'
             '#### 3. 罪與悖逆的後果\n'
             '\n'
             '*   **詳細闡述：** '
             '亞干的罪行及其所帶來的後果（約書亞記7章）是這一信息的強烈例證。亞干因貪婪私藏當滅之物，導致以色列人在艾城戰役中失敗，最終他本人和家人也因此被處死。這表明個人的罪不僅影響自己，也會對整個群體帶來災難性的影響。\n'
             '*   **希伯來文研究：**\n'
             "    *   **חֵטְא (*chet'*)：** "
             '「罪」、「射不中靶心」。亞干的行為是直接違背上帝命令，偏離了上帝的旨意。\n'
             "    *   **עָוֹן (*'awon*)：** 「罪孽」、「不義」。指道德上的扭曲和由此產生的罪疚。\n"
             '    *   **圖示：** '
             '想像一個團隊在執行一項重要任務，其中一個成員的私心和違規行為，導致整個團隊的努力付諸東流，甚至帶來毀滅性的後果。\n'
             '*   **神學意義：**\n'
             '    *   **上帝的聖潔與公義：** 上帝不能容忍罪惡，祂必會審判罪。\n'
             '    *   **罪的傳染性：** 罪惡不僅是個人的，也可能影響整個群體。\n'
             '    *   **悔改的必要性：** 只有處理罪惡，才能恢復與上帝的關係並重新蒙福。\n'
             '*   **新約貢獻：**\n'
             '    *   新約明確指出「罪的工價乃是死」（羅馬書6:23），強調罪的嚴重性。\n'
             '    *   耶穌基督的犧牲是為了解決罪的問題，使信徒得以赦免和救贖。\n'
