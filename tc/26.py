             '### 三、 作者思維進程：從審判到復興\n'
             '\n'
             '《以西結書》的思維進程是一部宏大而精心構建的神學敘事，它引導讀者從對以色列罪惡的嚴厲審判，逐步走向對其未來復興的宏偉願景。以西結作為被擄之地的先知，其信息在時間上與耶路撒冷的命運同步，從預言其毀滅到宣告其重建，展現了上帝公義與慈愛並存的完整圖景。\n'
             '\n'
             '1.  **第一階段：對猶大和耶路撒冷的審判預言 (1:1-24:27)**\n'
             '    *   **思維起點：上帝的超越性與主權。** '
             '以西結的異象始於迦巴魯河邊上帝榮耀的宏偉顯現（結1章），確立了上帝的絕對主權和無所不在。\n'
             '    *   **先知職責的確立：** '
             '以西結被呼召作「人子」，承擔向「悖逆的以色列家」傳達信息的任務，並被立為「守望的人」（結2-3章），強調先知和個人對罪的責任。\n'
             '    *   **審判的具體化與必然性：** '
             '一系列象徵性行動（圍困、飢荒、剃髮，結4-5章）和對偶像崇拜的譴責（結6章），預示了耶路撒冷不可避免的毀滅。\n'
             '    *   **審判的根本原因：上帝榮耀的離去。** '
             '以西結看見聖殿內外充斥可憎之事（結8章），導致上帝的榮耀一步步離開聖殿（結10章），打破了猶大人對聖殿永不陷落的錯誤觀念。\n'
             '    *   **個人責任的強調：** '
             '結18章駁斥「酸葡萄」俗語，確立「惟有犯罪的，他必死亡」（結18:4）的個人責任原則。\n'
             '    *   **最終宣告：** 沸騰的鍋寓言和以西結妻子之死（結24章）預示耶路撒冷圍困的開始和不可避免的毀滅。\n'
             '\n'
             '2.  **第二階段：對列國的預言 (25:1-32:32)**\n'
             '    *   **思維轉向：上帝對全地的統治權。** '
             '在耶路撒冷陷落前，信息轉向對周圍列國的審判。上帝審判那些因以色列苦難而幸災樂禍、驕傲自大的國家（結25章，推羅、埃及等）。\n'
             '    *   **神學意義：** 彰顯上帝的普世主權和公義，同時肯定以色列在上帝計劃中的獨特地位，為其未來復興鋪墊。\n'
             '\n'
             '3.  **第三階段：以色列復興的預言 (33:1-48:35)**\n'
             '    *   **思維轉變：從警告審判到呼籲悔改以得生命。** '
             '耶路撒冷陷落的消息（結33:21）是轉折點。以西結的守望者職責重申，重點轉向呼籲百姓悔改，因為上帝「不喜悅惡人死亡」（結33:11）。\n'
             '    *   **上帝主動的拯救與內在的更新：** '
             '譴責惡牧人，應許上帝作真牧人（結34章）。結36章應許賜予「新心，將新靈放在他們裡面」（結36:26），預示新約的內在更新。\n'
             '    *   **民族的復活：** 枯骨復生的異象（結37章）象徵以色列民族從絕望中復興，兩根木杖合一預言南北國統一。\n'
             '    *   **上帝對復興以色列的最終保護：** '
             '歌革和瑪各的預言（結38-39章）表明上帝將保護復興後的以色列，並在列國面前彰顯祂的榮耀。\n'
             '    *   **上帝榮耀的歸回與完全的同在：** '
             '新聖殿異象（結40-48章）描繪上帝榮耀從東門歸回聖殿（結43章），與榮耀的離去形成對比。從聖殿流出的生命河（結47章）象徵生命和豐盛。最終以新城名字「**耶和華的所在**」（結48:35）作結，預示上帝與祂子民完美的盟約關係和永恆同在。\n'
