             '《以西結書》是舊約聖經中一部極其重要且內容豐富的先知書，它在以色列歷史上最黑暗的時期——巴比倫流亡時期——發出上帝的聲音。這本書不僅揭示了上帝對罪惡的嚴厲審判，也充滿了對未來復興的宏偉應許，為被擄的以色列民帶來了盼望。\n'
             '\n'
             '### 一、 書卷簡介與背景概覽\n'
             '\n'
             '1.  **作者或歸屬作者：** '
             '《以西結書》的作者是先知以西結本人。書中多處以第一人稱「我」來敘述，明確指出這是他所領受和傳達的信息（例如，結1:3：「在迦勒底人之地，迦巴魯河邊，耶和華的話特特臨到布西的兒子祭司以西結。」）。以西結是一位祭司，這背景深刻影響了他的神學觀點和對聖殿、聖潔的關注。\n'
             '\n'
             '2.  **成書日期或時間段：** '
             '本書的寫作時間與以西結的先知事奉時期緊密相連，即巴比倫流亡時期。以西結在猶大王約雅斤被擄的第五年，即主前593年，蒙召作先知（結1:2）。他的事奉持續了至少22年，直到約雅斤被擄的第二十七年（結29:17）。因此，本書的寫作時間約在主前593年至571年之間，記錄了他在流亡期間所領受的異象、信息和象徵性行動。\n'
             '\n'
             '3.  **主要主題或目的：** 《以西結書》的核心主題是**上帝的榮耀（כָּבוֹד, '
             'kavod）**、**審判**與**復興**。\n'
             '    *   **上帝的榮耀：** '
             '以西結的異象始於上帝榮耀的顯現（結1章），並描寫了上帝的榮耀如何因以色列的罪惡而離開聖殿（結10章），最終又應許上帝的榮耀將來會回到新的聖殿（結43章）。\n'
             '    *   **審判：** 書中詳細闡述了上帝對猶大、耶路撒冷及其周圍列國的審判，證明上帝是公義的。\n'
             '    *   **復興：** '
             '儘管審判嚴厲，但本書最終指向了以色列的復興，應許賜予他們「一個新心，將新靈放在他們裡面」（結36:26）。\n'
             '    '
             '本書的目的是要向被擄的以色列民解釋他們為何會遭遇亡國之痛，駁斥他們對上帝不公的抱怨，並在絕望中給予他們對未來復興的堅定盼望。\n'
             '\n'
             '4.  **重要歷史事件或背景：** '
             '《以西結書》的背景是**巴比倫流亡**。主前605年、597年（以西結被擄）和586年（耶路撒冷最終被毀，聖殿被焚）的三次被擄，是猶大民族歷史上最黑暗的時期。以西結的預言正是在這段動盪不安、充滿絕望的時期發出，解釋了亡國之痛的神學原因，並在異鄉為百姓帶來盼望。\n'
             '\n'
             '5.  **關鍵人物或角色：**\n'
             '    *   **以西結（Ezekiel）：** '
             '本書的中心人物，一位祭司出身的先知，被上帝呼召在流亡之地向同胞傳達信息，被上帝稱為「人子」（結2:1）。\n'
             '    *   **耶和華上帝：** 貫穿全書的最高主宰，是發出審判和應許的源頭。\n'
             '    *   **以色列民：** 以西結信息的直接對象。\n'
             '    *   **列國：** 書中也包含對周圍列國的審判預言。\n'
             '\n'
             '6.  **文學形式或體裁：** 《以西結書》主要屬於**先知文學**，但其體裁非常獨特，融合了多種元素：\n'
             '    *   **異象（Visions）：** '
             '充滿了生動、複雜且有時令人費解的異象，如上帝榮耀的異象、枯骨復生的異象、新聖殿的異象，帶有強烈的**啟示文學**（Apocalyptic '
             'Literature）特徵。\n'
             '    *   **象徵性行動（Symbolic Actions）：** '
             '以西結被要求進行許多奇特的象徵性行動，以戲劇性地傳達上帝的信息。\n'
             '    *   **寓言（Allegories）：** 使用寓言故事來比喻以色列的罪惡。\n'
             '    *   **神諭（Oracles）：** 大量的審判和應許神諭。\n'
             '    *   **哀歌和挽歌：** 偶爾穿插的哀歌。\n'
             '    *   **散文與詩歌：** 書中混合了散文敘述和詩歌體裁的預言。\n'
             '\n'
             '7.  **獨特特徵或爭議：**\n'
             '    *   **異象的複雜性與神秘性：** 使得解釋充滿挑戰。\n'
             '    *   **象徵性行動的極端性：** 在先知書中獨一無二。\n'
             '    *   **「人子」的稱謂：** 預示了彌賽亞的稱謂。\n'
             '    *   **新聖殿異象的解釋：** 結40-48章引發了巨大的解經爭議，究竟是字面意義上的未來聖殿，還是象徵性描述。\n'
             '    *   **與祭司傳統的聯繫：** 反映了流亡時期對律法和聖潔的重新強調。\n'
             '    *   **對個人責任的強調：** 結18章被視為以色列神學發展中的重要里程碑。\n'
             '\n'
