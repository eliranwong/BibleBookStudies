             '    *   **影響：** 鼓勵對世俗和屬靈領袖的辨別力，激勵為公義領袖禱告，培養對上帝這位完美牧人的信任。\n'
             '\n'
             '### 六、 正典處境：在聖經中的連結與地位\n'
             '\n'
             '《以西結書》在聖經正典中佔據著獨特而關鍵的地位，它不僅是舊約先知文學的巔峰之作，更是連接舊約與新約神學主題的橋樑。\n'
             '\n'
             '**A. 在舊約中的連結：**\n'
             '1.  **歷史與時間的定位：** '
             '以西結與耶利米（在耶路撒冷）和但以理（在巴比倫王宮）同時代，共同為猶大亡國提供了神學解釋，並為未來帶來盼望。\n'
             '    *   **與《耶利米書》的承接：** '
             '耶利米預言新約（耶31:31-34），以西結更詳細闡述新約的內在更新（新心新靈，結36:26-27）。\n'
             '    *   **與《但以理書》的平行：** 兩者都包含豐富的異象和啟示文學元素，塑造了流亡時期猶大人的末世觀。\n'
             '2.  **對《申命記》歷史與早期敘事的解釋：** '
             '以西結書為《申命記》所預言的盟約咒詛（申28章）提供了最生動的例證，解釋了以色列因悖逆而亡國被擄。\n'
             '    *   **聖殿神學的延續與轉折：** '
             '以西結書中上帝榮耀離開聖殿（結10:18-19）的異象，回應了《出埃及記》（出40:34-35）和《列王紀上》（王上8:10-11）中上帝榮耀充滿會幕和聖殿的敘事，但揭示了聖殿並非不可摧毀。\n'
             '3.  **獨特的舊約神學貢獻：** '
             '獨特描繪上帝榮耀的動態性、強調個人責任（結18章）、應許新心新靈（結36章）、聖靈的生命賦予能力（結37章），以及末世聖殿與生命河的異象（結40-48章）。\n'
             '4.  **塑造後流亡時期猶太思想：** '
             '為流亡後的猶太人提供了重建信仰和身份的藍圖，影響了第二聖殿的建造和祭司制度的發展，激發了對上帝最終救贖的期待。\n'
             '\n'
             '**B. 與新約的關係和實現：**\n'
             '1.  **彌賽亞預言與基督論的實現：**\n'
             '    *   **「人子」的稱謂：** '
             '耶穌基督頻繁自稱「人子」（太8:20），既強調祂的人性，也暗示祂是但以理書中那位擁有神聖權柄的彌賽亞（但7:13-14，太26:64）。以西結書為理解耶穌這個核心自稱提供了重要背景。\n'
             '    *   **好牧人：** '
             '耶穌宣告「**我是好牧人**」（約10:11），實現了以西結書34:23-24對公義牧人的應許。\n'
