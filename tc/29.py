             '#### 4. 悔改與哀悼 (Repentance and Lamentation)\n'
             '\n'
             '*   **闡述：** '
             '面對蝗災和「耶和華的日子」的威脅，約珥書發出了緊急而深刻的悔改呼籲。這種悔改不僅是外在的儀式，更是內心的轉變，伴隨著全國性的哀悼、禁食和禱告。先知強調神所看重的是內心真誠的破碎，而非表面的宗教行為。\n'
             '*   **聖經希伯來文研究：**\n'
             '    *   希伯來文 **שׁוּב (shuv)** '
             '意為「回轉、歸回」，強調從錯誤的道路轉向正確的道路，從罪惡轉向神。約珥書2:12呼籲「一心歸向我」（שׁוּבוּ עָדַי '
             'בְּכָל־לְבַבְכֶם shuvu adai bekol-levavkhem），強調是全心全意的回轉。\n'
             '    *   「撕裂你們的心腸，不撕裂你們的衣服」（קִרְעוּ לְבַבְכֶם וְאַל־בִּגְדֵיכֶם '
             "qir'u levavkhem ve'al-bigdeikhem，約珥書2:13）是一個強烈的對比。\n"
             "        *   **קִרְעוּ (qir'u)** 意為「撕裂」，在古近東和以色列文化中，撕裂衣服（בֶּגֶד "
             'beged）是表達極度悲傷、哀悼或悔改的外在標誌。\n'
             '        *   然而，約珥強調的是「心腸」（לֵבָב '
             'levav），即內心、意志和情感的中心。這表明神所看重的是內在的真實改變，而非僅僅是外在的儀式或表演。\n'
             '    *   **圖解：** '
             '想像一個人穿著破爛的衣服，外表看似哀傷，卻內心剛硬不悔。約珥書指出，神要的不是外在的破爛衣服（形式），而是內心被撕裂的破碎和謙卑（實質），這才是真誠悔改的標誌。\n'
             '*   **神學意涵：**\n'
             '    *   **神對真誠的渴望：** 神不滿足於表面的宗教行為，祂渴望人內心真實的歸回和與祂建立真誠的關係。\n'
             '    *   **悔改帶來轉機：** '
             '悔改是神恩典的途徑。約珥書2:14說：「或許他會轉意後悔，留下餘福」，表明真誠的悔改能改變神的審判，帶來祝福和復興。\n'
             '    *   **神有恩典和憐憫：** '
             '悔改的呼籲是基於神「有恩典，有憐憫，不輕易發怒，有豐盛的慈愛，並且後悔不降所說的災」（約珥書2:13）的屬性。這揭示了神不僅是公義的審判者，更是充滿慈愛的救贖者。\n'
             '*   **新約聖經的貢獻：**\n'
             '    *   新約聖經延續並深化了悔改的重要性，將其與耶穌基督的福音和救恩聯繫起來。\n'
             '    *   **馬太福音 3:2** 施洗約翰的開場信息就是：「天國近了，你們應當悔改！」（μετανοεῖτε '
             'metanoeite）。\n'
             '    *   **使徒行傳 3:19** '
             '彼得在講道中呼籲：「所以，你們當悔改歸正，使你們的罪得以塗抹，這樣，那安舒的日子就必從主面前來到。」這與約珥書中悔改帶來復興的應許相呼應。\n'
             '    *   **哥林多後書 7:10** '
             '保羅區分了兩種憂愁：「因為依著神的意思憂愁，就生出沒有後悔的悔改，以致得救；世俗的憂愁卻是叫人死。」這進一步強調了真誠悔改的內在性和其救贖性的結果。\n'
             '*   **對我們與神關係的影響：**\n'
             '    *   **自我省察與謙卑：** 悔改的呼籲促使我們不斷省察自己的內心，承認自己的罪過，並在神面前謙卑自己。\n'
             '    *   **經歷神的赦免與更新：** 當我們真誠悔改時，就能經歷神的赦免、憐憫和更新，使我們與神的關係得以恢復和深化。\n'
