             'basar）在此指所有人類，強調聖靈的澆灌將是普世性的，不分以色列人或外邦人，不分男女老少，不分僕人或自由人。這在舊約中是相當獨特的應許，因為舊約時代聖靈通常只臨到特定的領袖（如先知、祭司、君王）身上。\n'
             '    *   **圖解：** '
             '想像在乾旱的土地上，突然天降甘霖，滋潤萬物，使生命得以復甦。聖靈的澆灌就像這場甘霖，帶來屬靈的生命和更新，使所有信徒都能領受神的啟示和能力，不再僅限於少數人。\n'
