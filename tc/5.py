             '### 五、 主要主題與信息\n'
             '\n'
             '《申命記》透過重複出現的關鍵詞和概念，傳達了幾個核心主題和主要信息：\n'
             '\n'
             '1.  **盟約 (בְּרִית, Berit)：**\n'
             '    *   **主題闡述：** '
             '盟約是《申命記》的基石，是上帝與以色列之間關係的框架，強調了盟約的**關係性**和**約束性**。它定義了以色列的身份和命運，要求以色列對耶和華絕對忠誠，遵守祂的誡命，作為回報，上帝應許賜予祝福和保護。\n'
             '    *   **神學意涵：** 揭示上帝是**立約的上帝**，祂信實、主權、公義。\n'
             '    *   **新約貢獻：** 新約將耶穌基督的救贖視為「新約」的實現（路加福音 22:20；希伯來書 '
             '8:6-13），將律法寫在心版上，實現了《申命記》中「心裡的割禮」（申命記 30:6）的應許。\n'
             '\n'
             "2.  **耶和華是獨一的 / 獨一真神 (Yahweh's Uniqueness / Monotheism)：**\n"
             '    *   **主題闡述：** '
             '這是《申命記》最核心的神學宣告，集中體現在「示瑪」中：「以色列啊，你要聽！耶和華─我們神是**獨一的主**。」（申命記 '
             '6:4）。強調耶和華是天上地下獨一的真神，以色列必須專一敬拜祂，不可有別神。\n'
             '    *   **神學意涵：** 確立上帝的**至高主權**和**獨一性**，祂是忌邪的上帝，不容許偶像崇拜。\n'
             '    *   **新約貢獻：** 猶太教和基督教信仰的基石，耶穌和使徒們都強調只有一位真神（馬可福音 12:29；提摩太前書 '
             '2:5）。\n'
             '\n'
             '3.  **愛耶和華你的神 (אָהַב, Ahav)：**\n'
             '    *   **主題闡述：** '
             '「愛」在《申命記》中被提升為所有律法的總綱和順服的內在動機，要求以色列人「**盡心、盡性、盡力愛耶和華─你的神**」（申命記 '
             '6:5），這是一種全人、全生命對上帝的忠誠和委身，是對上帝之愛的回應。\n'
             '    *   **神學意涵：** 揭示上帝是**愛的上帝**，祂要求全人委身，愛是盟約關係的精髓。\n'
             '    *   **新約貢獻：** 耶穌將愛神與愛鄰舍結合，作為「最大誡命」（馬太福音 '
             '22:37-40），並以祂的犧牲之愛作為榜樣（約翰福音 13:34）。\n'
             '\n'
             '4.  **律法 / 誡命 (תּוֹרָה, Torah / מִצְוָה, Mitzvah)：**\n'
             '    *   **主題闡述：** '
             '律法是上帝愛的禮物，是引導以色列人過聖潔、公義、蒙福生活的指南。遵守律法是愛上帝的具體表現，也是在應許之地得以長久存活的條件。\n'
             '    *   **神學意涵：** 揭示上帝的**聖潔**、**公義**和**智慧**。\n'
             '    *   **新約貢獻：** 耶穌說祂來不是要廢掉律法，乃是要成全（馬太福音 '
             '5:17）。保羅闡述律法顯明罪並引導人歸向基督（羅馬書 3:20；加拉太書 3:24）。\n'
             '\n'
             '5.  **應許之地 (אֶרֶץ, Eretz)：**\n'
             '    *   **主題闡述：** '
             '應許之地是上帝對列祖所立之約的具體實現，是流奶與蜜之地，象徵著上帝對以色列的祝福和同在。它的擁有和持續享受，建立在以色列對盟約忠誠的基礎上。\n'
             '    *   **神學意涵：** 揭示上帝是**賜予者**、**主權的上帝**，土地是祂恩典的禮物，也象徵祂的同在。\n'
             '    *   **新約貢獻：** 新約將「土地」的概念擴展到更廣闊的屬靈領域，指向上帝的國度或永恆的產業（馬太福音 '
             '5:5；希伯來書 11:13-16）。\n'
             '\n'
             '6.  **祝福與咒詛 (בְּרָכָה וּקְלָלָה, Berakhah u-Qelalah)：**\n'
