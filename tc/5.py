             '    *   **修辭策略：** 透過重述十誡，摩西讓百姓重新面對上帝的直接要求，強化了盟約的嚴肅性。\n'
             '\n'
             '2.  **申命記的核心教導：示瑪與愛的誡命 (6:1-25)：**\n'
             '    *   **目的：** 從律法的「內容」轉向律法的「精神」——愛。這是《申命記》最核心的神學信息。\n'
             '    *   **進程：** '
             '在重申十誡之後，摩西立即引出「示瑪」：「以色列啊，你要聽！耶和華─我們神是獨一的主。你要盡心、盡性、盡力愛耶和華─你的神。」（6:4-5）。這將所有律法歸結為對上帝的愛，並強調這種愛必須透過殷勤教訓兒女、將律法銘記於心來傳承。接著，他警告百姓在進入應許之地享受豐盛後，不可忘記上帝（6:10-19），這再次將歷史教訓應用於未來。\n'
             '    *   **修辭策略：** '
             '將律法從外在的行為準則提升到內在的生命原則，使順服成為愛的表達。強調信仰的代際傳承，確保盟約的延續性。\n'
             '\n'
             '3.  **聖潔的民族與上帝的揀選 (7:1-11)：**\n'
             '    *   **目的：** 解釋以色列作為上帝選民的特殊地位和責任，特別是在迦南地如何保持聖潔。\n'
             '    *   **進程：** '
             '從愛神的誡命引申出對迦南地偶像和居民的徹底清除（7:1-5），這是保持聖潔的必要條件。摩西強調以色列被揀選並非因其強大或人數眾多，而是因上帝的愛和信實（7:6-8），這消除了以色列人可能產生的自義，將榮耀歸於上帝。\n'
             '    *   **修辭策略：** 強調上帝的恩典和主權，同時也明確了以色列作為聖潔子民的排他性要求。\n'
             '\n'
             '4.  **順服的祝福與征服的應許 (7:12-26)：**\n'
             '    *   **目的：** 具體說明順服所帶來的豐盛祝福，激勵百姓遵守盟約。\n'
             '    *   **進程：** '
             '承接上帝的愛和揀選，摩西列舉了順服將帶來的物質和軍事上的祝福（生育、豐收、健康、戰勝仇敵）。這將抽象的「蒙福」具體化，使百姓對順服的益處有清晰的認識。\n'
             '    *   **修辭策略：** 透過具體的應許激發百姓的盼望和順服的意願。\n'
             '\n'
             '5.  **曠野經歷的教訓與警告 (8:1-20)：**\n'
             '    *   **目的：** 回顧曠野經歷的深層意義，警告百姓在富足後可能面臨的誘惑。\n'
             '    *   **進程：** '
             '摩西再次回顧曠野四十年，指出上帝磨練他們是為了教導他們「人活著不是單靠食物，乃是靠耶和華口裡所出的一切話」（8:3），並使他們謙卑。他描述了應許之地的豐饒（8:7-10），然後立即發出嚴厲警告：在富足後最容易忘記上帝，若忘記，必遭滅亡（8:11-20）。\n'
             '    *   **修辭策略：** '
             '透過歷史的重溫，將過去的苦難解釋為上帝愛的管教，並將其轉化為對未來富足的警惕，預防潛在的驕傲和忘恩。\n'
             '\n'
             '6.  **回顧以色列的悖逆史與呼籲心靈的割禮 (9:1-10:22)：**\n'
             '    *   **目的：** 徹底粉碎以色列可能存在的自義，強調他們得以進入應許之地完全是上帝的恩典。\n'
             '    *   **進程：** '
             '摩西直接指出以色列人得以進入迦南並非因他們的義，而是因迦南人的邪惡和上帝的應許（9:1-6）。他詳細回顧了金牛犢事件（9:7-21）和其他悖逆行為，證明以色列是「硬著頸項的百姓」。在徹底揭露百姓的罪惡之後，摩西轉向呼籲內在的轉變：「所以你們要**將心裡的污穢除掉**，不可再硬著頸項。」（10:16），並強調上帝對以色列的要求是敬畏、愛、事奉和專靠祂（10:12-22）。\n'
             '    *   **修辭策略：** '
             '透過嚴厲的自我批判，使百姓謙卑，認識到自己的不配，從而更深地體會上帝的恩典。呼籲「心裡的割禮」是從外在行為到內在生命的深刻轉變，為盟約的真正履行奠定基礎。\n'
             '\n'
             '7.  **祝福與咒詛的選擇 (11:1-32)：**\n'
             '    *   **目的：** 總結前述所有教導，將順服與悖逆的後果明確地擺在百姓面前，為接下來的具體律法作鋪墊。\n'
             '    *   **進程：** '
             '摩西再次提醒上帝在埃及和曠野所行的奇事（11:1-7），然後清晰地闡明順服將帶來雨水、豐收和地土的擴張（11:8-17），而悖逆則導致乾旱和滅亡。他再次強調將律法銘記於心並教訓兒女的重要性（11:18-25）。最後，他以一個戲劇性的宣告結束這一部分：「我今日將祝福與咒詛都擺在你們面前」（11:26），並指示他們進入迦南後要在基利心山和以巴路山宣告祝福與咒詛。\n'
             '    *   **修辭策略：** '
             '這是對百姓意志的直接挑戰，將他們的未來命運與他們的選擇緊密掛鉤。這種二元對立的呈現方式，為接下來的具體律法提供了強烈的動機。\n'
             '\n'
             '**III. 律法典章：具體條例與生活應用 (申命記 12:1-26:19)**\n'
             '\n'
             '1.  **敬拜的律法：中央聖所原則 (12:1-32)：**\n'
             '    *   **目的：** 從總體原則轉向具體實踐，首先確立正確敬拜的場所和方式，以防止偶像崇拜和信仰混雜。\n'
             '    *   **進程：** '
             '摩西命令百姓進入迦南後要徹底毀滅當地偶像（12:1-4），然後立即確立「耶和華─你神所選擇的地方」（12:5）作為唯一獻祭和敬拜的中心。這限制了地方性祭壇，旨在集中敬拜，確保信仰的純潔性。他區分了日常宰殺牲畜（可在各城進行）和獻祭（必須在中央聖所），並警告不可效法迦南人的惡俗。\n'
             '    *   **修辭策略：** 透過明確的禁令和規定，為以色列的宗教生活劃定界限，強調耶和華的獨一性和聖潔性。\n'
             '\n'
             '2.  **警告假先知與引誘拜偶像者 (13:1-18)：**\n'
             '    *   **目的：** 進一步強化對偶像崇拜的零容忍態度，保護信仰的純潔性。\n'
             '    *   **進程：** '
             '緊隨中央聖所原則之後，摩西立即處理了可能引誘百姓偏離上帝的威脅——假先知和引誘者。他規定了對這些人的嚴厲處置，無論是個人、親友還是整個城邑，都必須被徹底清除。\n'
             '    *   **修辭策略：** '
             '透過極端嚴厲的懲罰，強調了對耶和華專一忠誠的絕對重要性，將偶像崇拜視為對整個社群生存的威脅。\n'
             '\n'
             '3.  **聖潔的飲食與什一奉獻條例 (14:1-29)：**\n'
             '    *   **目的：** 將聖潔原則應用於日常生活的各個方面，並確立支持利未人和窮人的經濟體系。\n'
             '    *   **進程：** '
             '從以色列作為「聖潔子民」（14:1-2）的身份出發，摩西列舉了潔淨與不潔淨的食物，以區分以色列與外邦人。接著，他詳細說明了什一奉獻的條例，不僅用於在聖所歡樂，更重要的是每三年一次要用於供給利未人、寄居者、孤兒和寡婦。\n'
             '    *   **修辭策略：** 透過飲食和奉獻的規定，將聖潔的身份具體化，並將對上帝的愛與對弱勢群體的關懷聯繫起來。\n'
             '\n'
             '4.  **關於禧年與奴隸的條例 (15:1-18)：**\n'
             '    *   **目的：** 體現上帝律法中的憐憫和公義，特別是對貧窮和受壓迫者的關懷。\n'
             '    *   **進程：** '
             '摩西規定了每七年的債務豁免（15:1-6），以及要慷慨幫助窮人（15:7-11）。接著，他詳細說明了希伯來奴隸在服事六年後必須被釋放，且要厚待他們（15:12-18）。\n'
             '    *   **修辭策略：** 這些條例展示了盟約律法超越單純的懲罰，更包含著深厚的社會倫理和人道關懷，反映了上帝的屬性。\n'
             '\n'
             '5.  **三大節期與審判官的設立 (16:1-22)：**\n'
             '    *   **目的：** 確立以色列的年度宗教節期，並建立公正的司法體系。\n'
             '    *   **進程：** '
             '摩西詳細說明了逾越節、七七節和住棚節的慶祝方式，強調這些節期是記念上帝的作為，並要與家人、利未人、寄居者一同歡樂。接著，他轉向設立審判官和官長，強調要秉公行義，不可屈枉正直（16:18-20）。\n'
             '    *   **修辭策略：** '
             '透過宗教節期強化民族認同和對上帝的記憶，同時透過司法體系確保社會的公義和秩序，將信仰與社會治理結合。\n'
             '\n'
             '6.  **關於獻祭、法庭與君王的條例 (17:1-20)：**\n'
             '    *   **目的：** 規範宗教獻祭的純潔性，建立最高司法權威，並對未來君王的權力進行限制。\n'
             '    *   **進程：** '
             '從獻祭的純潔性（不可獻有殘疾的牲畜）和對拜偶像者的嚴厲處置開始（17:1-7）。接著，他設立了處理難斷案件的最高法庭（17:8-13），確保司法公正。最重要的是，他預見了以色列將來會立王，並對君王提出了嚴格的限制（不可多馬、多妻、多金銀），要求君王要抄寫律法並終身誦讀（17:14-20）。\n'
             '    *   **修辭策略：** '
             '這些條例預防了權力的濫用，確保了律法在宗教、司法和政治領域的至高無上地位，特別是將君王置於律法之下，這是古代近東罕見的。\n'
             '\n'
             '7.  **祭司、利未人與先知的條例 (18:1-22)：**\n'
             '    *   **目的：** 確立宗教領袖的職責和權利，並警告百姓遠離異教習俗，期待真正的先知。\n'
             '    *   **進程：** '
             '摩西說明了利未支派沒有產業，靠獻祭和百姓的奉獻為生（18:1-8），確保他們能專心事奉。他嚴禁百姓效法迦南地的各種可憎之術（18:9-14），強調與異教的徹底決裂。然後，他預言耶和華必從以色列人中興起一位像摩西的先知（18:15-19），並提供了分辨真假先知的方法（18:20-22）。\n'
             '    *   **修辭策略：** '
             '透過確立宗教領袖的地位和職責，確保信仰的傳承和教導。預言「像摩西的先知」為未來的神聖啟示留下了空間，也為彌賽亞的到來埋下伏筆。\n'
             '\n'
             '8.  **公義與憐憫的條例 (19:1-25:19)：**\n'
             '    *   **目的：** '
             '涵蓋了民事、刑事、社會、家庭等各方面的具體律法，將盟約原則應用於日常生活的方方面面，體現公義與憐憫。\n'
             '    *   **進程：** '
             '這部分內容極為廣泛，從設立逃城（19:1-13）保障誤殺者的生命，到對誣告者的嚴懲（19:15-21）保障司法公正；從戰爭的條例（20:1-20）到處理未知兇殺案的儀式（21:1-9）；從家庭倫理（21:10-22:30）到社區純潔（23:1-25），再到對弱勢群體的關懷（24:17-22）。這些律法細緻入微，旨在建立一個基於上帝公義和憐憫的理想社會。\n'
             '    *   **修辭策略：** '
             '透過具體而詳細的條例，將抽象的「愛神愛人」原則轉化為可操作的行為規範，展示了律法對社會各個層面的全面影響。強調「以眼還眼」的原則（19:21）是為了限制報復，確保公正，而非鼓勵殘忍。\n'
             '\n'
             '9.  **初熟之物與什一奉獻的告白 (26:1-19)：**\n'
             '    *   **目的：** 總結所有律法，將遵守律法提升為一種敬拜和盟約的宣告，並再次確認以色列作為上帝特選子民的身份。\n'
             '    *   **進程：** '
             '在所有具體律法之後，摩西提供了一個儀式性的告白。當百姓獻上初熟之物時，要回顧上帝如何將他們從埃及為奴之家領出來，賜予他們這流奶與蜜之地（26:5-10）。這將奉獻行為與以色列的救贖歷史緊密相連。三年一次的什一奉獻告白（26:12-15）也強調了對利未人、寄居者、孤兒寡婦的關懷。最後，摩西總結說，遵守這些誡命將使以色列成為耶和華的特選子民，聖潔的國度（26:16-19）。\n'
             '    *   **修辭策略：** '
             '透過儀式性的告白，將律法從單純的規則變為一種感恩和委身的表達，強化了盟約的關係性。這部分是律法核心的總結和高潮，再次將歷史、律法和身份融為一體。\n'
             '\n'
             '**IV. 盟約的確認：祝福與咒詛 (申命記 27:1-30:20)**\n'
             '\n'
             '1.  **在以巴路山和基利心山立約的儀式 (27:1-26)：**\n'
             '    *   **目的：** 透過一個戲劇性的公共儀式，使盟約的祝福與咒詛具體化，讓百姓親身參與盟約的確認。\n'
             '    *   **進程：** '
             '摩西命令百姓進入迦南後，要在以巴路山築壇，將律法寫在石頭上，並在兩座山上宣告祝福與咒詛。他列舉了十二項具體咒詛（27:15-26），百姓要回應「阿們」。\n'
             '    *   **修辭策略：** 這種儀式性的宣告，旨在給百姓留下深刻的印象，使他們意識到盟約的嚴肅性和不可違背性。\n'
             '\n'
             '2.  **順服的祝福 (28:1-14)：**\n'
             '    *   **目的：** 詳細描繪順服上帝誡命所帶來的豐盛生活，激勵百姓選擇順服。\n'
             '    *   **進程：** '
             '摩西列舉了順服將帶來的一切美好：在城裡蒙福，在田間蒙福，身所生的、地所產的、牲畜所下的都蒙福，戰勝仇敵，作首不作尾。\n'
             '    *   **修辭策略：** 透過具體而全面的祝福，描繪了一個理想的、蒙上帝恩寵的民族圖景，激發百姓對美好未來的嚮往。\n'
             '\n'
             '3.  **悖逆的咒詛 (28:15-68)：**\n'
             '    *   **目的：** 極其詳細和生動地描繪悖逆上帝誡命所帶來的災難性後果，以強烈的恐懼感震懾百姓，使其不敢輕忽盟約。\n'
             '    *   **進程：** '
             '這是《申命記》中最長、最為震撼的部分。摩西從普遍的咒詛（28:15-19）開始，逐步升級到疾病、飢荒、戰敗、被擄、羞辱、土地荒涼、甚至在極端飢荒中吃自己兒女的肉，最終是被分散到列國，不得安寧。這些咒詛預言了以色列歷史上的許多悲劇。\n'
             '    *   **修辭策略：** '
             '透過極其詳盡和可怕的描述，摩西旨在讓百姓深刻認識到背棄盟約的嚴重性，將其與民族的存亡緊密掛鉤。這是一個強烈的警示，為後來的申命記歷史提供了神學解釋框架。\n'
             '\n'
             '4.  **在摩押地所立的盟約與悔改的應許 (29:1-30:10)：**\n'
             '    *   **目的：** '
