             '路加在《使徒行傳》中的思想進程，是圍繞著耶穌升天前所頒布的「大使命」和「聖靈應許」展開的，即「**但聖靈降臨在你們身上，你們就必得著能力，並要在耶路撒冷、猶太全地，和撒馬利亞，直到地極，作我的見證。**」（徒1:8）。這節經文不僅是全書的地理大綱，更是其神學和敘事結構的藍圖。路加透過一系列精心選擇和編排的事件、講道和人物互動，逐步深化讀者對聖靈工作、見證的意義、教會的本質以及福音普世性的理解。\n'
             '\n'
             '### 第一階段：奠定基礎——聖靈在耶路撒冷的降臨與教會的誕生 (徒1:1-8:3)\n'
             '\n'
             '路加首先確立了福音事工的**神聖起源和權威基礎**。\n'
             '\n'
             '1.  **承接耶穌事工與聖靈應許 (徒1:1-11)：**\n'
             '    *   **思想進程：** '
             '路加開篇即將《使徒行傳》定位為《路加福音》的續集，強調耶穌復活後的四十天教導，證明了祂的復活與神國的真實性。他刻意記錄了門徒對「復興以色列國」的猶太民族主義期待（徒1:6），隨即引出耶穌對聖靈降臨和普世見證的應許（徒1:8）。這立刻將讀者的焦點從狹隘的政治期望轉向廣闊的屬靈使命，並預設了聖靈將是這一切的驅動力。耶穌的升天標誌著祂在地上的事工告一段落，但同時也開啟了聖靈時代的序幕，為門徒的宣教提供了神聖的授權。\n'
             '    *   **關鍵詞句：** '
             '「**我已經作了前書**」（徒1:1），「**四十天之久向他們顯現，講說神國的事**」（徒1:3），「**你們要受聖靈的洗**」（徒1:5），「**直到地極，作我的見證**」（徒1:8）。\n'
             '\n'
             '2.  **使徒的預備與權柄的確立 (徒1:12-26)：**\n'
             '    *   **思想進程：** '
             '在聖靈降臨之前，路加展示了門徒的**順服與合一**。他們恆切禱告，並在彼得的帶領下，根據舊約預言和耶穌的教導，補選了馬提亞，恢復了十二使徒的完整性。這強調了使徒群體作為耶穌復活見證人的**獨特權柄和重要性**，為即將到來的聖靈工作和教會建立提供了穩固的領導核心。\n'
             '    *   **關鍵詞句：** 「**都同心合意的恆切禱告**」（徒1:14），「**作他復活的見證**」（徒1:22）。\n'
             '\n'
             '3.  **五旬節聖靈降臨與教會的誕生 (徒2:1-47)：**\n'
             '    *   **思想進程：** '
             '這是全書的**轉捩點**。路加以戲劇性的方式描繪聖靈的降臨，伴隨著大風、火焰和說方言的神蹟，直接應驗了耶穌的應許。彼得的講道是核心，他巧妙地引用舊約（約珥書和詩篇），證明耶穌就是彌賽亞，祂的受死、復活和升天都是神的預定，並呼召人悔改。聖靈的降臨不僅賦予門徒能力，更直接促成了三千人歸主，標誌著**教會的正式誕生**。路加隨後描繪了早期教會的共同生活，強調了教訓、團契、擘餅和禱告，展現了聖靈工作所帶來的**內在轉變和社群凝聚力**。\n'
             '    *   **關鍵詞句：** '
             '「**都被聖靈充滿，按著聖靈所賜的口才說起別國的話來**」（徒2:4），「**你們釘在十字架上的這位耶穌，神已經立他為主，為基督了**」（徒2:36），「**你們各人要悔改，奉耶穌基督的名受洗，叫你們的罪得赦，就必領受所賜的聖靈**」（徒2:38），「**主將得救的人天天加給他們**」（徒2:47）。\n'
             '\n'
             '4.  **教會的成長與初期挑戰 (徒3:1-6:7)：**\n'
             '    *   **思想進程：** '
             '路加展示了聖靈透過使徒所行的**神蹟奇事**（彼得醫治瘸腿者），這些神蹟成為福音信息的有力印證。彼得的講道再次強調耶穌的復活和悔改的必要性。隨之而來的是**外部的逼迫**（公會的審問），這證明了福音的真實性，也考驗了信徒的信心。路加刻意記錄了使徒們「**順從神，不順從人，是應當的**」（徒5:29）的堅定立場，以及他們在逼迫中禱告後再次被聖靈充滿，放膽傳道。同時，他也揭示了**內部的挑戰**（亞拿尼亞和撒非喇的欺哄），強調了教會的聖潔和聖靈的鑑察。選立七位執事則顯示了教會在面對內部問題時的**智慧和組織能力**，確保了使徒能專心於禱告和傳道，也為福音的進一步擴展奠定了基礎。\n'
             '    *   **關鍵詞句：** '
             '「**奉拿撒勒人耶穌基督的名，叫你起來行走！**」（徒3:6），「**除他以外，別無拯救**」（徒4:12），「**欺哄聖靈**」（徒5:3），「**神的道興旺起來；在耶路撒冷門徒數目加增的甚多**」（徒6:7）。\n'
             '\n'
             '5.  **司提反的殉道與福音的擴散 (徒6:8-8:3)：**\n'
             '    *   **思想進程：** '
             '司提反的事件是耶路撒冷階段的**高潮和轉捩點**。路加將司提反描繪為一位「**被聖靈充滿，智慧充足**」的執事，他的講道不僅是為自己辯護，更是對以色列歷史的深刻反思，指出他們一貫拒絕神和祂的使者，最終拒絕了彌賽亞。司提反的殉道，以及掃羅（保羅）的參與，引發了耶路撒冷教會的**大逼迫**。然而，路加強調這並非失敗，而是神旨意的成就——信徒們「**分散在猶太和撒馬利亞各處**」，將福音帶出了耶路撒冷，為下一階段的擴展鋪平了道路。\n'
             '    *   **關鍵詞句：** '
             '「**被聖靈充滿，定睛望天，看見神的榮耀**」（徒7:55），「**從這日起，耶路撒冷的教會大遭逼迫，除了使徒以外，門徒都分散在猶太和撒馬利亞各處**」（徒8:1）。\n'
             '\n'
             '### 第二階段：跨越界限——福音在猶太全地和撒馬利亞的傳播 (徒8:4-12:25)\n'
             '\n'
             '此階段路加著重描繪福音如何**突破種族和文化界限**，從猶太人傳向撒馬利亞人，並最終向外邦人開放。\n'
             '\n'
             '1.  **腓利在撒馬利亞和迦薩的宣教 (徒8:4-40)：**\n'
             '    *   **思想進程：** '
             '腓利的宣教是「撒馬利亞」部分的直接應驗。路加展示了福音如何藉著神蹟奇事在撒馬利亞人中廣傳，並透過彼得和約翰的按手，使撒馬利亞信徒領受聖靈，確認了他們在教會中的**完全接納**。這打破了猶太人與撒馬利亞人之間的長期隔閡。隨後，腓利在聖靈的引導下，向埃提阿伯太監傳福音，這不僅展示了**聖靈對個人宣教的精確引導**，更預示了福音將跨越地理和種族的界限，觸及更遠的外邦世界。\n'
             '    *   **關鍵詞句：** '
             '「**腓利下撒馬利亞城去，宣講基督**」（徒8:5），「**使徒按手在他們頭上，他們就受了聖靈**」（徒8:17），「**腓利就開口從這經上起，對他講說耶穌**」（徒8:35）。\n'
             '\n'
             '2.  **掃羅的歸主：外邦人使徒的興起 (徒9:1-31)：**\n'
             '    *   **思想進程：** '
             '這是《使徒行傳》中**最關鍵的轉折點之一**。路加三次重複保羅的歸主經歷（徒9章、22章、26章），足見其重要性。他刻意描繪掃羅從一個狂熱的逼迫者，被復活的耶穌親自呼召，轉變為外邦人的使徒。這不僅證明了**耶穌基督復活的大能**，更顯示了**神主權的揀選**，為福音向外邦世界的廣泛傳播預備了最核心的器皿。亞拿尼亞的順服也強調了神如何使用普通信徒來成就祂的旨意。\n'
             '    *   **關鍵詞句：** '
             '「**掃羅，掃羅！你為什麼逼迫我？**」（徒9:4），「**他是我所揀選的器皿，要在外邦人和君王並以色列人面前宣揚我的名**」（徒9:15）。\n'
             '\n'
             '3.  **彼得與哥尼流：外邦人歸主的突破 (徒9:32-11:18)：**\n'
             '    *   **思想進程：** '
             '路加在此處再次聚焦彼得，展示了神如何透過異象和聖靈的引導，**親自打破猶太人對外邦人的隔閡**。彼得的異象（潔淨不潔之物）直接挑戰了猶太人的飲食律法和潔淨觀念，為他進入外邦人哥尼流的家鋪路。當聖靈在彼得講道時降臨在哥尼流一家身上，甚至在他們受洗之前，這無可辯駁地證明了**神對外邦人的接納**，與猶太信徒毫無二致。彼得回到耶路撒冷後，向教會解釋這一事件，最終促使耶路撒冷教會承認「**神也賜恩給外邦人，叫他們悔改得生命了**」（徒11:18），這是福音普世性的一個里程碑。\n'
             '    *   **關鍵詞句：** '
             '「**神所潔淨的，你不可當作俗物**」（徒10:15），「**我真看出神是不偏待人。原來，各國中那敬畏主、行義的人都為主所悅納**」（徒10:34-35），「**聖靈降在一切聽道的人身上**」（徒10:44）。\n'
             '\n'
             '4.  **安提阿教會的興起與巴拿巴、掃羅的事奉 (徒11:19-30)：**\n'
             '    *   **思想進程：** '
             '路加將安提阿描繪為**外邦宣教的中心**。因耶路撒冷逼迫而分散的信徒，在安提阿開始向希臘人傳福音，並取得了巨大成功。巴拿巴被差遣前往，並邀請掃羅一同事奉，這顯示了耶路撒冷教會對外邦宣教的**認可和支持**。安提阿教會不僅是外邦宣教的發源地，也是「**基督徒**」（徒11:26）這個名稱首次出現的地方，標誌著信徒群體身份的確立，不再僅僅是猶太教的一個分支。\n'
             '    *   **關鍵詞句：** '
             '「**主的手與他們同在；信而歸主的人很多**」（徒11:21），「**門徒稱為基督徒是從安提阿起首**」（徒11:26）。\n'
             '\n'
             '5.  **希律王的逼迫與彼得的獲釋 (徒12:1-25)：**\n'
             '    *   **思想進程：** '
             '路加再次插入一段關於逼迫的敘述，展示了教會在面對強大政治勢力時的**脆弱與神的保守**。雅各的殉道顯示了信仰的代價，而彼得的奇蹟獲釋則強調了**神對祂僕人的超自然保護**，以及教會恆切禱告的力量。希律王的悲慘結局（被蟲咬死）與「**神的道日見興旺，越發廣傳**」（徒12:24）形成鮮明對比，有力地證明了神的主權和福音的不可阻擋。這也為彼得逐漸淡出敘事，保羅成為主角做好了鋪墊。\n'
             '    *   **關鍵詞句：** '
             '「**教會卻為他切切的禱告神**」（徒12:5），「**主的使者開了監門，領他們出來**」（徒12:10），「**他被蟲所咬，氣就絕了**」（徒12:23）。\n'
             '\n'
             '### 第三階段：直到地極——保羅的宣教旅程與福音抵達羅馬 (徒13:1-28:31)\n'
             '\n'
             '此階段是《使徒行傳》的重心，路加詳細記錄了保羅三次宣教旅程和被捕受審的經歷，展現了福音如何**跨越帝國的疆界**，最終抵達羅馬。\n'
             '\n'
             '1.  **第一次宣教旅程：模式的確立 (徒13:1-14:28)：**\n'
             '    *   **思想進程：** '
             '路加從安提阿教會在聖靈引導下的差派開始，確立了**普世宣教的模式**：聖靈的呼召、教會的差遣、使徒的傳道（先向猶太人，後向外邦人）、神蹟奇事的印證、猶太人的拒絕、外邦人的接受、逼迫與建立教會。保羅在彼西底安提阿的講道（徒13:16-41）是福音信息的典範，總結了救贖歷史。猶太人的拒絕促使保羅宣告「**我們就轉向外邦人去**」（徒13:46），這是福音傳播策略的關鍵轉變。旅程結束後，保羅和巴拿巴向安提阿教會報告「**神怎樣為外邦人開了信道的門**」（徒14:27），強調了外邦宣教的成功。\n'
             '    *   **關鍵詞句：** '
             '「**聖靈說：『要為我分派巴拿巴和掃羅，去作我召他們所作的工。』**」（徒13:2），「**我已經立你作外邦人的光，叫你施行救恩，直到地極**」（徒13:47）。\n'
             '\n'
             '2.  **耶路撒冷會議：外邦人歸主的正式確認 (徒15:1-35)：**\n'
             '    *   **思想進程：** '
             '這是教會歷史上**最關鍵的神學爭議**之一。路加將此會議置於保羅第一次宣教旅程之後，顯示了外邦人歸主所帶來的實際問題。彼得、巴拿巴、保羅的見證，以及雅各根據舊約預言（阿摩司書）所作的裁決，最終確立了**外邦信徒無需受割禮或遵守摩西律法即可得救**的原則。這不僅解決了教會內部的重大分歧，更為福音向外邦世界的自由傳播**移除了巨大的障礙**，從神學上確認了福音的普世性和恩典的本質。\n'
             '    *   **關鍵詞句：** '
             '「**若不按摩西的規條受割禮，就不能得救**」（徒15:1），「**我們得救乃是因主耶穌的恩，和他們一樣**」（徒15:11），「**聖靈和我們，定意不將別的重擔加給你們**」（徒15:28）。\n'
             '\n'
             '3.  **第二次宣教旅程：福音進入歐洲 (徒15:36-18:22)：**\n'
             '    *   **思想進程：** '
             '路加強調了**聖靈對宣教方向的引導**。保羅和巴拿巴的分開，以及西拉和提摩太的加入，顯示了宣教團隊的調整。最重要的是「**馬其頓的呼聲**」（徒16:9-10），聖靈明確引導保羅將福音帶入歐洲。在腓立比，呂底亞的歸主和禁卒的信主，展示了福音如何觸及不同社會階層的人。在帖撒羅尼迦和庇哩亞，路加對庇哩亞信徒「**天天查考聖經**」（徒17:11）的讚揚，強調了聖經在信仰中的重要性。保羅在雅典亞略巴古的講道，則展示了福音信息在不同文化背景下的**適應性**，從哲學角度切入，引導人認識獨一真神。在哥林多，路加記錄了主在異象中對保羅的鼓勵（徒18:9-10），再次強調了**神對宣教士的同在與保護**。\n'
             '    *   **關鍵詞句：** '
             '「**夜間有異象現與保羅。有一個馬其頓人站著求他說：『請你過到馬其頓來幫助我們！』**」（徒16:9），「**主就開導他的心，叫他留心聽保羅所講的話**」（徒16:14），「**世人蒙昧無知的時候，神並不監察，如今卻吩咐各處的人都要悔改**」（徒17:30）。\n'
             '\n'
             '4.  **第三次宣教旅程：堅固教會與預備羅馬之行 (徒18:23-21:16)：**\n'
             '    *   **思想進程：** '
             '此旅程的重點是**堅固已建立的教會**，特別是在以弗所的長期事奉。路加記錄了亞波羅的出現，以及保羅為約翰門徒施洗，強調了**聖靈洗禮的完整性**。以弗所的騷亂（徒19:23-41）顯示了福音對異教文化的衝擊。保羅在米利都對以弗所長老的告別講話（徒20:17-38），是保羅牧養心腸的展現，也是對未來挑戰的預言，特別是「**凶暴的豺狼進入你們中間**」（徒20:29）。儘管有聖靈透過亞迦布和其他人預言的捆鎖和患難，保羅仍堅定地「**心靈被捆綁**」前往耶路撒冷，因為他知道這是神旨意的一部分，最終將引導他到羅馬。\n'
             '    *   **關鍵詞句：** '
             '「**聖靈便降在他們身上，他們就說方言，又說預言**」（徒19:6），「**我到了那裡以後，也必須往羅馬去看看**」（徒19:21），「**我卻不以性命為念，也不看為寶貴，只要行完我的路程，成就我從主耶穌所領受的職事，證明神恩惠的福音**」（徒20:24），「**願主的旨意成就！**」（徒21:14）。\n'
             '\n'
