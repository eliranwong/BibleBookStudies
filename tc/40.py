             '然而，現代學術界對此存在討論，認為現存的希臘文《馬太福音》可能是一位匿名的猶太基督徒文士，在參考了《馬可福音》及其他資料（如假想的「Q資料」）的基礎上，並結合使徒馬太所收集的耶穌言論而編纂而成。儘管如此，書中對舊約的深入理解和猶太背景知識，仍與一位猶太基督徒作者的身份相符。\n'
             '\n'
             '寫作日期通常定在**公元80-90年之間**。此推斷基於以下幾點：\n'
             '1.  對《馬可福音》的依賴（《馬可福音》約公元65-70年完成）。\n'
             '2.  書中對耶路撒冷聖殿被毀（公元70年）的暗示（馬太福音 22:7；23:38）。\n'
             '3.  「教會」（ekklesia）一詞的出現（馬太福音 16:18；18:17），反映早期教會組織的發展。\n'
             '4.  對法利賽人的嚴厲批判，顯示公元一世紀末猶太教與基督教日益加劇的張力與分離。\n'
             '\n'
             '### 二、 結構與思想進程\n'
             '\n'
             '《馬太福音》的結構嚴謹，常被學者分為序言、五個主要敘事與講論單元，以及受難與復活的結尾。其思想進程可概括為：**確立彌賽亞身份 '
             '→ 宣告天國降臨及其倫理 → 展現彌賽亞的權柄與天國的奧秘 → 門徒訓練與教會的建立 → 彌賽亞的受苦與得勝 → '
             '普世使命的頒布。**\n'
             '\n'
             '**I. 耶穌的降生與預備事工 (馬太福音 1:1-4:11)**\n'
             '*   **A. 耶穌的家譜與降生 (1:1-2:23)：** '
             '透過家譜（追溯至亞伯拉罕和大衛）確立耶穌的彌賽亞合法血統。降生敘事強調「以馬內利」（神與我們同在）的神性，並透過博士朝拜與希律的敵意，預示耶穌的王權與將面臨的拒絕。頻繁的「應驗公式」將耶穌的生命與舊約預言緊密連結。\n'
             '*   **B. 施洗約翰的事工與耶穌受洗 (3:1-17)：** '
             '約翰作為先鋒預備主的道，宣告「天國近了」。耶穌受洗時，天父公開認證其為「愛子」，聖靈降臨，三重見證確立耶穌的神性與彌賽亞身份。\n'
             '*   **C. 耶穌受試探 (4:1-11)：** '
             '耶穌在曠野以神的話語抵擋撒但，證明他將以順服神而非世俗權力來建立天國，與以色列在曠野的失敗形成對比。\n'
             '\n'
             '**II. 耶穌在加利利的事工：天國的宣告與教導 (馬太福音 4:12-18:35)**\n'
             '*   **A. 加利利事工的開始與呼召門徒 (4:12-25)：** '
             '耶穌在加利利傳講「天國近了」，呼召門徒，並以教導、傳道、醫治全面彰顯天國的權柄。\n'
             '*   **B. 第一個主要講論：登山寶訓——天國的憲章 (5:1-7:29)：** '
             '耶穌在此頒布天國子民的倫理標準，強調「八福」、門徒作「鹽和光」的影響力，並將律法的要求從外在行為提升到內心動機（「來不是要廢掉律法，乃是要成全」5:17），揭示天國更高的義。\n'
             '*   **C. 神蹟敘事：彌賽亞的權柄與天國的彰顯 (8:1-9:38)：** '
             '一系列醫病、趕鬼、平靜風浪的神蹟，證明耶穌勝過疾病、死亡、自然界和邪惡勢力，彰顯天國的能力。\n'
             '*   **D. 第二個主要講論：差遣門徒——天國的擴展 (10:1-42)：** '
             '耶穌賦予十二使徒權柄，指示他們宣教策略、將面臨的逼迫，以及作門徒的代價。\n'
             '*   **E. 對耶穌身份的疑問與天國的奧秘 (11:1-12:50)：** '
             '施洗約翰的疑問、耶穌對不悔改之城的責備與對勞苦者的邀請，以及與法利賽人關於安息日和褻瀆聖靈的爭論，揭示了人們對彌賽亞的誤解與天國真理的隱藏。\n'
             '*   **F. 第三個主要講論：天國的比喻——天國的本質與發展 (13:1-58)：** '
             '透過撒種、稗子、芥菜種等比喻，耶穌解釋天國的奧秘、其成長方式、內在混合性、無比價值及最終審判。\n'
             '*   **G. 門徒訓練與轉向耶路撒冷 (14:1-17:27)：** '
             '施洗約翰之死預示耶穌命運。餵飽五千人、水面行走等神蹟，強調信心。彼得認信耶穌為「基督，是永生神的兒子」（16:16），耶穌隨後宣告「我要把我的教會建造在這磐石上」（16:18），並首次預言受難，教導作門徒的代價。\n'
             '*   **H. 第四個主要講論：教會講論——天國子民的群體生活 (18:1-35)：** '
             '耶穌教導謙卑、不可使人跌倒、尋找迷羊、弟兄間的勸戒與饒恕，為教會內部生活提供了倫理和紀律指引。\n'
             '\n'
             '**III. 耶穌前往耶路撒冷與在猶太地的教導 (馬太福音 19:1-23:39)**\n'
             '*   **A. 前往耶路撒冷途中的教導 (19:1-20:34)：** '
             '論婚姻、財富的危險、作僕人的教導，並三次預言受難，強調耶穌的道路是捨己服事。\n'
             '*   **B. 在耶路撒冷的衝突與教導 (21:1-23:39)：** '
             '耶穌榮入聖城宣告王權，潔淨聖殿彰顯權柄。與猶太領袖就權柄、納稅、復活等問題展開激烈辯論，透過兇惡園戶、婚筵等比喻譴責他們的拒絕。最終，耶穌對文士和法利賽人發出「七禍」，嚴厲譴責他們的虛偽和假冒為善，並為耶路撒冷哀哭。\n'
             '\n'
             '**IV. 耶穌的第五個主要講論：末世論 (馬太福音 24:1-25:46)**\n'
             '*   **A. 橄欖山講論：** 耶穌預言聖殿被毀與末世徵兆，強調大災難與人子的降臨。\n'
             '*   **B. 警醒與預備：** '
             '透過無花果樹、忠僕、十個童女、按才幹受責任等比喻，教導門徒要時刻警醒、忠心服事，因人子再來的日子無人知曉。\n'
             '*   **C. 末日審判：** '
             '綿羊和山羊的比喻揭示審判的標準是人如何對待「我這弟兄中一個最小的」（25:40），強調對弱勢群體的憐憫和服事。\n'
             '\n'
             '**V. 耶穌的受難與復活 (馬太福音 26:1-28:20)**\n'
             '*   **A. 受難的預備 (26:1-46)：** '
             '耶穌再次預言受難，馬利亞膏抹，猶大出賣，設立聖餐（「我立約的血，為多人流出來，使罪得赦」26:28），客西馬尼園禱告順服天父旨意。\n'
             '*   **B. 耶穌被捕與受審 (26:47-27:26)：** '
             '耶穌被捕，在大祭司和彼拉多面前受審，彼得三次不認主，猶大結局，彼拉多洗手將耶穌交給群眾釘十字架。\n'
             '*   **C. 耶穌被釘十字架與埋葬 (27:27-66)：** '
             '兵丁戲弄，耶穌被釘十字架，在極大痛苦中死去（「我的神，我的神，為什麼離棄我？」27:46），聖殿幔子裂開等神蹟彰顯其死之意義。亞利馬太的約瑟安葬耶穌，墳墓被看守。\n'
             '*   **D. 耶穌的復活與大使命 (28:1-20)：** '
             '婦女發現空墳，天使宣告耶穌復活，耶穌向婦女顯現。兵丁受賄作假見證。復活的耶穌頒布「大使命」：「天上地下所有的權柄都賜給我了。所以，你們要去，使萬民作我的門徒，奉父、子、聖靈的名給他們施洗。凡我所吩咐你們的，都教訓他們遵守，我就常與你們同在，直到世界的末了。」（28:18-20）。這總結了全書主題，並開啟了教會的普世使命。\n'
             '\n'
             '### 三、 歷史與文化背景\n'
             '\n'
             '《馬太福音》寫於公元一世紀末，羅馬帝國統治下的猶太地區，特別是在公元70年耶路撒冷聖殿被毀之後。理解這些背景對於詮釋福音書至關重要。\n'
             '\n'
             '1.  **猶太文化背景與早期基督徒的掙扎：**\n'
             '    *   **第二聖殿時期猶太教的多元性：** '
             '法利賽人（強調口傳律法，會堂為中心）與撒都該人（祭司貴族，不信復活）的教導與耶穌形成對比。耶穌與法利賽人的衝突（如安息日爭論、潔淨禮儀、23章的「七禍」）反映了早期基督徒與法利賽人主導的猶太教之間的張力。基督徒需要證明耶穌的教導超越了法利賽人的形式主義，活出「勝於文士和法利賽人的義」（馬太福音 '
             '5:20）。\n'
             '    *   **彌賽亞期望：** '
             '當時猶太人普遍期待一位政治性、軍事性的彌賽亞。耶穌被釘十字架對猶太基督徒構成神學困境。馬太福音透過家譜、博士朝拜、榮入聖城等強調耶穌的王權，同時透過三次預言受難，修正了政治性期望，指出彌賽亞必須先受苦、被殺，然後復活。\n'
             '    *   **聖殿毀滅與猶太教重組：** '
             '公元70年聖殿被毀，加速了猶太教與基督教的分離。猶太基督徒可能在會堂中被排斥。馬太福音透過頻繁的「應驗公式」確立基督徒信仰的合法性，並透過耶穌對「教會」（馬太福音 '
             '16:18）的建立，為猶太基督徒提供新的身份認同和社群歸屬感。\n'
             '    *   **律法與恩典的張力：** '
             '耶穌在登山寶訓中對律法的闡釋，強調內在的潔淨和愛，為猶太基督徒在遵守律法方面提供了新的倫理框架，幫助他們理解新約的倫理是舊約的深化與成全。\n'
             '    *   **普世使命的轉變：** 從耶穌初期「往以色列家迷失的羊那裡去」（馬太福音 '
             '10:5-6）到最終的「使萬民作我的門徒」（馬太福音 '
             '28:19），反映了早期教會在猶太人普遍拒絕福音後，轉向外邦人宣教的歷史進程。\n'
             '\n'
             '2.  **希臘羅馬文化背景與早期基督徒的掙扎：**\n'
             '    *   **羅馬統治與社會結構：** '
             '羅馬帝國的政治壓力、稅收和社會階層（強調榮譽、地位、財富）對基督徒構成挑戰。耶穌關於納稅給凱撒的教導（馬太福音 '
             '22:15-22）為基督徒提供了平衡對神忠誠與對世俗權柄順服的指引。耶穌對謙卑、服事和憐憫弱勢的強調（馬太福音 '
             '20:26；25:31-46）挑戰了當時社會的價值觀。\n'
             '    *   **迫害的威脅：** '
             '基督徒可能因失去猶太教的合法宗教地位而面臨羅馬帝國的迫害。耶穌對門徒將受逼迫的預言（馬太福音 '
             '10:16-25；24:9-14）為社群提供了心理準備和堅忍的鼓勵。\n'
             '\n'
             '這些背景因素共同塑造了《馬太福音》的寫作目的和內容，使其成為一份回應早期基督徒社群在信仰、倫理和身份認同上掙扎的重要文獻。\n'
             '\n'
             '### 四、 關鍵主題與神學信息\n'
             '\n'
             '《馬太福音》透過反覆出現的關鍵字和概念，傳達了深刻的神學信息：\n'
             '\n'
             '1.  **天國 (Kingdom of Heaven / ἡ βασιλεία τῶν οὐρανῶν)：**\n'
             '    *   **闡述：** '
             '馬太福音最核心的主題，出現32次。它既是現在藉著耶穌降臨的現實，也是將來完全實現的盼望。它是一種神的主權統治，要求人悔改並順服其倫理。\n'
             '    *   **希臘文洞見：** 獨特使用「天國」而非「神的國」，反映猶太文化對神名諱的敬畏，強調國度源於天上。\n'
             '    *   **神學意涵：** 揭示神的**主權性**、**掌權性**和**救贖計劃**，透過耶穌基督建立子民群體。\n'
             '    *   **舊約貢獻：** 根植於但以理書 2:44、7:13-14 等對神永恆國度的預言。\n'
             '    *   **對連結的影響：** 呼召我們將神視為君王，順服其旨意，活出天國倫理，並參與其擴展。\n'
             '\n'
             '2.  **耶穌是彌賽亞／大衛的子孫 (Jesus as Messiah / Son of David)：**\n'
             '    *   **闡述：** 從家譜開始（馬太福音 1:1），透過博士朝拜、彼得認信（馬太福音 '
             '16:16）及榮入聖城，不斷強調耶穌是舊約預言的彌賽亞君王。\n'
             '    *   **希臘文洞見：** Χριστός (Christos) 意為「受膏者」，υἱὸς Δαυίδ (huios '
             'Dauid) 強調其王室血統與大衛之約的連結。\n'
             '    *   **神學意涵：** 彰顯神的**信實**和**守約**，大衛之約在耶穌身上應驗，他是帶來救贖的屬靈君王。\n'
             '    *   **舊約貢獻：** 應驗撒母耳記下 7:12-16、以賽亞書 9:6-7 等對彌賽亞君王的預言。\n'
             '    *   **對連結的影響：** 加深對神信實的信任，呼召我們將耶穌視為生命的主和君王。\n'
             '\n'
             '3.  **舊約的應驗 (Fulfillment of the Old Testament / πληρόω)：**\n'
             '    *   **闡述：** 頻繁使用「應驗公式」，將耶穌的生平事件與舊約預言連結（如馬太福音 1:22-23 引用以賽亞書 '
             '7:14）。\n'
             '    *   **希臘文洞見：** πληρόω (plēroō) '
             '不僅指字面實現，更指耶穌「充滿」了舊約的意義，將其帶到最終完成。\n'
             '    *   **神學意涵：** '
             '彰顯神的**主權性**、**全知性**和**信實性**，他的話語真實可靠，救贖計劃在耶穌身上達到高潮。\n'
             '    *   **舊約貢獻：** 馬太福音直接引用以賽亞書 7:14、彌迦書 5:2、以賽亞書 9:1-2、53:4 '
             '等多處舊約經文。\n'
             '    *   **對連結的影響：** 增強對聖經權威和神話語真實性的信心，鼓勵深入學習舊約。\n'
             '\n'
             '4.  **公義 (Righteousness / δικαιοσύνη)：**\n'
             '    *   **闡述：** 耶穌要求門徒的義必須「勝於文士和法利賽人的義」（馬太福音 '
             '5:20），強調內在動機的純正，超越外在律法遵守。\n'
             '    *   **希臘文洞見：** δικαιοσύνη (dikaiosynē) '
             '指符合神旨意的生命狀態和行為，強調內心純潔。\n'
             '    *   **神學意涵：** 揭示神的**聖潔**和**公義**，他要求子民活出聖潔，並透過耶穌基督使人稱義。\n'
             '    *   **舊約貢獻：** 舊約律法（申命記 6:25）和先知書（阿摩司書 5:24）對公義的教導，在耶穌裡得到深化。\n'
             '    *   **對連結的影響：** 挑戰我們審視內心，追求超越世俗的聖潔生活，依靠神恩典活出公義。\n'
             '\n'
             '5.  **門徒 (Disciples / μαθητής)：**\n'
             '    *   **闡述：** 耶穌呼召、教導、差遣門徒，他們是天國倫理的實踐者和使命繼承者。大使命（馬太福音 '
             '28:19）將門徒角色擴展為宣教者和教導者。\n'
             '    *   **神學意涵：** 強調委身於基督的教導和使命，是神建立忠心跟隨者群體的計劃。\n'
             '    *   **對連結的影響：** 呼召我們委身於基督的教導，並參與他的使命。\n'
             '\n'
             '6.  **教會 (Church / ἐκκλησία)：**\n'
             '    *   **闡述：** 馬太福音是唯一直接提及「教會」的福音書（馬太福音 '
             '16:18；18:17），預言其建立，並提供內部紀律原則。\n'
             '    *   **神學意涵：** 教會是天國在地上可見的代表，是基督的身體，被賦予傳揚福音的使命。\n'
             '    *   **對連結的影響：** 理解教會的本質和目的，積極參與教會生活和服事。\n'
             '\n'
