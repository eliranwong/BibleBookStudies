             '### VI. 猶大書的關鍵字分析\n'
             '\n'
             '以下關鍵字是理解猶大書上下文和整本聖經的基礎：\n'
             '\n'
             '1.  **真道 (Faith / The Faith)**\n'
             '    *   **猶大書：** 指客觀的基督教教義和信仰體系，是信徒必須「竭力爭辯」和捍衛的（猶大書3節）。\n'
             '    *   **聖經：** '
             '貫穿始終的主題，從亞伯拉罕因信稱義（創世記15:6）到新約福音的核心（加拉太書1:23），代表基督教信仰的純正教義。\n'
             '\n'
             '2.  **不敬虔的人 (Ungodly People)**\n'
             '    *   **猶大書：** '
             '指教會內部的假教師，他們缺乏對神的敬畏、順服和忠誠，是錯誤教義和敗壞行為的根源（猶大書4節）。\n'
             '    *   **聖經：** 指那些不認識神、不敬畏神、不遵守神律法的人（詩篇1:1），是神震怒的對象（羅馬書1:18）。\n'
             '\n'
             '3.  **審判 (Judgment)**\n'
             '    *   **猶大書：** '
             '貫穿全書的中心主題，透過歷史鑑戒（猶大書5-7節）和以諾預言（猶大書14-15節），強調神對不敬虔者的必然、嚴厲審判。\n'
             '    *   **聖經：** '
             '彰顯神的公義和聖潔，從洪水滅世（創世記6-9章）到末世最終審判（馬太福音25:31-46），提醒神是公義的報應者。\n'
             '\n'
             '4.  **竭力爭辯 (Contend Earnestly)**\n'
             '    *   **猶大書：** '
             '核心命令（猶大書3節），源自希臘運動競賽用語，強調為信仰奮力搏鬥、全力以赴的精神，主動捍衛純正福音。\n'
             '    *   **聖經：** '
             '舊約先知為真理爭辯（列王紀上18章），新約保羅勸勉為真道打美好的仗（提摩太前書6:12），強調信徒對真理的委身和對抗邪惡的責任。\n'
             '\n'
             '5.  **保守 (Keep / Preserve)**\n'
             '    *   **猶大書：** '
             '包含神的保守（猶大書1節、24節）和信徒的自我保守（猶大書21節）兩種面向，強調神的主動護理和信徒的主動責任。\n'
             '    *   **聖經：** '
             '神應許保守祂的子民（詩篇121篇，約翰福音17:11），同時也要求人保守神的誡命和真道（申命記4:6，提摩太前書6:20）。\n'
             '\n'
