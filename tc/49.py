             '神恩典的管家職分與奧秘的啟示**：保羅解釋他蒙神賜予「恩惠的管家職分」，藉此「用奧秘指示你們」。這奧秘在舊約時代是隱藏的，但如今「藉著聖靈啟示他的聖使徒和先知」，其核心就是「外邦人在基督耶穌裡，藉著福音，得以同為後嗣，同為一體，同蒙應許」。這再次強化了猶太人與外邦人合一的真理。\n'
             '3.  **弗3:7-9 '
             '保羅蒙召傳揚基督測不透的豐富**：保羅謙卑地承認自己「比眾聖徒中最小的還小」，卻蒙恩典得以「把基督那測不透的豐富傳給外邦人」，並「使眾人明白這奧秘是如何安排的」。\n'
             '4.  **弗3:10-11 '
             '藉著教會彰顯神的智慧**：這奧秘的啟示不僅是為了信徒，更是為了「藉著教會使天上執政的、掌權的，現在得知神百般的智慧」，這一切都是「照神從萬世以前，在我們主基督耶穌裡所定的旨意」。這將教會的意義提升到宇宙性的層面。\n'
             '5.  **弗3:12 '
             '信徒在基督裡坦然無懼**：因著這奧秘的啟示和基督的救贖，信徒「因信耶穌，就在他裡面放膽無懼，篤信不疑地來到神面前」。\n'
             '6.  **弗3:13 '
             '勸勉信徒不要因保羅的患難喪膽**：保羅鼓勵信徒不要因他被囚而灰心，因為他的患難反而是他們的「榮耀」，證明了福音的真實性和外邦人蒙恩的價值。\n'
             '    *   '
             '**思想進程**：這段經文將教會的奧秘與保羅的個人職事緊密相連，強調了神對外邦人的救贖是祂永恆計劃的一部分，並透過教會向宇宙萬有彰顯祂的智慧。這為接下來的禱告奠定了基礎，使信徒能更深地領受這些真理。\n'
             '\n'
             '**VI. 保羅為信徒的禱告與頌讚 (弗3:14-21)**\n'
             '保羅以另一個深情的禱告結束教義部分，祈求信徒能被聖靈充滿，在愛中生根建造，並經歷基督那測不透的愛。\n'
             '\n'
             '1.  **弗3:14-15 屈膝在父面前**：保羅再次以謙卑的姿態「屈膝在父面前」，強調禱告的莊重和對神的敬畏。\n'
             '2.  **弗3:16-19 祈求信徒在愛中生根建造，認識基督的愛**：保羅祈求：\n'
             '    *   神藉著聖靈使信徒「心裡的力量剛強起來」，使「基督因你們的信住在你們心裡」。\n'
             '    *   使他們的「愛心有根有基」，能與眾聖徒一同「明白基督的愛是何等長闊高深」，並知道這愛「是過於人所能測度的」。\n'
             '    *   最終目標是「便叫神一切所充滿的充滿了你們」。\n'
             '3.  **弗3:20-21 '
             '頌讚神能成就一切**：保羅以一個充滿信心的頌讚結束禱告，宣告神「能照著運行在我們心裡的大力，充充足足的成就一切，超過我們所求所想的」，並願「他在教會中，並在基督耶穌裡，得著榮耀，直到世世代代，永永遠遠。阿們！」\n'
             '    *   '
             '**思想進程**：這段禱告是教義部分的最高潮。它將前面所闡述的一切宏大真理（神的揀選、基督的救贖、聖靈的印記、教會的奧秘）歸結到信徒個人的生命體驗中，祈求他們不僅是頭腦上的理解，更是心靈上的經歷，被基督的愛所充滿。這為接下來的實踐性應用提供了內在的動力和能力。\n'
             '\n'
             '### **第二部分：實踐性應用——基督徒的生活與行為 (弗4:1-6:24)**\n'
             '\n'
             '在建立了堅實的教義基礎後，保羅從「是」（who you are in Christ）轉向「當行」（how you should '
             'live in Christ），呼籲信徒將所領受的真理活出來，彰顯在個人、教會和社會關係中。\n'
             '\n'
             '**I. 在基督裡合一的生活 (弗4:1-16)**\n'
             '保羅以一個強烈的勸勉開始實踐部分，呼籲信徒活出與蒙召恩典相稱的生命，特別是在教會的合一上。\n'
             '\n'
             '1.  **弗4:1-3 蒙召的行事為人當與恩相稱，竭力保守合一**：\n'
             '    *   '
             '保羅以「我為主被囚的勸你們」開頭，賦予了勸勉權威性和個人情感。他呼籲信徒「既然蒙召，行事為人就當與蒙召的恩相稱」，這是一個總綱。\n'
             '    *   '
             '具體表現為：「凡事謙虛、溫柔、忍耐，用愛心互相寬容，用和平彼此聯絡，竭力保守聖靈所賜合而為一的心。」這些都是維護合一的品格。\n'
             '2.  **弗4:4-6 '
             '合一的基礎：一主、一信、一洗、一神**：保羅列舉了「七個一」，強調教會合一的客觀基礎：一個身體、一位聖靈、一個指望、一位主、一種信心、一個洗禮、一位神。這表明合一不是人為的努力，而是神所賜的客觀事實。\n'
             '3.  **弗4:7-10 '
             '基督賜下恩賜，為要建立教會**：雖然有合一的基礎，但也有恩賜的多樣性。保羅指出「我們各人蒙恩，都是照基督所量給各人的恩賜」，並引用詩篇68篇，說明基督升天後賜下恩賜，目的是「充滿萬有」。\n'
             '4.  **弗4:11-12 '
             '職事恩賜的目的：成全聖徒，各盡其職**：保羅列舉了基督所賜的職事恩賜（使徒、先知、傳福音的、牧師和教師），其目的是「為要成全聖徒，各盡其職，建立基督的身體」。這強調了恩賜是為了教會的整體建造。\n'
             '5.  **弗4:13-14 '
             '達到真理的合一，不再作小孩子**：恩賜的最終目標是使教會「在真道上同歸於一，認識神的兒子，得以長大成人，滿有基督長成的身量」，不再「作小孩子，中了人的詭計和欺騙的法術」。\n'
             '6.  **弗4:15-16 '
             '在愛中說誠實話，身體在愛中增長**：教會的成長是透過「用愛心說誠實話」，「凡事長進，連於元首基督」，並且「全身都靠他聯絡得合式，百節各按各職，照著各體的功用彼此相助，便叫身體漸漸增長，在愛中建立自己」。\n'
             '    *   '
             '**思想進程**：這段經文從教義上的合一（2:11-22）過渡到實踐上的合一。保羅首先呼籲信徒在品格上努力維護合一，然後闡明合一的客觀基礎，接著說明神賜下恩賜是為了建立這合一的身體，最終目標是使教會在真理和愛中成熟成長。這為接下來的個人倫理轉變奠定了群體基礎。\n'
             '\n'
             '**II. 脫去舊人穿上新人 (弗4:17-5:2)**\n'
             '保羅從教會的合一轉向信徒個人的倫理轉變，呼籲他們脫離過去外邦人的生活方式，活出與新生命相稱的聖潔。\n'
             '\n'
             '1.  **弗4:17-19 '
             '不再像外邦人一樣行事為人**：保羅勸誡信徒「行事不要再像外邦人存虛妄的心行事」，因為他們「心地昏昧，與神所賜的生命隔絕」，「放縱私慾，貪行種種的污穢」。這再次強調了信徒身份的根本轉變。\n'
             '2.  **弗4:20-24 '
             '學習基督，脫去舊人，穿上新人**：與外邦人的生活方式形成對比，信徒「學了基督」，就當「脫去你們從前行為上的舊人」，「將你們的心志改換一新」，並且「穿上新人，這新人是照著神的形像造的，有真理的仁義和聖潔」。這是基督徒倫理的核心原則。\n'
             '3.  **弗4:25-32 具體的倫理誡命**：保羅列舉了一系列具體的行為準則，作為「穿上新人」的實際表現：\n'
             '    *   **弗4:25 說誠實話**：棄絕謊言，與鄰舍說實話。\n'
             '    *   **弗4:26-27 生氣卻不可犯罪**：不可含怒到日落，不給魔鬼留地步。\n'
             '    *   **弗4:28 勞力助人**：從前偷竊的，要勞力作正經事，有餘分給缺乏的人。\n'
             '    *   **弗4:29 說造就人的話**：不說污穢的言語，只說造就人的好話。\n'
             '    *   **弗4:30 不叫聖靈擔憂**：提醒信徒聖靈的印記。\n'
             '    *   **弗4:31-32 '
             '除去惡毒，彼此饒恕**：除去一切苦毒、惱恨、忿怒、嚷鬧、毀謗、惡毒，並要「以恩慈相待，存憐憫的心，彼此饒恕，正如神在基督裡饒恕了你們一樣」。\n'
             '4.  **弗5:1-2 '
             '效法神，憑愛心行事**：保羅總結這些倫理要求，呼籲信徒「效法神，好像蒙慈愛的兒女一樣」，並且「憑愛心行事，正如基督愛我們，為我們捨了自己」。這將基督的捨己之愛作為基督徒倫理的最高典範。\n'
             '    *   '
             '**思想進程**：這段經文是從群體合一到個人聖潔的過渡。保羅首先確立了「脫去舊人，穿上新人」的總原則，然後列舉了具體的行為改變，這些改變不僅關乎個人品德，也直接影響到人際關係，特別是教會內部的關係。最後，他將所有倫理行為的動機和標準歸結為「效法神」和「憑愛心行事」。\n'
             '\n'
             '**III. 在愛中行事為人與被聖靈充滿 (弗5:3-21)**\n'
             '保羅繼續深化倫理教導，特別強調聖潔、光明和被聖靈充滿的生活。\n'
             '\n'
             '1.  **弗5:3-7 '
             '遠離淫亂、污穢、貪婪、污言穢語**：保羅嚴厲譴責淫亂、污穢、貪婪和不合宜的言語，指出這些行為與聖徒的身份不符，且會招致神的忿怒。\n'
             '2.  **弗5:8-14 '
             '作光明的子女，不與黑暗有分**：信徒從前是「暗昧的」，如今在主裡是「光明的」，所以「行事為人就當像光明的子女」，結出「良善、公義、誠實」的果子。他們不應與「暗昧無益的事」有分，反倒要「責備行這事的人」，因為「凡事受了責備，就被光顯明出來」。\n'
             '3.  **弗5:15-17 '
             '謹慎行事，愛惜光陰，明白主的旨意**：保羅勸勉信徒要「謹慎行事」，「愛惜光陰」，因為「現今的世代邪惡」，並且要「明白主的旨意如何」，不要作糊塗人。\n'
             '4.  **弗5:18-21 '
             '不要醉酒，乃要被聖靈充滿，彼此順服**：這是這段倫理教導的高潮。保羅將「醉酒」與「被聖靈充滿」對比，指出被聖靈充滿的表現是：\n'
             '    *   「用詩章、頌詞、靈歌彼此對說，口唱心和的讚美主。」\n'
             '    *   「凡事要奉我們主耶穌基督的名常常感謝父神。」\n'
             '    *   「又當存敬畏基督的心，彼此順服。」\n'
             '    *   '
             '**思想進程**：這段經文從消極的「遠離罪惡」轉向積極的「活出光明」。保羅不僅列舉了不應有的行為，更強調了積極的生命態度——被聖靈充滿，這不僅帶來內在的喜樂和感恩，也引導出「彼此順服」的群體倫理，為接下來的家庭關係教導鋪墊。\n'
             '\n'
             '**IV. 家庭關係的教導 (弗5:22-6:9)**\n'
             '保羅將「彼此順服」的原則應用到具體的家庭和社會關係中，闡述了妻子、丈夫、兒女、父母、僕人、主人各自的責任。\n'
             '\n'
             '1.  **弗5:22-24 '
             '妻子順服丈夫**：妻子當「順服自己的丈夫，如同順服主」，因為「丈夫是妻子的頭，如同基督是教會的頭」。\n'
             '2.  **弗5:25-33 '
             '丈夫愛妻子**：丈夫當「愛你們的妻子，正如基督愛教會，為教會捨己」，並將基督與教會的關係作為婚姻的典範。他強調丈夫愛妻子如同愛自己的身子，並引用創世記的經文，指出婚姻的奧秘是指著「基督和教會」說的。\n'
             '3.  **弗6:1-3 兒女孝敬父母**：兒女當「在主裡聽從父母」，「孝敬父母」，這是「第一條帶應許的誡命」。\n'
             '4.  **弗6:4 父母教養兒女**：父親「不要惹兒女的氣」，而要「照著主的教訓和警戒養育他們」。\n'
             '5.  **弗6:5-8 '
             '僕人順服主人**：僕人要「懼怕戰兢，用誠實的心聽從你們肉身的主人，好像聽從基督一般」，「甘心事奉，好像服事主，不像服事人」，因為「必按所行的得主的賞賜」。\n'
             '6.  **弗6:9 '
             '主人善待僕人**：主人待僕人「也是一理，不要威嚇他們」，因為知道「他們和你們同有一位主在天上；他並不偏待人」。\n'
             '    *   '
             '**思想進程**：這段經文將前面「彼此順服」的原則具體化，應用於家庭和社會的權威結構中。保羅不是簡單地重複社會規範，而是將這些關係置於「在主裡」和「如同基督與教會」的框架下，提升了其屬靈意義，並強調了神對所有人的公平審判。這為最後的屬靈爭戰教導提供了日常生活的場景。\n'
             '\n'
             '**V. 穿戴神的全副軍裝 (弗6:10-20)**\n'
             '保羅以屬靈爭戰的呼召結束實踐部分，提醒信徒要意識到背後的屬靈實體，並裝備自己以抵擋仇敵。\n'
             '\n'
             '1.  **弗6:10-12 '
             '靠主剛強，穿戴全副軍裝，抵擋魔鬼的詭計**：保羅呼籲信徒「靠著主，倚賴他的大能大力作剛強的人」，並「穿戴神所賜的全副軍裝，就能抵擋魔鬼的詭計」。他明確指出爭戰的對象不是「屬血氣的」，而是「執政的、掌權的、管轄這幽暗世界的，以及天空屬靈氣的惡魔」。\n'
             '2.  **弗6:13-17 神的全副軍裝**：保羅詳細列舉了軍裝的各個部分，並賦予其屬靈意義：\n'
             '    *   「真理當作帶子束腰」（基礎）\n'
             '    *   「公義當作護心鏡穿上」（保護）\n'
             '    *   「平安的福音當作預備走路的鞋穿在腳上」（穩固與傳揚）\n'
             '    *   「信德當作藤牌」（抵擋攻擊）\n'
             '    *   「救恩的頭盔」（思想保護）\n'
             '    *   「聖靈的寶劍，就是神的道」（攻擊性武器）\n'
             '3.  **弗6:18-20 '
             '隨時多方禱告，為眾聖徒和保羅禱告**：除了穿戴軍裝，保羅強調「靠著聖靈，隨時多方禱告祈求」，並要「警醒不倦，為眾聖徒祈求」。他特別請求信徒為他禱告，使他能「放膽開口講明福音的奧秘」，即使身為「帶鎖鍊的使者」。\n'
             '    *   '
             '**思想進程**：這段經文將信徒的倫理生活提升到屬靈爭戰的層面。保羅提醒信徒，他們所面對的挑戰不僅是內在的罪性或外在的社會壓力，更有來自黑暗權勢的攻擊。因此，他們需要神所賜的屬靈裝備和持續的禱告，才能在惡劣的日子站立得穩，並繼續傳揚福音。這為書信的結尾提供了力量和盼望。\n'
             '\n'
             '**VI. 結語與祝福 (弗6:21-24)**\n'
             '保羅以典型的書信結語結束，傳達個人消息並送上祝福。\n'
             '\n'
             '1.  **弗6:21-22 '
             '推基古將報告保羅的近況**：保羅提及「所親愛、忠心的僕人推基古」，他將向以弗所信徒報告保羅的近況，並「安慰你們的心」。這顯示了保羅對收信人的關懷。\n'
             '2.  **弗6:23-24 '
             '願平安、仁愛、信心與恩惠歸與眾聖徒**：保羅以「平安、仁愛、信心」從父神和主耶穌基督歸與弟兄們作為祝福，並願「所有誠心愛我們主耶穌基督的人都蒙恩惠」。\n'
             '    *   '
             '**思想進程**：結語部分將書信從宏大的神學論述和嚴謹的倫理教導，帶回到溫馨的個人關懷和祝福。它再次強調了「平安」、「仁愛」、「信心」和「恩惠」這些貫穿全書的核心主題，為信徒的生命畫上一個充滿盼望的句點。\n'
