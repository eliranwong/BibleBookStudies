             '### 四、 核心主題與神學信息\n'
             '\n'
             '提多書所傳達的神學信息是全面而深刻的，它將純正的教義與敬虔的生活緊密結合。\n'
             '\n'
             '1.  **純正的道理 / 健全的信心 (Sound Doctrine / Sound Faith)**\n'
             '    *   '
             '**主題闡述**：這是提多書的核心，強調教義必須是「健康的」（ὑγιαίνω），能夠滋養信徒靈命。長老必須「**堅守所教導的純正道理**」（提多書 '
             '1:9），提多要傳講「**純正的道理**」（提多書 2:1），使信徒在「**真道上健全**」（提多書 1:13）。\n'
             '    *   '
             '**神學含義**：神是真理的源頭，祂的道是純全無疵的。純正的道理是教會健康發展的基石，也是抵禦異端、引導人過敬虔生活的基礎。\n'
             '    *   **舊約貢獻**：舊約律法（妥拉）被視為神純正的教導，如詩篇 19:7-9 '
             '讚美耶和華的律法「**全備，能甦醒人心**」。\n'
             '\n'
             '2.  **美好的行為 / 善事 (Good Works)**\n'
             '    *   **主題闡述**：提多書多次強調信徒因恩典得救後，必須「**熱心為善**」（提多書 '
             '2:14），「**留心作各樣善事**」（提多書 3:8, '
             '14）。這些善行是真信心的必然果子，是福音轉化生命的明證，而非得救的條件。\n'
             '    *   '
             '**神學含義**：善行是神在信徒生命中工作的證明，是神榮耀的彰顯，表明救恩不僅是地位上的改變，更是生命本質上的轉化。\n'
             '    *   **舊約貢獻**：舊約先知不斷呼籲以色列人行公義、好憐憫，如彌迦書 6:8 '
             '指出神向人所要的是「**行公義，好憐憫，存謙卑的心與你的神同行**」。\n'
             '\n'
             '3.  **恩典與救贖 (Grace and Salvation)**\n'
             '    *   **主題闡述**：提多書清晰闡明救恩完全出於神的恩典和憐憫，而非人的行為（提多書 2:11-12; '
             '3:5-7）。這恩典不僅赦罪，更「**教訓我們除去不敬虔的心和世俗的情慾，在今世自守、公義、敬虔度日**」（提多書 '
             '2:12）。\n'
             '    *   '
             '**神學含義**：神是慈愛、憐憫和公義的。祂的救贖計劃是普世性的，透過耶穌基督的犧牲實現，旨在恢復人被造時的形象，使人能反映祂的良善。\n'
             '    *   **舊約貢獻**：舊約中神的救贖行動（如出埃及）彰顯了祂的恩典和憐憫，如出埃及記 34:6-7 '
             '啟示神是「**有憐憫有恩典的神**」。\n'
             '\n'
             '4.  **教會領袖的品格與職責**\n'
             '    *   **主題闡述**：提多書 1:5-9 '
