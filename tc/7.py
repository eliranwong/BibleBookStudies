             '#### B. 利未人的妾與便雅憫的內戰 (士師記 19:1-21:25)\n'
             '這是《士師記》中最黑暗、最暴力的一段敘事，展示了士師時代末期以色列社會道德的徹底崩潰，最終導致一場幾乎滅絕便雅憫支派的內戰。\n'
             '\n'
             '1.  **利未人與他的妾 (19:1-10)**\n'
             '    *   a. **利未人從伯利恆接回妾 '
             '(19:1-3)**：那時以色列中沒有王。有一個利未人住在以法蓮山地，他娶了一個猶大伯利恆的女子作妾。他的妾因與他生氣而回到父家。利未人去伯利恆，用好話勸她回來。\n'
             '    *   b. **利未人在岳父家停留 '
             '(19:4-9)**：利未人在岳父家停留了四天，岳父熱情款待。第五天，利未人想離開，岳父又挽留他，直到日頭偏西才動身。\n'
             '    *   c. **利未人與妾離開 (19:10)**：利未人帶著妾和僕人，以及兩匹驢，離開伯利恆，往以法蓮山地去。\n'
             '\n'
             '2.  **基比亞的暴行 (19:11-30)**\n'
             '    *   a. **利未人選擇在基比亞過夜 '
             '(19:11-15)**：日落時分，他們來到便雅憫的基比亞。利未人決定不在耶布斯（耶路撒冷）過夜，而選擇在基比亞過夜。他們坐在城裡的廣場上，卻沒有人接待他們。\n'
             '    *   b. **老人接待利未人 '
             '(19:16-21)**：一個從以法蓮山地來，住在基比亞的利未人老者，看見他們，就邀請他們到他家過夜。老人為他們預備食物和水。\n'
             '    *   c. **基比亞匪徒圍攻房屋 '
             '(19:22-24)**：他們正歡宴時，城裡的匪徒（「彼列之子」）圍住房屋，敲門，要求老人將那男子交出來，讓他們與他交合。老人拒絕，並提出將自己的女兒和利未人的妾交給他們。\n'
             '    *   d. **利未人將妾推出門外 '
             '(19:25-26)**：匪徒不聽老人的話。利未人就將自己的妾推出門外。匪徒整夜凌辱她，直到天亮才放她走。\n'
             '    *   e. **妾被輪姦致死 '
             '(19:27-28)**：清晨，利未人起來開門，發現他的妾倒在門口，手扶著門檻。他叫她，她沒有回應。利未人將她放在驢上，帶回家。\n'
             '    *   f. **利未人肢解妾的屍體並分送各支派 '
