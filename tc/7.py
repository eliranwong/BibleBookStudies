             '#### A. 米迦的偶像與但支派的遷移 (士師記 17:1-18:31)\n'
             '一個以法蓮人米迦設立家庭神堂，並聘用利未人作祭司，隨後但支派在尋找地業的過程中，奪走了米迦的偶像和祭司，並在他們的新定居地設立了偶像崇拜。\n'
             '\n'
             '1.  **米迦的偶像與利未人祭司 (17:1-13)**\n'
             '    *   a. **米迦偷錢與母親的咒詛和祝福 '
             '(17:1-2)**：以法蓮山地有一個人名叫米迦。他曾偷了他母親一千一百舍客勒銀子，他母親因此咒詛。米迦承認是他偷的，母親就為他祝福。\n'
             '    *   b. **母親為耶和華鑄造偶像 '
             '(17:3-4)**：母親將銀子獻給耶和華，為兒子鑄造一個雕刻的偶像和一個鑄造的偶像。\n'
             '    *   c. **米迦設立神堂與以弗得、家神 '
             '(17:5)**：米迦設立了一個神堂，製造了以弗得和家神，又派他的一個兒子作祭司。\n'
             '    *   d. **利未人青年來到米迦家 '
             '(17:6-8)**：那時以色列中沒有王，各人任意而行。有一個利未人青年從猶大伯利恆出來，尋找住處，來到米迦家。\n'
             '    *   e. **米迦聘用利未人作祭司 '
             '(17:9-13)**：米迦問利未人從何而來，利未人說明來意。米迦邀請他留下作他的父和祭司，並應許給他工資和衣食。利未人同意，米迦就說「現在我知道耶和華必賜福與我，因為我有利未人作祭司」。\n'
             '\n'
             '2.  **但支派尋找地業 (18:1-10)**\n'
             '    *   a. **但支派的困境與派出探子 '
             '(18:1-2)**：那時但支派還沒有得著地業居住。他們從宗族中派出五個壯士去偵察地業。探子來到以法蓮山地，到了米迦家。\n'
             '    *   b. **探子遇見米迦的利未人 '
             '(18:3-6)**：探子認出利未人的口音，問他為何在此。利未人說明情況。探子請利未人求問神，他們所行的道路是否亨通。利未人說「你們可以平安而去，你們所行的道路在耶和華面前」。\n'
             '    *   c. **探子偵察拉億 '
             '(18:7-10)**：探子來到拉億，發現那裡的人安居無慮，與西頓人一樣，沒有人欺壓他們，也沒有人管轄他們，且離西頓很遠。探子回去報告，鼓勵但支派去攻取那地，因為那地甚是美好。\n'
             '\n'
             '3.  **但支派奪取米迦的偶像與祭司 (18:11-26)**\n'
             '    *   a. **但支派的六百壯士出發 '
             '(18:11-13)**：但支派從瑣拉和以實陶派出六百名壯士，帶上兵器，上到以法蓮山地，在基列耶琳安營。\n'
             '    *   b. **但支派奪取米迦的偶像和祭司 '
             '(18:14-20)**：五個探子告訴六百壯士米迦家中有以弗得、家神、雕刻的偶像和鑄造的偶像。他們就進米迦家，奪取了這些偶像。利未人問他們做什麼，但支派的人勸他不要說話，並邀請他作他們支派的祭司，說作一家人的祭司不如作一個支派的祭司。利未人很高興，就帶著以弗得、家神和雕刻的偶像，跟隨但支派的人。\n'
             '    *   c. **米迦追趕但支派與被恐嚇 '
             '(18:21-26)**：但支派的人帶著婦女、孩子和牲畜走在前面。米迦召集鄰居追趕他們，質問他們為何搶走他的神和祭司。但支派的人恐嚇米迦，米迦見他們人多勢眾，只好作罷，回家去了。\n'
             '\n'
             '4.  **但支派攻取拉億並設立偶像 (18:27-31)**\n'
             '    *   a. **但支派攻取拉億並改名為但 '
             '(18:27-29)**：但支派的人帶著米迦所造的偶像和利未人，來到拉億。他們攻打那安居無慮的居民，用刀殺盡他們，放火燒城。他們重建城，並改名為但，以他們祖先的名字命名。\n'
             '    *   b. **但支派設立米迦的雕刻偶像 '
             '(18:30-31)**：但支派的人為自己設立了米迦所造的雕刻偶像。約拿單（摩西的孫子革舜的兒子）和他的子孫作但支派的祭司，直到那地被擄。米迦的偶像在但支派中設立，直到神殿在示羅的日子。\n'
