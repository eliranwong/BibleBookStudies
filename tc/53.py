             '    *   **「主的日子」的基督化：** 將舊約「耶和華的日子」（賽 13:9, 珥 '
             '2:31）與基督的再來連結，表明耶穌是舊約預言的中心。\n'
             '    *   **「不法之人」與舊約敵對者的預表：** 舊約但以理書中「小角」（但 7:25, '
