             '    *   **傳統觀點** '
             '認為作者是耶穌基督的使徒西門彼得。書信開篇即自稱：「耶穌基督的僕人和使徒西門彼得，寫信給那因我們的神和救主耶穌基督之義，與我們同得一樣寶貴信心的人。」（彼後1:1）。作者提及自己是「在聖山上與主同在」的見證人（彼後1:18），並明確指出這是繼《彼得前書》之後的「第二封信」（彼後3:1-2）。書中「我脫離這帳棚的時候快到了」（彼後1:13-14）的語氣，帶有使徒即將殉道前的「遺囑式」色彩。\n'
             '    *   **學術爭議** '
             '則基於希臘文風格差異、神學觀念發展（如對「知識」的強調可能反映早期諾斯底主義語境）、對保羅書信的引用（稱其為「一切經書」，彼後3:15-16，暗示較晚期的正典觀念）以及早期教父對其正典地位的爭議。儘管如此，保守派學者仍堅信彼得是作者，或至少是其門徒忠實記錄了彼得的教導。\n'
