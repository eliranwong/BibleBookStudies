             '### 六、 關鍵字深度解析\n'
             '\n'
             '以下是《彼得後書》中幾個重要的關鍵字，它們在書卷和整本聖經中具有深遠意義：\n'
             '\n'
             '1.  **知識 (Knowledge / '
             'ἐπίγνωσις)**：指深刻的、轉化的、關係性的認識，是關於神和主耶穌基督的。在彼後1:2, 1:3, 1:8, 2:20, '
             '3:18中出現。它區別於膚淺的γνῶσις，強調知識的深度和其帶來的生命轉化。在舊約是「認識耶和華」（耶利米書9:23-24），在新約是「認識你—獨一的真神，並且認識你所差來的耶穌基督，這就是永生」（約翰福音17:3）。\n'
             '2.  **敬虔 (Godliness / εὐσέβεια)**：指對神的敬畏和順服，並在生活中活出這種敬畏。在彼後1:3, '
             '1:6, '
             '3:11中出現。它是真知識的實際體現，是基督徒生命的核心特質。舊約強調「敬畏耶和華」（箴言1:7），新約則指出「敬虔，凡事都有益處」（提摩太前書4:7-8）。\n'
             '3.  **應許 (Promises / ἐπαγγελία)**：指神所作的、必然實現的承諾，是信徒盼望的基石。在彼後1:4, '
             '3:4, 3:9, '
             '3:13中出現。神對亞伯拉罕的應許（創世記12:1-3）是舊約應許的基礎，新約則強調神的應許在基督裡都是「是」的（哥林多後書1:20）。\n'
             '4.  **假教師 (False Teachers / '
             'ψευδοδιδάσκαλοι)**：指那些傳播錯誤教義、道德敗壞並引誘信徒偏離真道的人。在彼後2:1中明確指出。他們是聖經中反覆警告的對象（申命記13:1-5；馬太福音7:15），是信仰純潔性的主要威脅。\n'
             '5.  **主的日子 (Day of the Lord / ἡμέρα '
             'Κυρίου)**：指神最終施行審判和救贖的日子，與基督的第二次降臨緊密相關。在彼後3:10中出現。這是舊約先知書（約珥書2:1-2）和新約（帖撒羅尼迦前書5:2）的重要末世論概念。\n'
             '6.  **聖潔 (Holiness / '
             'ἁγιότης)**：指道德上的純潔和分別為聖，是神本性的核心屬性，也是祂對子民的根本要求。在彼後3:11中出現。舊約要求以色列人「要聖潔，因為我是聖潔的」（利未記11:44-45），新約則強調「非聖潔沒有人能見主」（希伯來書12:14）。\n'
             '7.  **沉淪 (Destruction / '
             'ἀπώλεια)**：指與神隔絕、永恆滅亡的狀態，是罪惡和悖逆神之最終後果。在彼後2:1, 2:20, 3:7, '
             '3:16中出現。這是神公義的彰顯，與「永生」形成鮮明對比（馬太福音7:13）。\n'
