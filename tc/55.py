             '**三、 關鍵詞彙與重複主題**\n'
             '\n'
             '提摩太後書透過幾個重複出現的關鍵詞彙和主題，傳達了其核心信息：\n'
             '\n'
             '1.  **福音 (Gospel, εὐαγγέλιον, *euangelion*)：**\n'
             '    *   **重要性：** 福音是保羅使徒職分的根基、動力和核心信息。他為福音受苦（提後1:8, '
             '2:9），並將福音的核心定義為「耶穌基督乃是大衛的後裔，他從死裡復活」（提後2:8）。即使保羅被捆綁，「神的道卻不被捆綁」（提後2:9），顯示福音超越一切限制的能力。\n'
             '    *   **聖經整體：** '
             '福音是聖經的中心信息，從舊約的預言（創3:15，賽53章）到新約的實現（太4:23，羅1:16-17），它宣告了神藉著耶穌基督的生、死、復活，為人類預備了救恩，帶來「生命的應許」（提後1:1）。\n'
             '\n'
             '2.  **真理/真道 (Truth/Sound Doctrine, ἀλήθεια, *alētheia* / '
             'ὑγιαινόντων λόγων, *hygiainontōn logōn*)：**\n'
             '    *   **重要性：** '
             '在異端邪說盛行的背景下，保羅強調提摩太要堅守「純正話語的規模」（提後1:13）和「善道」（提後1:14），並「按著正意分解真理的道」（提後2:15）。他警告提摩太遠避「世俗的虛談」（提後2:16）和「愚拙無學問的辯論」（提後2:23），因為這些會偏離真道，敗壞信心（提後2:18）。\n'
             '    *   **聖經整體：** '
             '真理是神本性的真實和啟示的純正。耶穌說：「我就是道路、真理、生命」（約14:6）。神的道就是真理（約17:17），聖靈是真理的靈。教會是「真理的柱石和根基」（提前3:15），有責任維護和傳揚真理。\n'
             '\n'
             '3.  **苦難/受苦 (Suffering, πάθημα, *pathēma* / κακοπάθεια, '
             '*kakopatheia*)：**\n'
             '    *   **重要性：** '
             '苦難是保羅生命和事奉的顯著特徵，也是他對提摩太的勸勉核心。他鼓勵提摩太「為福音與我同受苦難」（提後1:8），「作基督耶穌的精兵」（提後2:3），並指出「凡立志在基督耶穌裡敬虔度日的，也都要受逼迫」（提後3:12）。儘管受苦，保羅卻不以為恥，深信神能保全他所交託的（提後1:12）。\n'
             '    *   **聖經整體：** '
             '苦難是人類墮落後的普遍經驗，但對信徒而言，它具有救贖意義。基督是「受苦的僕人」（賽53章），信徒的苦難是參與基督的苦難（腓3:10），最終引向榮耀（羅8:17）。\n'
             '\n'
             '4.  **聖經 (Scripture, γραφή, *graphē*)：**\n'
             '    *   **重要性：** '
             '保羅將聖經確立為抵禦異端、堅守真理的最終權威。他宣告：「聖經都是神所默示的（θεόπνευστος, '
             '*theopneustos*，意為「神所吹氣的」），於教訓、督責、使人歸正、教導人學義都是有益的，叫屬神的人得以完全，預備行各樣的善事」（提後3:16-17）。這確立了聖經的超自然起源、全面功用和完備性。\n'
             '    *   **聖經整體：** '
             '聖經是神對人類的啟示，是神的話語。舊約中神的話語是律法和生命（申6:6-9，詩119:105），新約中耶穌和使徒們都高度肯定舊約聖經的權威（太5:17-18，彼後1:20-21）。聖經是信徒信仰和道德的最高準則。\n'
             '\n'
             '5.  **忠心 (Faithfulness, πιστός, *pistos*)：**\n'
             '    *   **重要性：** '
             '忠心是保羅對提摩太的核心要求，也是他自己生命的寫照。他囑咐提摩太將真理「交託那忠心能教導別人的人」（提後2:2）。保羅以自己「那美好的仗我已經打過了，當跑的路我已經跑盡了，所信的道我已經守住了」（提後4:7）來總結一生，這是對他忠心事奉的最高肯定。\n'
             '    *   **聖經整體：** '
             '忠心是神性情的重要屬性（林前1:9），也是神對其子民的核心要求（太25:21）。忠心不僅體現在大而顯著的事奉上，也體現在日常生活的點滴中（路16:10）。\n'
             '\n'
             '6.  **傳承/交託 (Transmission/Entrustment, παραθήκη, '
             '*parathēkē*)：**\n'
             '    *   **重要性：** '
             '保羅將自己從主所領受的福音真理和事奉職責，鄭重地「交託」給提摩太（提後1:14），這個詞強調其珍貴性和保管的責任。他明確指示提摩太將這些「交託那忠心能教導別人的人」（提後2:2），形成一個多代的傳承鏈條，確保福音的純正性和延續性。\n'
             '    *   **聖經整體：** '
             '傳承是神救贖歷史中一個持續的主題，確保神的話語和旨意能夠代代相傳（申6:6-7）。耶穌將大使命託付給門徒（太28:19-20），使徒們也將福音傳遞給後繼者。\n'
             '\n'
             '**四、 神學信息與規範性語境**\n'
             '\n'
             '提摩太後書所傳達的神學信息是多層次且相互關聯的，它在聖經整體敘事中扮演著承上啟下的關鍵角色。\n'
             '\n'
             '1.  **福音的本質、能力與其所帶來的生命應許：**\n'
             '    *   **神學信息：** '
             '福音是神永恆旨意和恩典的彰顯，其核心是基督戰勝死亡，帶來不能壞的生命。它不是基於人的行為，而是神的大能。\n'
             '    *   **規範性語境：** '
             '這信息是舊約彌賽亞應許（創3:15，賽53章）在新約基督耶穌身上（提後2:8）的實現。它與保羅在羅馬書1:16-17中對福音大能的宣告一脈相承，並為使徒行傳中福音廣傳提供了神學基礎。\n'
             '\n'
             '2.  **聖經的權威、默示與其在信徒生命中的完備性：**\n'
             '    *   **神學信息：** '
             '聖經是神所默示的，具有神的權威和無誤性。它在教訓、督責、歸正、學義方面是完備的，足以使屬神的人得以完全，預備行各樣的善事。\n'
             '    *   **規範性語境：** '
             '這信息肯定了舊約聖經的權威（提後3:15），並將其延續到新約時代。它與舊約中對神話語的尊崇（詩119:105）和新約中耶穌對聖經的確認（太5:17-18）相符，為教會提供了信仰和生活的最終準則。\n'
             '\n'
             '3.  **堅守純正教義的重要性與福音的代代傳承：**\n'
             '    *   **神學信息：** '
             '在異端猖獗的末世，信徒必須堅守純正的教義，並將其忠實地傳遞給下一代，以確保福音的純正性和延續性。\n'
             '    *   **規範性語境：** '
             '這信息是舊約中神命令以色列人將律法教導子孫（申6:6-7）的延續，也是耶穌大使命（太28:19-20）的具體實踐。它與提摩太前書和提多書共同構成了教牧書信的核心，為教會的門徒訓練和領袖培養提供了藍圖。\n'
             '\n'
             '4.  **忠心事奉與為福音受苦的必然性及最終的獎賞：**\n'
             '    *   **神學信息：** '
             '忠心事奉基督必然伴隨著苦難和逼迫，但這苦難是參與基督的受苦，最終將得著神所賜的「公義的冠冕」。\n'
             '    *   **規範性語境：** '
             '這信息與舊約中受苦的義人（約伯）和先知（耶利米）的經歷相呼應，並在新約中以基督的受苦（賽53章）為終極榜樣。它與羅馬書8:17和彼得前書4:12-13中關於與基督一同受苦的教導一致，並指向啟示錄中對忠心至死者的應許（啟2:10）。\n'
             '\n'
             '5.  **神的信實與主權，以及對末世的警醒：**\n'
             '    *   **神學信息：** '
             '即使人失信，神仍是信實的，他不能背乎自己。神掌管一切，他的揀選和保守是穩固的。同時，信徒應警醒面對末世人心的敗壞和邪惡的加劇。\n'
             '    *   **規範性語境：** '
             '這信息反映了舊約中神對其約的信實（申7:9，詩89:33-34）和主權（但4:35）。它與新約中耶穌對末世的預言（太24章）和啟示錄的末世異象相呼應，提醒信徒要活出聖潔，警醒等候主的再來。\n'
