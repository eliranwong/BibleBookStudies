             '    *   '
             '新約將聖殿的意義從物質建築轉向**耶穌基督本身**和**祂的教會**。耶穌說：「你們拆毀這殿，我三日內要再建立起來。」（約翰福音 '
             '2:19），祂指的是自己的身體。\n'
