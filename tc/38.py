             '\n'
             '1.  **上帝的信實與恢復：** 即使在百姓悖逆和被懲罰之後，上帝仍信守祂的約，應許恢復耶路撒冷和聖殿的榮耀（撒迦利亞書 '
             '1:14-16, 8:3）。這揭示了上帝不變的信實和憐憫。\n'
             '2.  **彌賽亞的中心地位與雙重職分：** '
             '撒迦利亞書將彌賽亞置於上帝救贖計劃的中心，並獨特地預示了祂將同時具備**君王和祭司**的職分（「苗裔」，צֶמַח, '
             '*tsemakh*，撒迦利亞書 3:8, 6:12-13）。書中預言了彌賽亞的謙卑降臨（撒迦利亞書 '
