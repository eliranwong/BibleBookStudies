             '章）具有濃厚的啟示文學色彩，這在古代近東並非獨特。美索不達米亞的夢兆文學和埃及的預言文本都有類似的異象。理解這種體裁，能幫助我們認識到這些異象充滿象徵意義，如四輛戰車（撒迦利亞書 '
             '6:1-8）象徵上帝的宇宙力量，強化了耶和華作為宇宙主宰的概念。\n'
             '*   **「苗裔」的彌賽亞稱謂：** 「苗裔」（צֶמַח, '
             '*tsemakh*）這個稱謂在古代近東的王室意識形態中也有其根源，君王常被描繪為從神聖根源中「發芽」或「生長」。撒迦利亞書將其應用於大祭司約書亞和未來的君王兼祭司（撒迦利亞書 '
             '3:8；6:12），為百姓提供了一個超越當下政治困境的神聖君王形象。\n'
             '*   **聖殿的宇宙性意義：** '
             '在古代近東，聖殿不僅是敬拜場所，更是宇宙秩序的中心。撒迦利亞書對聖殿重建的強調，以及耶和華將作耶路撒冷「四圍的火城，並要作其中的榮耀」（撒迦利亞書 '
             '2:5）的應許，將聖殿視為上帝同在的具體化，是上帝在地上建立其國度的核心。\n'
             '\n'
             '### 三、 撒迦利亞書的結構與思維進程\n'
             '\n'
             '撒迦利亞書的思維進程是一部精心編排的預言，從當下的歷史困境出發，逐步揭示上帝對以色列和列國的宏偉計劃。\n'
             '\n'
             '**A. 第一部分：異象與重建的呼召（撒迦利亞書 1-8 章）**\n'
             '這部分以**呼籲悔改**（撒迦利亞書 '
             '1:1-6）開篇，提醒百姓從列祖的悖逆中學習教訓。隨後，透過**八個夜間異象**（撒迦利亞書 '
             '1:7-6:8），逐步建立起上帝對耶路撒冷和聖殿重建的堅定旨意：\n'
             '1.  **騎馬的人與巡視全地（1:7-17）：** 確立上帝對世界的**主權與關懷**，應許安慰耶路撒冷。\n'
             '2.  **四角與四匠（1:18-21）：** 解決**阻礙重建的外部威脅**，預言上帝將擊敗敵對勢力。\n'
             '3.  **量度的準繩（2:1-13）：** 描繪耶路撒冷**未來的榮耀與擴張**，上帝將作其火城和榮耀。\n'
             '4.  **潔淨大祭司約書亞（3:1-10）：** 聚焦**內部屬靈潔淨**，預言「苗裔」將除去罪孽。\n'
             '5.  **金燈臺與兩棵橄欖樹（4:1-14）：** '
             '鼓勵**重建的具體動力**，強調「不是倚靠勢力，不是倚靠才能，乃是倚靠我的靈方能成事」（撒迦利亞書 4:6）。\n'
             '6.  **飛行的書卷（5:1-4）：** 引入**內部道德潔淨**的必要性，清除偷竊和假誓。\n'
             '7.  **量器中的婦人（5:5-11）：** 進一步強調**罪惡的徹底清除**，將罪惡運到示拿地。\n'
             '8.  **四輛戰車（6:1-8）：** 總結上帝對世界的**普世主權與審判**。\n'
             '異象之後，是**象徵性加冕**（撒迦利亞書 '
             '6:9-15），將君王和祭司的職分結合在彌賽亞「苗裔」身上。最後，透過對**禁食真義**的闡述，將神聖的應許與百姓的**道德責任**聯繫起來，呼籲公義，並重申耶路撒冷未來的榮耀與列國的歸順（撒迦利亞書 '
             '7-8 章）。\n'
             '\n'
             '**B. 第二部分：審判與救贖的預言（撒迦利亞書 9-14 章）**\n'
             '這兩段「默示」更具末世啟示文學的特徵，將焦點從重建聖殿轉向彌賽亞的降臨、列國的審判和上帝國度的最終實現。\n'
             '1.  **第一個默示：彌賽亞的降臨與牧者的命運（撒迦利亞書 9-11 章）：** '
             '預言上帝對列國的審判，然後戲劇性地引入**謙卑的彌賽亞君王**將騎驢而來（撒迦利亞書 '
             '9:9-10）。接著描繪以色列的得勝與上帝的賜福，但也預言了彌賽亞（好牧人）的**被棄絕**，被估價「三十塊錢」，導致約的破裂和無用牧者的興起（撒迦利亞書 '
             '11:4-17）。\n'
             '2.  **第二個默示：耶路撒冷的最終勝利與潔淨（撒迦利亞書 12-14 章）：** '
             '這是預言的**末世高潮**。預言耶路撒冷將得勝，但隨後將為「他們所扎的」彌賽亞**哀哭悔改**（撒迦利亞書 '
             '12:10）。上帝將開一個「泉源，洗除罪惡與污穢」（撒迦利亞書 '
             '13:1），並煉淨餘民。最終，在「耶和華的日子」，上帝將親自降臨，擊敗敵人，建立祂的**普世性、永恆且完全聖潔的國度**，其中「耶和華必作全地的王」（撒迦利亞書 '
             '14:9）。\n'
             '\n'
             '### 四、 撒迦利亞書的關鍵主題與神學信息\n'
             '\n'
