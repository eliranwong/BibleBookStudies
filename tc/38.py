             '4:6：「不是倚靠勢力，不是倚靠才能，乃是倚靠我的靈方能成事。」）。上帝的同在是耶路撒冷復興的核心（撒迦利亞書 2:5, '
             '8:3）。\n'
             '4.  **道德更新與公義生活：** '
             '上帝呼籲百姓從過去的罪惡中悔改，過公義和憐憫的生活，強調內在的道德更新比外在的宗教儀式更重要（撒迦利亞書 1:3-6, '
             '7:9-10）。\n'
