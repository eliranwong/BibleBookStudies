             '### 五、 重複主題與主要神學信息\n'
             '\n'
             '約伯記透過幾個重複出現的文學主題，巧妙地編織其複雜的信息，並傳達了幾個核心的神學和哲學信息。\n'
             '\n'
             '**A. 重複主題 (Recurring Motifs)**\n'
             '\n'
             '1.  **義人的苦難 (The Suffering of the Righteous)：**\n'
             '    *   **闡述：** '
             '約伯這位「完全正直，敬畏神，遠離惡事」（約伯記1:1）的義人，無故遭受極端痛苦。這直接挑戰了「義人必蒙福」的傳統觀念。希伯來語「義人」（צַדִּיק, '
             '*tzaddiq*）和「正直」（תָּם, *tam*）的定義，強化了約伯的無辜，使苦難更難解釋。\n'
             '    *   **神學意涵：** 苦難不總是罪的直接懲罰，它可能有更深層次、人類無法理解的目的。\n'
             '    *   **新約貢獻：** '
             '耶穌基督是完全無罪的義人，卻承受了人類最大的苦難（彼得前書2:21-23）。約伯的經歷預示了耶穌的受難。\n'
             '    *   **與神關係影響：** 提醒我們與神的關係不應建立在功利主義基礎上，鼓勵我們在不解中信靠神。\n'
             '\n'
             '2.  **朋友的傳統智慧與其不足 (The Conventional Wisdom of the Friends and '
             'its Inadequacy)：**\n'
             '    *   **闡述：** '
             '約伯的朋友們堅持「神是公義的，苦難必然是罪的結果」的報應神學，試圖說服約伯認罪。他們的「智慧」（חָכְמָה, '
             '*ḥokhmah*）是基於對神律法和世界運作的有限理解。約伯諷刺他們為「叫人愁煩的安慰者」（約伯記16:2）。\n'
             '    *   **神學意涵：** 揭示人類智慧的局限性，尤其是在面對神奧秘的旨意時。警告我們不要用簡單公式解釋複雜生命現象。\n'
             '    *   **新約貢獻：** '
             '新約也強調人類智慧的局限性（哥林多前書1:19-20），將真正的智慧與對神的認識和信靠聯繫起來。\n'
             '    *   **與神關係影響：** 教導我們謙卑，不應自以為能完全理解神的作為，也不應輕易論斷他人的苦難。\n'
             '\n'
             '3.  **神從旋風中說話 (God Speaking from the Whirlwind)：**\n'
             '    *   **闡述：** 神從「旋風」（סְעָרָה, '
             '*sĕʿārāh*）中向約伯發言（約伯記38:1），以一系列關於宇宙創造和自然界奧秘的反問句，挑戰約伯的知識和掌控能力。神沒有解釋苦難原因，而是彰顯其無限智慧和主權。\n'
             '    *   **神學意涵：** '
             '神的旨意和作為是**不可測度**的（inscrutable）。人類無法憑藉有限理性完全理解神的計劃。\n'
             '    *   **新約貢獻：** '
             '羅馬書11:33-36頌讚神的智慧和判斷何其難測。歌羅西書1:16-17將創造的智慧和權能歸於基督。\n'
             '    *   **與神關係影響：** 教導我們謙卑和敬畏，承認自己在神面前的渺小和無知，在不理解中信靠神的全能和智慧。\n'
             '\n'
             "4.  **約伯的哀歌與堅持無辜 (Job's Lament and Insistence on his "
             'Innocence)：**\n'
             '    *   **闡述：** 約伯不斷發出痛苦的「哀歌」（קִינָה, '
             '*qinah*），表達絕望、對神的質疑，並堅決捍衛自己的「無辜」（נָקִי, *naqi* 或 צֶדֶק, '
             '*tzedeq*）。\n'
             '    *   **神學意涵：** '
             '肯定人類在苦難中表達真實情感的權利，神允許祂的子民向祂傾訴痛苦和困惑。真誠的信仰即使在質疑中，也比虛假的教條更有價值。\n'
             '    *   **新約貢獻：** '
             '耶穌在十字架上的呼喊（馬太福音27:46）和羅馬書8:26中聖靈用說不出來的嘆息替我們禱告，都與約伯的哀歌產生共鳴。\n'
             '    *   **與神關係影響：** 鼓勵我們在與神的關係中保持真實和誠懇，不必隱藏痛苦，神能承受我們的質疑。\n'
