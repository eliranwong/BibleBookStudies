             '    *   **《詭詐》 (עָקֹב, *akov*)：** 耶利米書 17:9 '
             '說「人心比萬物都詭詐，壞到極處，誰能識透呢？」這個詞強調了人心的欺騙性和不可測性，揭示了人類罪性的深度。\n'
             '\n'
             '*   **神學意涵：** '
             '這個母題揭示了人類**罪惡的深度**，罪不僅是行為上的，更是內心深處的敗壞。它也預示了上帝救贖的**徹底性**，真正的救贖必須從改變人心開始。這為新約的「重生」概念奠定了基礎。\n'
             '\n'
             '*   **新約的貢獻：** '
             '耶穌在登山寶訓中強調了內心的重要性，指出罪惡的根源在於心。新約也明確指出，唯有聖靈才能賜予人新心，使人能順服上帝。\n'
             '    *   **馬太福音 5:28**：「只是我告訴你們，凡看見婦女就動淫念的，這人心中已經與她犯姦淫了。」\n'
             '    *   **希伯來書 8:10**（引用耶利米書 '
             '31:33）：「主又說：那些日子以後，我與以色列家所立的約乃是這樣：我要將我的律法放在他們裡面，寫在他們心上；我要作他們的上帝，他們要作我的子民。」\n'
             '    新約將「新心」的應許與聖靈的工作和基督的救贖聯繫起來，強調了內在生命的更新。\n'
             '\n'
             '*   **對我們與上帝關係的影響：** '
             '這個母題呼籲我們省察自己的內心，認識到罪惡的根源。它鼓勵我們向上帝祈求一顆「新心」，讓聖靈來更新我們的思想和意志。它也給予我們盼望，因為上帝有能力改變最剛硬的心，使我們能真誠地愛祂、順服祂。\n'
             '\n'
             '#### **6. 被擄與歸回 (Exile and Return)**\n'
             '\n'
             '*   **詳細闡述：** 耶利米書不僅預言了猶大將被擄到巴比倫（耶利米書 '
             '25:11），也預言了七十年後將從被擄之地歸回（耶利米書 '
             '29:10）。這個母題是上帝對其選民審判與救贖計劃的核心部分，強調了上帝的信實和祂對選民的最終眷顧。\n'
             '\n'
             '*   **希伯來文研究的幫助：**\n'
             '    *   **《被擄》 (גָּלָה, *galah*)：** '
             '這個詞在舊約中指被迫離開家園，流亡異鄉。它不僅是地理上的遷移，更是民族身份和信仰上的巨大危機。\n'
             '    *   **《歸回》 (שׁוּב, *shuv*)：** '
             '這個詞既指地理上的回歸，也指靈性上的「回轉」或「悔改」。耶利米書將兩者聯繫起來，強調真正的歸回必須伴隨著內心的悔改。\n'
             '\n'
             '*   **神學意涵：** '
             '這個母題揭示了上帝的**公義**（因罪而審判）和**信實**（對其應許的堅守）。被擄是上帝對背約的懲罰，但歸回則彰顯了祂對選民的**不離不棄**和**救贖計劃**的最終實現。它也強調了上帝對歷史的**掌管**，祂設定了被擄的期限。\n'
             '\n'
             '*   **新約的貢獻：** '
             '新約將「被擄」的概念擴展到人類因罪與上帝隔絕的狀態，而「歸回」則指向藉著基督與上帝和好。耶穌的救贖工作使我們從罪的權勢下被釋放，歸向上帝。\n'
             '    *   **路加福音 '
             '15:11-32**（浪子的比喻）：「我失而復得的兒子。」這個比喻生動地描繪了從迷失到歸回的過程，以及天父對浪子回家的喜悅。\n'
             '    *   **彼得前書 2:25**：「你們從前好像迷路的羊，如今卻歸到你們靈魂的牧人監督了。」\n'
             '    新約將這種歸回提升到靈性層面，強調了與上帝關係的恢復。\n'
             '\n'
             '*   **對我們與上帝關係的影響：** '
             '這個母題給予我們巨大的盼望，即使我們因罪而遠離上帝，祂仍然信實地等待我們回轉。它鼓勵我們在任何困境中都不要絕望，因為上帝的救贖計劃終將成就。它也提醒我們，真正的歸回是內心的悔改和對上帝的順服。\n'
