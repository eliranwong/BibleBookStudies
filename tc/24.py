             '### **二、 主要神學訊息 (Primary Theological Messages)**\n'
             '\n'
             '這些是耶利米書所傳達的核心教導和真理。\n'
             '\n'
             '#### **1. 因不悔改而來的審判必然性 (The Inevitability of Judgment Due to '
             'Unrepentance)**\n'
             '\n'
             '*   **詳細闡述：** '
             '耶利米書最嚴峻的訊息之一是，由於猶大百姓的持續不悔改，上帝的審判是必然且不可避免的。儘管耶利米不斷呼籲悔改，但百姓的剛硬和對上帝話語的拒絕，使得耶路撒冷的毀滅和被擄成為定局。上帝的公義要求對罪惡進行懲罰，而祂的忍耐也有極限。\n'
             '\n'
             '*   **希伯來文研究的幫助：**\n'
             "    *   **《不悔改》 (קָשֶׁה עֹרֶף, *kasheh 'oref* - 硬著頸項)：** "
             '這個短語在舊約中常用來形容以色列的頑固和不順服。它強調了百姓在面對上帝的警告時，選擇了抗拒而非回轉。\n'
             "    *   **《災禍》 (רָעָה, *ra'ah*)：** "
             '這個詞在耶利米書中頻繁出現，指上帝所降的災難、惡事或不幸。它強調了審判的真實性和嚴重性。\n'
             '\n'
             '*   **神學意涵：** '
             '這個訊息深刻地揭示了上帝的**公義**和**聖潔**。祂絕不以有罪為無罪。它也強調了**自由意志的後果**，人類有選擇順服或悖逆的自由，但必須為自己的選擇承擔責任。上帝的審判是祂對罪惡的最終回應。\n'
             '\n'
             '*   **新約的貢獻：** '
             '新約同樣強調了不悔改將導致審判，但將其指向末世的最終審判。耶穌和使徒們都呼籲人們悔改，以避免將來的忿怒。\n'
             '    *   **路加福音 13:3**：「我告訴你們，不是的！你們若不悔改，都要如此滅亡！」\n'
             '    *   **羅馬書 2:5**：「你竟任著你剛硬不悔改的心，為自己積蓄忿怒，以致上帝震怒，顯他公義審判的日子來到。」\n'
             '    新約將審判的必然性與耶穌基督的救贖聯繫起來，強調了悔改是進入救恩的唯一途徑。\n'
             '\n'
             '*   **對我們與上帝關係的影響：** '
             '這個訊息呼籲我們嚴肅對待罪惡，並認識到不悔改的嚴重後果。它激勵我們時刻省察自己，並以真誠的心向上帝悔改，因為祂是公義的，也是慈愛的。它也讓我們更加珍惜上帝所賜的悔改機會。\n'
             '\n'
             '#### **2. 呼喚真誠的悔改 (The Call for Genuine Repentance)**\n'
             '\n'
             '*   **詳細闡述：** '
             '儘管審判必然臨到，耶利米仍不斷呼籲百姓進行真誠的悔改。這種悔改不僅是外在行為的改變，更是內心和思想的轉變。他強調「你們要改正你們的行動作為，聽從耶和華你們上帝的話」（耶利米書 '
             '26:13），並指出百姓的悔改往往是表面的，內心並未真正歸向上帝（耶利米書 3:10）。\n'
             '\n'
             '*   **希伯來文研究的幫助：**\n'
             '    *   **《回轉/悔改》 (שׁוּב, *shuv*)：** '
             '這個詞在耶利米書中頻繁出現，是先知信息的核心。它不僅指地理上的「回轉」，更指道德和靈性上的「歸回」上帝。它強調了悔改的全面性，包括思想、情感和行為的轉變。\n'
             '\n'
             '*   **神學意涵：** '
             '這個訊息彰顯了上帝的**慈愛**和**憐憫**。祂不願見惡人死亡，乃願他回轉存活。它也強調了**人類的責任**，上帝提供了悔改的機會，但選擇權在於人。真誠的悔改是恢復與上帝關係的唯一途徑。\n'
             '\n'
             '*   **新約的貢獻：** 悔改是新約福音的核心信息。施洗約翰、耶穌和使徒們都以「你們當悔改！」作為他們事奉的開端。\n'
             '    *   **馬太福音 3:2**（施洗約翰）：「天國近了，你們應當悔改！」\n'
             '    *   **馬可福音 1:15**（耶穌）：「日期滿了，上帝的國近了。你們當悔改，信福音！」\n'
             '    *   **使徒行傳 '
             '2:38**（彼得）：「彼得說：『你們各人要悔改，奉耶穌基督的名受洗，叫你們的罪得赦，就必領受所賜的聖靈。』」\n'
             '    新約將悔改與相信耶穌基督聯繫起來，強調了悔改是進入上帝國度的必要條件。\n'
             '\n'
             '*   **對我們與上帝關係的影響：** '
             '這個訊息呼籲我們不斷省察自己的生命，並以真誠的心向上帝悔改。它提醒我們，上帝始終敞開雙臂歡迎回轉的兒女，祂的赦免和恩典是豐盛的。它也激勵我們不僅在行為上，更在內心深處歸向祂。\n'
             '\n'
             "#### **3. 上帝對列國和歷史的主權 (God's Sovereignty Over Nations and "
             'History)**\n'
             '\n'
             '*   **詳細闡述：** '
             '耶利米書不僅預言了猶大的命運，也包含了對埃及、巴比倫、摩押等列國的預言（第46-51章）。上帝興起巴比倫作為祂審判的工具，但最終也將審判巴比倫。這個訊息強調了上帝是全地的君王，祂掌管著所有國家和歷史的進程，沒有任何國家或勢力能超越祂的權柄。\n'
             '\n'
             '*   **希伯來文研究的幫助：**\n'
             "    *   **《耶和華說》 (נְאֻם יְהוָה, *ne'um YHWH*)：** "
             '這個短語在耶利米書中頻繁出現，強調了信息的來源是上帝，而非先知個人。它確立了上帝話語的權威性和祂對一切事物的最終掌控。\n'
             '    *   **《列國》 (גּוֹיִם, *goyim*)：** '
             '這個詞在耶利米書中不僅指猶大周邊的異教國家，也指上帝對這些國家的審判權柄。\n'
             '\n'
             '*   **神學意涵：** '
             '這個訊息深刻地揭示了上帝的**全能**和**主權**。祂是歷史的導演，萬事萬物都在祂的掌控之中。它也彰顯了上帝的**公義**，祂不僅審判其選民，也審判所有悖逆祂的國家。這為我們理解歷史事件背後的上帝旨意提供了框架。\n'
             '\n'
             '*   **新約的貢獻：** '
             '新約同樣強調上帝對列國和歷史的主權，尤其是在耶穌基督的復活和升天之後，祂被賜予了「天上地下所有的權柄」。\n'
             '    *   **馬太福音 28:18**：「耶穌進前來，對他們說：『天上地下所有的權柄都賜給我了。』」\n'
             '    *   **羅馬書 13:1**：「在上有權柄的，人人當順服他，因為沒有權柄不是出於上帝的；凡掌權的都是上帝所命的。」\n'
             '    新約將上帝的主權與基督的統治聯繫起來，表明祂是萬有的主宰。\n'
             '\n'
             '*   **對我們與上帝關係的影響：** '
             '這個訊息給予我們巨大的**安全感**和**盼望**。在動盪不安的世界中，我們知道有一位掌管一切的上帝。它鼓勵我們信靠上帝的計劃，即使我們無法完全理解，也相信祂會引導歷史走向祂最終的目標。它也呼籲我們為國家和領袖禱告，因為他們的權柄來自上帝。\n'
             '\n'
             '#### **4. 對未來復興和新約的盼望 (Hope for a Future Restoration and a New '
             'Covenant)**\n'
             '\n'
             '*   **詳細闡述：** '
             '儘管耶利米書充滿了審判的信息，但在最黑暗的時刻，上帝也透過耶利米發出了對未來復興和新約的應許。這包括百姓從被擄之地歸回、國家重建、以及最重要的是，上帝將與以色列和猶大另立「新約」，將律法寫在他們心上，使他們能真正認識祂（耶利米書 '
             '29:10-14, 31:31-34, 32:37-41）。\n'
             '\n'
             '*   **希伯來文研究的幫助：**\n'
             '    *   **《新約》 (בְּרִית חֲדָשָׁה, *berit chadashah*)：** '
             '這個短語在舊約中只出現在耶利米書 '
             '31:31，強調了這是一個與舊約不同、更深層次的約。它預示了上帝與人關係的根本性轉變，從外在的律法條文到內在的心靈更新。\n'
             "    *   **《寫在心上》 (כָּתַב עַל לֵב, *katav 'al lev*)：** "
             '這個意象強調了律法將不再是外在的規定，而是成為人內在的本性，使人能自發地順服上帝。\n'
             '\n'
             '*   **神學意涵：** '
             '這個訊息彰顯了上帝的**信實**和**恩典**。儘管百姓背約，上帝仍不放棄祂的選民，祂的救贖計劃終將實現。新約的應許揭示了上帝對人類關係的終極目標：建立一個內在、親密、永恆的關係，超越了舊約的限制。這為新約的救贖奠定了基礎。\n'
             '\n'
             '*   **新約的貢獻：** '
             '新約明確指出，耶利米所預言的「新約」已經藉著耶穌基督的血得以成就。耶穌在最後的晚餐中設立了新約，而希伯來書則詳細闡述了新約的優越性。\n'
             '    *   **路加福音 22:20**：「飯後也照樣拿起杯來，說：『這杯是用我血所立的新約，是為你們流出來的。』」\n'
             '    *   **希伯來書 8:8-12**（引用耶利米書 '
             '31:31-34）：「主又說：日子將到，我要與以色列家和猶大家另立新約...我要將我的律法放在他們裡面，寫在他們心上；我要作他們的上帝，他們要作我的子民。他們不用各人教導自己的鄉鄰和自己的弟兄，說：『你該認識主』；因為他們從最小的到至大的，都必認識我。我要寬恕他們的不義，不再記念他們的罪惡。」\n'
             '    新約將新約的應許與耶穌基督的犧牲和聖靈的內住聯繫起來，表明上帝已透過基督完成了對人心的根本改變。\n'
             '\n'
             '*   **對我們與上帝關係的影響：** '
             '這個訊息給予我們最深層次的**盼望**和**安慰**。我們知道上帝的愛和信實永不改變，祂的救贖計劃是完全的。它鼓勵我們活在新約的恩典之下，讓聖靈在我們心中工作，使我們能更深地認識上帝，並以真誠的心順服祂。它也提醒我們，我們與上帝的關係是基於祂的恩典，而非我們的行為。\n'
             '\n'
             '#### **5. 先知順服的代價 (The Cost of Prophetic Obedience)**\n'
             '\n'
             '*   **詳細闡述：** '
             '耶利米書生動地描繪了先知耶利米在忠實傳達上帝信息時所承受的巨大代價。他被囚禁、鞭打、投入泥坑、被自己的百姓和家人拒絕，甚至詛咒自己的生日。他的生命成為上帝話語的活見證，展示了順服上帝旨意所帶來的痛苦和孤獨。\n'
             '\n'
             '*   **希伯來文研究的幫助：**\n'
             "    *   **《受苦》 (עָנָה, *'anah*)：** "
             '這個詞在舊約中常用來形容受壓迫、受苦難。耶利米書中對先知受苦的描寫，強調了他在事奉中所經歷的真實痛苦。\n'
             '    *   **《孤獨》 (בָּדָד, *badad*)：** '
             '耶利米常常感到孤獨，因為他的信息使他與所有人都為敵。這種孤獨感是先知事奉的固有部分。\n'
             '\n'
             '*   **神學意涵：** '
             '這個訊息揭示了上帝的**呼召**是神聖且嚴肅的，但回應呼召往往意味著犧牲和苦難。它強調了**忠心**的重要性，即使面對巨大的反對和迫害，先知仍必須忠於上帝的話語。它也預示了彌賽亞受苦僕人的形象。\n'
             '\n'
             '*   **新約的貢獻：** '
