             '先知將視野擴展到更廣闊的普世審判，宣告「耶和華的日子臨近萬國」（第15a節），將以東的命運置於宏大的神聖歷史框架中。他明確指出報應原則：「你怎樣待人，人也必怎樣待你；你的報應必歸到你頭上。」（第15b節）並強調以東將像猶大一樣受審判，且將徹底滅亡（第16節）。這一階段將以東的特殊案例提升為一個普遍的原則，表明上帝的公義針對所有作惡的國家。\n'
