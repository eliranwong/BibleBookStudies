             '4.  **耶和華的日子 (The Day of the Lord)**\n'
             '    *   **重要性：** 俄巴底亞書中審判的宏大框架，指上帝介入歷史施行審判和拯救的時期（俄巴底亞書1:15）。\n'
             '    *   **聖經例子：** '
