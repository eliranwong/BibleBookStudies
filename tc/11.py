             '### **六、 規範性背景與與聖經整體連結**\n'
             '\n'
             '1.  **在舊約中的位置：**\n'
             '    '
             '《列王紀上》是「申命記歷史」的一部分，承接了《撒母耳記》中大衛王朝的敘事，並將敘事推進到王國的分裂和早期君王的統治。它為《列王紀下》中兩國的最終滅亡和被擄奠定了神學基礎，解釋了為何上帝允許這些災難發生。在整個聖經敘事中，它強調了上帝的約（尤其是大衛之約）的持續性，以及人類（特別是君王）對約的忠誠的重要性。它也為後來的先知書提供了歷史背景，許多先知書中的預言都是針對《列王紀》所描述的君王和國家狀況發出的。\n'
             '\n'
             '2.  **與新約的關係：**\n'
             '    *   **彌賽亞的預表：** '
             '所羅門的失敗凸顯了人類君王的局限性，強化了對一位**完美君王——彌賽亞**的盼望。耶穌基督是那位完全順服上帝旨意的君王，祂的國度是永恆的，祂自己也說：「看哪，在這裡有一人比所羅門更大。」（《馬太福音》12:42）。\n'
             '    *   **先知的預言與應驗：** '
             '以利亞的事奉預表了新約中施洗約翰的角色（《瑪拉基書》4:5；《馬太福音》11:14），而耶穌基督則是上帝最終和最完全的啟示者（《希伯來書》1:1-2）。\n'
             '    *   **聖殿的預表與實體：** '
             '所羅門建造的聖殿預表了新約中耶穌基督本身（《約翰福音》2:19-21）和祂的身體——教會（《哥林多前書》3:16）。\n'
             '    *   **道德與屬靈的教訓：** '
             '《列王紀上》中關於偶像崇拜的警告和順服上帝的呼籲，在新約中得到了延續和深化，例如將貪婪視為偶像崇拜（《歌羅西書》3:5），並強調聽道行道的重要性（《馬太福音》7:24-27）。\n'
