             '*   '
             '**真理的延續與應驗**：舊約中神是「誠實無偽的神」（申命記32:4），祂的律法是「真理」（詩篇119:142）。新約中，耶穌基督是真理的終極應驗（約翰福音14:6）。《約翰二書》將這份真理應用於辨別異端，強調對基督裡真理的堅守。\n'
             '*   '
             '**愛心與誡命的更新與應驗**：舊約律法強調愛神愛人如己（申命記6:5，利未記19:18）。新約中，耶穌賜下「彼此相愛」的新命令（約翰福音13:34）。《約翰二書》將這份愛心與真理緊密結合，教導真正的愛是「照著他的命令去行」（約貳1:6），包括堅守純正教義。\n'
             '*   '
