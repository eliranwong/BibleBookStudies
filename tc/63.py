             '*   '
             '**神學意涵**：基督的道成肉身是基督教信仰的不可妥協的核心（約翰福音1:14），是救贖的基礎（希伯來書2:14）。敵基督者代表一切反對基督及其真理的勢力，是末世的標誌（約翰一書2:18）。\n'
