             '*   '
             '**道成肉身：救贖歷史的高峰**：舊約預言彌賽亞將是真實的人（以賽亞書7:14，9:6）。耶穌基督的道成肉身是神救贖歷史的高峰（約翰福音1:14）。《約翰二書》捍衛這一核心真理，強調否認基督道成肉身是對整個聖經救贖大工的根本否定。\n'
