             '**重要性**：愛心是基督徒的標誌，是「從起初就有的命令」（約貳1:5）。它必須與真理緊密結合，並體現在「照著他的命令去行」（約貳1:6）。\n'
             '*   '
             '**神學意涵**：愛是神的本性（約翰一書4:8），是祂救贖計劃的根本動機（約翰福音3:16）。耶穌將愛神愛人如己歸結為律法的總綱（馬太福音22:37-39）。\n'
             '*   **對與神關係的影響**：我們因被神無條件的愛所觸動，而學習去愛神和愛人，這份愛是我們與神關係的核心。\n'
             '\n'
             '#### 3. 誡命與遵行 (Commandment & Obedience - 希臘文：ἐντολή & '
             'περιπατέω)\n'
             '*   '
