             '*   '
             '**約翰著作群**：與《約翰一書》在真理、愛心、基督道成肉身和敵基督者等主題上高度一致，是其神學原則的具體應用。與《約翰福音》共享耶穌是真理、道成肉身和彼此相愛的新命令等核心教導。與《約翰三書》共同提供了關於基督徒「好客」原則的完整教導：對傳播真理的要熱情接待，對傳播謬誤的要堅決拒絕。\n'
             '*   '
             '**其他新約書卷**：與保羅書信（如歌羅西書2:8，提摩太前書4:1-3）和《猶大書》（猶大書1:4）共同構成了使徒時代對異端威脅的統一戰線。與《希伯來書》（希伯來書2:14-18）對基督完全神性和人性的強調相輔相成。\n'
             '\n'
