             '4:1：「住撒馬利亞山，像巴珊母牛的啊，你們聽我的話！你們欺壓貧寒人，壓碎窮乏人，對丈夫說：『拿酒來，我們喝吧！』」\n'
             '        *   阿摩司書 '
             '5:11-12：「你們踐踏貧寒人，向他們勒索麥子；因此，你們雖建造鑿石的房屋，卻不得住在其內；栽種美好的葡萄園，卻不得喝其酒。我知道你們的罪過甚多，罪惡滔天：你們苦待義人，收受賄賂，在城門口屈枉窮乏人。」\n'
             '        *   阿摩司書 '
             '8:4-6：「你們這些壓碎窮乏人，使地上困苦人滅絕的，當聽我的話！你們說：『月朔幾時過去，我們好賣糧；安息日幾時過去，我們好開市，縮小伊法，增大舍客勒，用詭詐的天平欺哄人，為銀子買窮人，為一雙鞋買貧乏人，將壞了的麥子賣給人呢？』」\n'
             '\n'
             '*   **希伯來文研究如何幫助理解**：\n'
             '    *   '
             '**「窮人」的詞彙多樣性**：希伯來文有多個詞彙來指代「窮人」，每個詞都帶有細微的差別，阿摩司書中均有使用，強調了窮人受苦的全面性。例如：\n'
             "        *   ***'evyon*** "
             '(אֶבְיוֹן)：指極度貧困、無助且依賴他人或上帝的人，強調其赤貧和無力感。\n'
             '        *   ***dal*** (דַּל)：指弱小、低微、缺乏力量和資源的人，強調其社會地位的低下。\n'
             "        *   ***'ani*** (עָנִי)：指受苦、受壓迫、被欺凌的人，強調其所遭受的痛苦和不公。\n"
             '        '
             '阿摩司使用這些詞彙，不僅僅是描述經濟狀況，更是描繪一種**被社會系統性壓迫的狀態**，這使得上帝的憤怒更具合理性。\n'
             '    *   **「公義」與「公平」的深度**：阿摩司書 5:24 的核心經文：「唯願公平如大水滾滾，使公義如江河滔滔。」\n'
             '        *   ***Mishpat*** '
             '(מִשְׁפָּט)：通常譯為「公平」或「判斷」，指在法律和社會層面上的公正行為，是維護社會秩序和弱者權益的具體行動。它強調的是**正確的判斷和執行**。\n'
             '        *   ***Tsedaqah*** '
             '(צְדָקָה)：通常譯為「公義」或「正直」，指符合上帝標準的道德行為，是人際關係中的正直和誠實，特別是**對弱者的關懷和扶持**。\n'
             '        '
             '這兩個詞彙常常並列出現，強調上帝所要求的公義是全面的，既有法律層面的公正，也有道德層面的正直和憐憫。將其比作「大水滾滾」(*mayim '
             "'addirim*)和「江河滔滔」(*nahal "
             "'eytan*)，描繪了一種**強大、持續、不可阻擋的公義之流**，與當時以色列社會中零星、被扭曲的「公義」形成鮮明對比。這不僅是呼籲，更是對上帝本性的宣告。\n"
             '\n'
             '*   '
             '**神學意涵**：這個主題深刻地揭示了上帝的**公義屬性**。上帝不僅是創造主和掌權者，祂更是一位**關懷弱勢、憎惡不公**的上帝。祂的品格要求祂的子民在社會中實踐公義和憐憫。這也表明上帝的計劃不僅關乎個人的救贖，更關乎建立一個**符合祂心意的公義社會**。祂與受壓迫者認同，並將社會倫理視為信仰的核心。\n'
             '\n'
             '*   **新約的貢獻**：新約聖經延續並深化了這一主題。\n'
             '    *   **耶穌的教導**：耶穌在登山寶訓中宣告「虛心的人有福了，因為天國是他們的」（馬太福音 '
             '5:3），並多次強調對窮人的關懷（路加福音 '
             '6:20-21）。祂譴責法利賽人的虛偽和對律法中「更重要的事」——「公義、憐憫、信實」的忽略（馬太福音 23:23）。\n'
             '    *   '
             '**雅各書的強調**：雅各書強烈譴責教會中對富人的偏袒和對窮人的歧視，指出「我親愛的弟兄們，請聽：上帝豈不是揀選了世上的貧窮人，叫他們在信心上富足，並承受祂所應許給那些愛祂之人的國嗎？你們反倒羞辱貧窮人」（雅各書 '
             '2:5-6）。雅各書 5:1-6 也嚴厲警告那些剝削工人、積攢不義之財的富人。\n'
             '    這些新約經文都與阿摩司書的信息遙相呼應，強調了上帝對社會公義的永恆要求，並將其視為真信仰的標誌。\n'
             '\n'
             '*   '
             '**對與上帝連結的影響**：理解這一主題，會促使我們反思自己的生活方式和社會責任。它挑戰我們走出個人信仰的舒適圈，去關注和參與社會中的不公義現象。它要求我們培養**同理心和憐憫之心**，將對弱勢群體的關懷視為對上帝的服事。這能深化我們與上帝的連結，因為我們在實踐公義時，就是在效法上帝的品格，並參與到祂建立公義國度的計劃中。\n'
             '\n'
             '#### **2. 宗教虛偽與空洞的儀式主義 (Religious Hypocrisy and Empty '
             'Ritualism)**\n'
             '\n'
             '*   '
             '**詳細闡述**：阿摩司嚴厲譴責以色列人將宗教儀式與道德行為割裂開來。他們熱衷於獻祭、守節期、朝聖，卻在日常生活中充滿罪惡和不公。上帝明確表示厭惡這種徒有其表的敬拜，因為它缺乏真誠的悔改和公義的行動。\n'
             '    *   **經文例證**：\n'
             '        *   阿摩司書 '
             '4:4-5：「以色列人哪，任你們往伯特利去犯罪，到吉甲去多多的犯罪！每天早晨獻祭，每三天獻上十分之一。獻有酵的感謝祭，報告甘心祭；因為你們喜歡這樣行，以色列人哪！」（此處帶有強烈諷刺意味）\n'
             '        *   阿摩司書 '
             '5:21-23：「我厭惡你們的節期，也不喜悅你們的嚴肅會。你們雖然向我獻燔祭和素祭，我卻不悅納，也不顧你們用肥畜獻的平安祭。要將歌唱的聲音遠離我，因為我不聽你們彈琴的聲音。」\n'
             '        *   阿摩司書 '
             '8:5：「你們說：『月朔幾時過去，我們好賣糧；安息日幾時過去，我們好開市...』」這表明他們遵守宗教節期只是為了方便進行不義的商業活動。\n'
             '\n'
             '*   **希伯來文研究如何幫助理解**：\n'
             "    *   **「厭惡」的強度**：動詞 ***sane'*** "
             '(שָׂנֵא)，譯為「厭惡」或「恨惡」，表達了上帝對這種虛偽敬拜的強烈反感和排斥。這不是簡單的不悅，而是深層次的憎惡，表明這種敬拜不僅無效，反而觸怒了上帝。\n'
             '    *   '
             "**具體儀式的提及**：阿摩司明確提及了「節期」(*chag*)、「嚴肅會」(*'atsarah*)、「燔祭」(*'olah*)、「素祭」(*minchah*)、「平安祭」(*shelem*)等，這些都是摩西律法中規定的、本應是聖潔的敬拜形式。先知指出，即使是上帝親自設立的儀式，一旦脫離了敬拜者的心靈和行為，也會變得毫無意義，甚至成為罪惡的遮羞布。上帝拒絕「歌唱的聲音」(*qol "
             'shir*)和「彈琴的聲音」(*zimrat nevalim*)，強調祂所看重的是敬拜的**內在實質**，而非外在形式的華麗。\n'
             '\n'
             '*   '
             '**神學意涵**：這個主題揭示了上帝的**聖潔與真實**。上帝不能被形式化的敬拜所愚弄或收買。祂要求的是**全心全意的順服和內在的正直**，而非僅僅是外在的宗教表演。真正的信仰必須是**整體性的**，將對上帝的敬畏與對人的公義行為緊密結合。這也表明上帝的計劃是尋求真實的關係，而非空洞的儀式。\n'
             '\n'
             '*   **新約的貢獻**：新約聖經對宗教虛偽的譴責與阿摩司書一脈相承。\n'
             '    *   '
             '**耶穌對法利賽人的批判**：耶穌多次嚴厲批判法利賽人的虛偽，他們「把難擔的重擔捆起來，擱在人的肩上，自己卻不肯用一個指頭動一下」（馬太福音 '
             '23:4）。祂強調「上帝是靈，所以敬拜祂的必須用心靈和誠實敬拜祂」（約翰福音 4:24）。\n'
             '    *   **保羅的教導**：保羅在羅馬書中指出，真正的割禮是「心裡的，在乎聖靈，不在乎儀文」（羅馬書 '
             '2:29），強調了內在生命的轉變比外在儀式更重要。\n'
             '    這些新約經文都強化了阿摩司的信息，呼籲信徒追求內在的真實和倫理的實踐，而非僅僅是外在的宗教形式。\n'
             '\n'
             '*   '
             '**對與上帝連結的影響**：這個主題挑戰我們審視自己的信仰生活，是否流於形式，是否將宗教活動與日常行為割裂。它提醒我們，上帝看重的是我們**內心的真誠和行為的公義**。這促使我們追求一種**整合性的信仰**，讓我們的敬拜、禱告和事奉都源於一顆清潔的心，並體現在我們對鄰舍的愛和對社會公義的追求上。這能使我們與上帝的連結更加真實和深刻。\n'
             '\n'
             '#### **3. 神聖審判的必然性與「耶和華的日子」 (The Inevitability of Divine '
             'Judgment and "The Day of the Lord")**\n'
             '\n'
             '*   '
             '**詳細闡述**：阿摩司書充滿了對以色列及其鄰國的審判預言。先知明確指出，由於以色列的罪惡深重且不肯悔改，上帝的審判是不可避免的。他特別重新定義了「耶和華的日子」，將其從以色列人所期待的救贖之日，轉變為對他們而言的黑暗審判之日。\n'
             '    *   **經文例證**：\n'
             '        *   阿摩司書 '
             '2:13-16：「看哪，我必壓住你們，如同裝滿禾捆的車壓地一樣。快跑的不能逃脫，有力的不能用勁，勇士不能自救...那日，勇士中最勇敢的也必赤身逃跑。」\n'
             '        *   阿摩司書 4:12：「以色列啊，你當預備迎見你的上帝！」（這是一句嚴厲的警告，而非邀請）\n'
             '        *   阿摩司書 '
             '5:2-3：「以色列跌倒，不能再起來，被拋棄在地上，無人扶持。主耶和華如此說：從城裡出來一千的，只剩下一百；從城裡出來一百的，只剩下十個。」\n'
             '        *   阿摩司書 '
             '5:18-20：「想望耶和華日子的人有禍了！那日於你們有何益處呢？那日是黑暗，並非光明。如同人躲避獅子卻遇見熊，或進房屋扶牆卻被蛇咬。耶和華的日子豈不是黑暗，並非光明嗎？是幽暗毫無光輝。」\n'
             '        *   阿摩司書 '
             '7:8：「主對我說：『阿摩司啊，你看見什麼？』我說：『鉛線。』主說：『看哪，我必在我民以色列中安放鉛線，我必不再寬恕他們。』」\n'
             '        *   阿摩司書 '
             '9:1-4：「我看見主站在祭壇旁邊，祂說：『擊打柱頂，使門檻震動，將柱子折斷，落在眾人頭上...他們雖挖透陰間，我的手必從那裡抓出他們；雖爬上天去，我的手必從那裡拉下他們...』」\n'
             '\n'
             '*   **希伯來文研究如何幫助理解**：\n'
             '    *   **「耶和華的日子」的顛覆性詮釋**：希伯來文 ***yom YHWH*** (יוֹם יְהוָה) '
             '在其他先知書中常指上帝為祂子民爭戰、擊敗敵人的日子。然而，阿摩司卻將其徹底顛覆，宣告對以色列而言，那將是「黑暗，並非光明」(*choshekh '
             "v'lo 'or*)。這種強烈的對比和反轉，旨在震醒那些自以為安全的以色列人。黑暗 (*choshekh*) 和幽暗 "
             "(*'afelah*) 的意象，強調了審判的恐怖、絕望和無可逃避。\n"
             "    *   **「不再寬恕」的決絕**：短語 ***lo 'osif 'od la'avor*** (לֹא־אוֹסִף "
             'עוֹד לַעֲבוֹר)，在 7:8 和 8:2 中出現，意為「我必不再越過（他們的罪）」，或「我必不再寬恕」。動詞 '
             "***'avar*** (עָבַר) "
             '在此處有「越過、放過」之意。這表明上帝的忍耐已達到極限，祂已作出最終的決定，審判將不再延遲或撤回。這與前兩個異象中阿摩司代求後上帝的「後悔」形成鮮明對比，凸顯了審判的最終性和不可逆轉性。\n'
             '\n'
             '*   '
             '**神學意涵**：這個主題深刻地揭示了上帝的**聖潔、公義和主權**。上帝是絕對公義的，祂不能容忍罪惡。祂也是掌管歷史和萬有的主宰，沒有任何力量可以阻擋祂的旨意。祂對選民的審判，表明祂對盟約的忠誠，並非無條件的偏袒，而是要求子民履行盟約的責任。這也強調了上帝的警告是真實的，祂的耐心是有限的，祂的計劃包括對罪惡的最終清算。\n'
             '\n'
             '*   **新約的貢獻**：新約聖經同樣強調了神聖審判的必然性，並將其指向耶穌基督的再來。\n'
             '    *   **耶穌的警告**：耶穌多次警告將來的審判，例如在馬太福音 7:21-23 '
             '中，祂說：「凡稱呼我『主啊，主啊』的人不能都進天國；唯獨遵行我天父旨意的人才能進去。」祂也講述了綿羊和山羊的比喻，強調審判將基於人對「最小的弟兄」的行為（馬太福音 '
             '25:31-46）。\n'
             '    *   **保羅和彼得的教導**：保羅在帖撒羅尼迦前書 5:2-3 '
             '中提到「主的日子來到，好像夜間的賊一樣」，並警告那些說「平安穩妥」的人將面臨突然的毀滅。彼得後書 3:10 '
             '也描述了「主的日子要像賊來到一樣」，屆時天地都將被烈火銷化。\n'
             '    這些新約經文都與阿摩司書的信息相呼應，提醒信徒要警醒預備，因為審判是真實且不可避免的。\n'
             '\n'
             '*   '
             '**對與上帝連結的影響**：理解這一主題，會培養我們對上帝的**敬畏之心**。它提醒我們，罪惡的後果是嚴重的，上帝的警告是真實的。這促使我們認真對待自己的罪，並積極悔改，尋求與上帝和好。它也鼓勵我們過一種警醒、順服的生活，因為我們知道有一天要向上帝交賬。這種敬畏不是恐懼，而是對上帝聖潔和公義的尊重，從而深化我們與祂的關係。\n'
             '\n'
             "#### **4. 上帝的普世主權與大能 (God's Universal Sovereignty and Power)**\n"
             '\n'
             '*   '
             '**詳細闡述**：阿摩司書不僅將耶和華描繪為以色列的上帝，更將祂呈現為掌管萬國萬有、創造宇宙的至高主宰。祂的權柄超越了以色列的疆界，祂審判列國的罪惡，也掌控著自然界的一切。\n'
             '    *   **經文例證**：\n'
             '        *   阿摩司書 '
             '1:3-2:3：上帝對亞蘭、非利士、推羅、以東、亞捫和摩押等列國的審判，證明祂對所有國家都有主權。\n'
             '        *   阿摩司書 '
             '4:13：「看哪，那造山、造風、將心意指示人、使晨光變為幽暗、腳踏在地之高處的，祂的名是耶和華萬軍之神。」\n'
             '        *   阿摩司書 '
             '5:8-9：「祂造昴星和參星，使死蔭變為晨光，使白日變為黑夜，祂召海水來澆灌地面；耶和華是祂的名。祂使毀滅臨到堅固城，使堅固城變為廢墟。」\n'
             '        *   阿摩司書 '
             '9:5-6：「主耶和華萬軍之神，祂一摸地，地就融化，住在其上的都必悲哀；地必像尼羅河水漲起又退去。祂在天上建造樓閣，在地上建立穹蒼，召海水來澆灌地面；耶和華是祂的名。」\n'
             '        *   阿摩司書 '
             '9:7：「耶和華說：『以色列人哪，你們在我眼中豈不與古實人一樣嗎？我豈不是領以色列人出埃及地，也領非利士人從迦斐託出來，領亞蘭人從吉珥出來嗎？』」\n'
