             '《箴言》中充滿了對愚昧人的警告和對愚昧行為的譴責，將愚昧與懶惰、傲慢、不聽勸告、自取滅亡連結起來（箴言1:7, 10:1）。\n'
             '    *   **新約的愚昧：** '
             '新約聖經將愚昧與不認識上帝、拒絕福音連結。羅馬書1:21-22指出，那些不認識上帝的人「他們的思念變為虛妄，無知的心就昏暗了。自稱為聰明，反成了愚拙。」耶穌也曾用無知財主的比喻（路加福音12:16-21），來警示那些只為自己積攢財富卻不為上帝富足的愚昧人。\n'
             '\n'
             '### 6. 時間 / 定期 (Time / Season)\n'
             '\n'
             '*   **在《傳道書》中的重要性：** 「時間」或「定期」（希伯來文：עֵת, *et* 和 זְמָן, '
             '*zeman*）是《傳道書》中一個重要的母題，尤其在3:1-8的著名詩歌中得到最充分的表達。傳道者指出「**凡事都有定期，天下萬務都有定時**」（傳3:1），列舉了生命中各種對立的經驗。這強調了時間的不可控性、循環性，以及人類在時間面前的無力感。一切事件的發生都有其特定的「定時」，不受人意支配。\n'
             '    *   **例證：** 「生有時，死有時；栽種有時，拔出所栽種的也有時。」（傳3:2）。\n'
             '\n'
             '*   **在整本聖經中的重要性：** 聖經強調上帝是時間的主宰，祂在歷史中掌權。\n'
             '    *   **上帝的主權：** '
             '詩篇31:15說：「我終身的事在你手中。」這表明上帝掌管著我們生命的每一個時刻。但以理書2:21也說：「他改變時候、日期、廢王、立王。」\n'
             '    *   **新約的時機：** 新約聖經強調上帝在歷史中的「時機」（希臘文：καιρός, '
             '*kairos*）。加拉太書4:4說：「及至時候滿足，神就差遣他的兒子，為女子所生，且生在律法以下。」這表明耶穌的降生是在上帝預定的完美時機。使徒行傳1:7也說：「耶穌對他們說：父憑著自己的權柄所定的時候、日期，不是你們可以知道的。」這再次強調了上帝對時間的絕對主權，人類不應過度探究，而應專注於當下的使命。\n'
             '\n'
             '### 7. 神 (God)\n'
             '\n'
             '*   **在《傳道書》中的重要性：** 儘管《傳道書》充滿了對「虛空」的感嘆，但「神」（希伯來文：אֱלֹהִים, '
             '*Elohim*）這個詞在書中出現了四十多次，是傳道者最終答案的關鍵。傳道者雖然觀察到世間的不公和生命的無常，但他從未否認上帝的存在和主權。他認為享受勞碌的成果是「出於神的手」（傳2:24），神使萬事各按其時成為美好（傳3:11），神必審判一切（傳3:17，12:14）。最終，他將人生的總結歸結為「敬畏神，謹守他的誡命」（傳12:13）。\n'
             '    *   **例證：** '
             '「人莫強如吃喝，且在勞碌中享福，我看這也是出於神的手。」（傳2:24）；「神造萬物，各按其時成為美好。」（傳3:11）。\n'
             '\n'
             '*   **在整本聖經中的重要性：** 「神」是整本聖經的中心，祂是創造者、掌權者、救贖者和審判者。\n'
             '    *   **創造者與主宰：** '
             '創世記1:1開篇即宣告：「起初，神創造天地。」詩篇103:19說：「耶和華在天上立定寶座，他的權柄統管萬有。」這與《傳道書》中上帝對時間和萬物的掌管相呼應。\n'
             '    *   **審判者：** '
             '詩篇96:13說：「因為他來了，他來要審判全地。他要按公義審判世界，按他的信實審判萬民。」這印證了《傳道書》12:14中「神都必審問」的最終結論。\n'
             '    *   **新約的啟示：** '
             '新約聖經透過耶穌基督更完全地啟示了上帝的愛和救贖計劃。約翰福音3:16說：「神愛世人，甚至將他的獨生子賜給他們，叫一切信他的，不致滅亡，反得永生。」這為《傳道書》中對生命意義的追問提供了終極的答案。\n'
             '\n'
             '### 8. 死亡 (Death)\n'
             '\n'
             '*   **在《傳道書》中的重要性：** 「死亡」（希伯來文：מָוֶת, '
             '*mavet*）是《傳道書》中一個揮之不去的陰影，它被視為一切人類努力的最終終結者和最大的平等者。無論是智慧人還是愚昧人，義人還是惡人，最終都將歸於塵土。死亡的必然性使得一切世俗的成就和區別都顯得微不足道，加劇了「虛空」的感受。\n'
             '    *   **例證：** '
             '「因為活著的人知道必死；死了的人卻一無所知，也不再得賞賜；他們的名無人記念。」（傳9:5）；「塵土仍歸於地，靈仍歸於賜靈的神。」（傳12:7）。\n'
             '\n'
             '*   **在整本聖經中的重要性：** 死亡是聖經中一個核心的現實，是罪的結果，但最終被基督戰勝。\n'
             '    *   **罪的工價：** 羅馬書6:23說：「因為罪的工價乃是死。」這解釋了死亡的起源。\n'
             '    *   **基督的勝利：** '
             '新約聖經透過耶穌基督的復活，宣告了對死亡的勝利。哥林多前書15:54-57說：「這必朽壞的總要變成不朽壞的，這必死的總要變成不死的。那時經上的話就應驗了：『死啊！你得勝的權勢在哪裡？死啊！你的毒鉤在哪裡？』死的毒鉤就是罪，罪的權勢就是律法。感謝神，使我們藉著我們的主耶穌基督得勝。」這將《傳道書》中死亡的悲觀陰影轉化為復活的盼望。\n'
             '    *   **死後審判：** '
             '希伯來書9:27說：「按著定命，人人都有一死，死後且有審判。」這為《傳道書》中對死亡的探討提供了最終的歸宿。\n'
             '\n'
             '### 9. 享受 / 快樂 (Enjoyment / Pleasure)\n'
             '\n'
             '*   **在《傳道書》中的重要性：** '
             '儘管傳道書充滿了對「虛空」的感嘆，但它也反覆出現一個積極的勸勉：在勞碌中享受飲食、工作和家庭的樂趣。這並非享樂主義，而是在認識到生命短暫和世俗虛空之後，對上帝所賜恩典的一種感恩和接受。傳道者多次指出，這是「**出於神的手**」（傳2:24）、「**神的恩賜**」（傳3:13，5:19）。\n'
             '    *   **例證：** '
             '「人莫強如吃喝，且在勞碌中享福，我看這也是出於神的手。」（傳2:24）；「我所見為善為美的，就是人在神賜他一生的日子吃喝，享受日光之下勞碌得來的。」（傳5:18）。\n'
             '\n'
             '*   **在整本聖經中的重要性：** 聖經肯定了上帝是良善的賜予者，鼓勵人們感恩地享受祂所賜的一切。\n'
             '    *   **上帝的恩典：** '
             '提摩太前書6:17說：「你要囑咐那些今世富足的人，不要自高，也不要倚靠無定的錢財；只要倚靠那厚賜百物給我們享受的神。」這與《傳道書》中將享受視為「神的恩賜」的觀點一致。\n'
             '    *   **喜樂的源頭：** '
             '詩篇16:11說：「你必將生命的道路指示我。在你面前有滿足的喜樂；在你右手中有永遠的福樂。」這表明真正的喜樂和滿足最終源於上帝本身。\n'
             '\n'
             '### 10. 欺壓 (Oppression)\n'
             '\n'
             '*   **在《傳道書》中的重要性：** '
             '傳道者對世間的不公義和欺壓有著敏銳的觀察。他看到窮人受欺壓，公義被奪去，卻無人安慰（傳4:1）。他觀察到惡人掌權，義人受苦，這與傳統的因果報應觀念相悖，加劇了他對「虛空」的感受。\n'
             '    *   **例證：** '
             '「我又轉念，看見日光之下所行的一切欺壓。看哪，受欺壓的流淚，卻無人安慰；欺壓他們的有勢力，也無人安慰。」（傳4:1）；「你若在省中見窮人受欺壓，並奪去公義公平的事，不要因此詫異；因有一位高過居高位的察看，在他們以上還有更高的。」（傳5:8）。\n'
             '\n'
             '*   **在整本聖經中的重要性：** '
             '聖經，特別是舊約先知書，對欺壓和不公義有著強烈的譴責，並將上帝描繪為公義的審判者和受欺壓者的辯護者。\n'
             '    *   **先知的譴責：** '
             '以賽亞書1:17勸勉：「學習行善，尋求公平，解救受欺壓的，為孤兒伸冤，為寡婦辨屈。」阿摩司書5:24說：「惟願公平如大水滾滾，使公義如江河滔滔。」這些經文都強烈譴責社會不公和欺壓。\n'
             '    *   **上帝的公義：** '
             '詩篇10:17-18說：「耶和華啊，謙卑人的心願，你早已知道。你必堅固他們的心，也必側耳聽他們的祈求；為要給孤兒和受欺壓的人伸冤，使地上的人不再威嚇。」這表明上帝是受欺壓者的最終盼望。\n'
             '\n'
             '### 11. 公義 (Justice)\n'
             '\n'
             '*   **在《傳道書》中的重要性：** '
             '傳道者對世間缺乏公義感到困惑和不滿。他觀察到「**在審判的地方有奸惡，在公義的地方也有奸惡**」（傳3:16），以及「**義人所當得的報應，反臨到惡人；惡人所當得的報應，反臨到義人**」（傳8:14）。這種對現實中公義缺失的觀察，是導致他對人生感到虛空的重要原因之一。然而，他最終相信「**神必審判義人和惡人**」（傳3:17），為公義的最終實現提供了盼望。\n'
             '    *   **例證：** '
             '「我又看見日光之下，在審判的地方有奸惡，在公義的地方也有奸惡。」（傳3:16）；「義人所當得的報應，反臨到惡人；惡人所當得的報應，反臨到義人。」（傳8:14）。\n'
             '\n'
             '*   **在整本聖經中的重要性：** 公義是上帝屬性的一個核心方面，也是祂對人類社會的期望。\n'
             '    *   **上帝的屬性：** '
             '申命記32:4說：「他是磐石，他的作為完全；他所行的無不公平，是誠實無偽的神；又公義，又正直。」詩篇89:14說：「公義和公平是你寶座的根基。」\n'
             '    *   **新約的公義：** '
             '新約聖經強調上帝的公義透過耶穌基督的救贖得以彰顯。羅馬書3:21-26解釋了上帝如何透過基督的犧牲，既顯為公義，又稱信耶穌的人為義。彼得前書2:23說耶穌「他被罵不還口；受害不說威嚇的話，只將自己交託那按公義審判人的主。」這表明最終的公義審判權在上帝手中。\n'
             '\n'
             '### 12. 敬畏神 (Fear God)\n'
             '\n'
             '*   **在《傳道書》中的重要性：** 「敬畏神」（希伯來文：יִרְאַת אֱלֹהִים, *yir’at '
             'Elohim*）是《傳道書》最終的結論和解決方案。在經歷了對一切世俗追求的徹底檢視和「虛空」的宣告之後，傳道者最終將人生的意義錨定在對上帝的敬畏和順服上。這不是一種恐懼，而是一種尊崇、敬重和順服的態度，承認上帝的至高無上和人類的有限。\n'
             '    *   **例證：** '
             '「這些事都已聽見了，總意就是：敬畏神，謹守他的誡命，這是人所當盡的本分。」（傳12:13）；「你只當敬畏神。」（傳5:7）。\n'
             '\n'
             '*   **在整本聖經中的重要性：** 「敬畏神」是聖經中一個貫穿始終的核心教導，是智慧的開端，也是與上帝建立正確關係的基礎。\n'
             '    *   **智慧的開端：** 箴言9:10說：「敬畏耶和華是智慧的開端；認識至聖者便是聰明。」\n'
             '    *   **蒙福的條件：** '
             '申命記10:12-13說：「以色列啊，現在耶和華你神向你所要的是什麼呢？只要你敬畏耶和華你的神，遵行他的道，愛他，盡心盡性事奉他，遵守他的誡命律例，就是我今日所吩咐你的，為要叫你得福。」\n'
             '    *   **新約的敬畏：** '
             '新約聖經也強調敬畏上帝。使徒行傳9:31描述早期教會「敬畏主，蒙聖靈的安慰，人數就增多了。」彼得前書2:17勸勉：「務要尊敬眾人，親愛教中的弟兄，敬畏神，尊敬君王。」這表明敬畏神是基督徒生活的重要組成部分。\n'
             '\n'
             '### 13. 謹守他的誡命 (Keep His Commandments)\n'
             '\n'
             '*   **在《傳道書》中的重要性：** 「謹守他的誡命」（希伯來文：שָׁמַר מִצְוֹתָיו, *shamar '
             'mitzvotav*）與「敬畏神」緊密相連，是《傳道書》最終結論的另一半（傳12:13）。這表明對上帝的敬畏不僅僅是一種內心的態度，更要體現在實際的行動中，即遵行上帝的律法和旨意。這是人類在一個充滿「虛空」的世界中，找到意義和方向的具體途徑。\n'
             '    *   **例證：** 「這些事都已聽見了，總意就是：敬畏神，謹守他的誡命，這是人所當盡的本分。」（傳12:13）。\n'
             '\n'
             '*   **在整本聖經中的重要性：** 遵守上帝的誡命是舊約律法的核心，也是新約信徒愛上帝的表現。\n'
             '    *   **舊約的律法：** '
             '申命記6:24說：「耶和華又吩咐我們遵行這一切律例，要敬畏耶和華我們的神，使我們常得好處，蒙他保全我們的性命，像今日一樣。」\n'
             '    *   **新約的愛：** '
             '耶穌在約翰福音14:15說：「你們若愛我，就必遵守我的命令。」約翰一書5:3說：「我們遵守神的誡命，這就是愛他了，並且他的誡命不是難守的。」這表明新約將遵守誡命與愛上帝連結起來，使其不再是律法主義的重擔，而是愛的自然流露。\n'
             '\n'
             '### 14. 益處 / 利益 (Advantage / Profit)\n'
             '\n'
             '*   **在《傳道書》中的重要性：** 「益處」或「利益」（希伯來文：יִתְרוֹן, '
             '*yitron*）是《傳道書》開篇提出的核心問題：「人一切的勞碌，就是他在日光之下的勞碌，有什麼益處呢？」（傳1:3）。傳道者在書中反覆探討，人類的各種努力和追求是否能帶來真正的、持久的「益處」。他最終的結論是，在「日光之下」，這些世俗的追求都無法帶來永恆的益處，因為它們最終都歸於虛空。\n'
             '    *   **例證：** '
             '「人一切的勞碌，就是他在日光之下的勞碌，有什麼益處呢？」（傳1:3）；「後來，我察看我手所經營的一切事和我勞碌所做的工夫。誰知都是虛空，都是捕風；在日光之下毫無益處。」（傳2:11）。\n'
             '\n'
             '*   **在整本聖經中的重要性：** 聖經教導我們，真正的益處和永恆的利益在於上帝本身，而非世俗的積累。\n'
             '    *   **屬靈的益處：** '
             '馬可福音8:36說：「人就是賺得全世界，賠上自己的生命，有什麼益處呢？」這直接挑戰了世俗對「益處」的定義，強調了靈魂的永恆價值。\n'
             '    *   **敬虔的益處：** '
             '提摩太前書4:8說：「操練身體，益處還少；惟獨敬虔，凡事都有益處，因有今生和來生的應許。」這表明真正的益處來自於敬虔的生活，它具有跨越今生來世的價值。\n'
             '\n'
             '### 15. 靈 / 風 (Spirit / Wind)\n'
             '\n'
             '*   **在《傳道書》中的重要性：** 「靈」或「風」（希伯來文：רוּחַ, '
             '*ruach*）在《傳道書》中有多重含義。它可以指「風」（傳1:6），「氣息」（傳3:19），「人的靈」（傳3:21，12:7），以及「捕風」（傳1:14，指徒勞無功）。這種多義性反映了生命的飄渺和不可捉摸。特別是「捕風」這個短語，與「虛空」緊密相連，強調了人類努力的徒勞和無法把握。\n'
             '    *   **例證：** '
