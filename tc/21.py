             '6.  **結語：傳道者的權威與最終的結論 (12:9-14)**\n'
             '    這部分通常被認為是編輯者的聲音或傳道者最終的神學總結：\n'
             '    *   **傳道者的智慧與教導 (12:9-12)：** 肯定傳道者的智慧和教導的價值，並警告不要過度追求學問。\n'
             '    *   **全書的總結：敬畏神，遵守誡命 (12:13-14)：** '
             '這是全書的高潮和最終的答案：「**這些事都已聽見了，總意就是：敬畏神，謹守他的誡命，這是人所當盡的本分。**」最終的原因是「**因為人所做的事，連一切隱藏的事，無論是善是惡，神都必審問。**」\n'
             '\n'
             '### 三、 核心關鍵詞與神學信息\n'
             '\n'
             '《傳道書》透過一系列關鍵詞傳達其深刻的神學信息：\n'
             '\n'
             '1.  **虛空 (Hevel / Vanity / Meaningless)：**\n'
             '    *   **意義：** '
             '指「短暫」、「飄渺」、「不可捉摸」、「徒勞」。是傳道者對「日光之下」一切世俗追求的最終判斷（傳1:2, 1:14）。\n'
             '    *   **神學信息：** 挑戰人類將受造物偶像化的傾向，揭示脫離上帝而追求意義的徒勞。\n'
             '    *   **聖經連結：** '
             '與創世記3章人類墮落後受造界的咒詛相呼應（羅馬書8:20-21），並為新約中基督所帶來的永恆意義預備背景（馬太福音6:19-21，哥林多前書15:58）。\n'
             '\n'
             '2.  **日光之下 (Tachat ha-shemesh / Under the Sun)：**\n'
             '    *   **意義：** 傳道者觀察和思考的有限視角，即純粹從人類世俗層面來看待世界（傳1:3, 1:9）。\n'
             '    *   **神學信息：** 突顯人類智慧的局限性，表明若無上帝啟示，對生命意義的探討終將導向虛空。\n'
             '    *   **聖經連結：** 與新約中鼓勵信徒「思念上面的事，不要思念地上的事」形成對比（歌羅西書3:1-2）。\n'
             '\n'
             '3.  **勞碌 (Amal / Toil)：**\n'
             '    *   **意義：** 人類在世間的辛勞和努力（傳1:3, 2:11）。\n'
             '    *   **神學信息：** 在墮落世界中，勞碌往往是徒勞和愁苦的，但享受勞碌的成果是上帝的恩賜（傳2:24）。\n'
             '    *   **聖經連結：** '
             '源於創世記3章人類墮落後的咒詛，但在新約中，在基督裡的勞碌「不是徒然的」（哥林多前書15:58）。\n'
             '\n'
             '4.  **智慧 (Chokmah) 與 愚昧 (Kesilut)：**\n'
             '    *   **意義：** 傳道者對兩者的比較，承認智慧優於愚昧，但兩者最終都難逃死亡的命運（傳1:18, '
             '2:13-16）。\n'
             '    *   **神學信息：** 人類智慧有其局限，無法解決生命根本問題。真正的智慧源於上帝。\n'
             '    *   **聖經連結：** '
             '與《箴言》中對智慧的肯定和《約伯記》中對人類智慧的挑戰形成對話。新約指出基督就是上帝的智慧（哥林多前書1:30）。\n'
             '\n'
             '5.  **時間 / 定期 (Et / Zeman / Time / Season)：**\n'
             '    *   **意義：** 指萬事萬物都有其特定的時機和循環，不受人意支配（傳3:1-8）。\n'
             '    *   **神學信息：** 肯定上帝對時間和歷史的絕對主權，人類無法掌控未來。\n'
             '    *   **聖經連結：** '
             '與詩篇中上帝掌管一切的讚美（詩篇103:19）和新約中上帝對「時候、日期」的絕對主權（馬太福音24:36，使徒行傳1:7）相呼應。\n'
             '\n'
             '6.  **神 (Elohim / God)：**\n'
             '    *   **意義：** '
             '傳道者最終答案的關鍵。他從未否認上帝的存在和主權，認為享受是「出於神的手」，神使萬物美好，神必審判一切（傳2:24, 3:11, '
             '12:14）。\n'
             '    *   **神學信息：** 上帝是超越一切的創造者、掌權者和最終審判者。\n'
             '    *   **聖經連結：** '
             '貫穿整本聖經的中心主題，新約透過耶穌基督更完全地啟示了上帝的愛和救贖計劃（約翰福音3:16）。\n'
             '\n'
             '7.  **死亡 (Mavet / Death)：**\n'
             '    *   **意義：** 一切人類努力的最終終結者和最大的平等者（傳9:5, 12:7）。\n'
             '    *   **神學信息：** 揭示人類的有限性和脆弱性，以及上帝作為生命主宰的權威。\n'
             '    *   **聖經連結：** '
             '罪的工價（羅馬書6:23），但在新約中被基督的復活所戰勝，帶來永恆的盼望（哥林多前書15:54-57）。\n'
             '\n'
             '8.  **享受 / 快樂 (Simcha / Tov / Enjoyment / Pleasure)：**\n'
             '    *   **意義：** 在勞碌中享受飲食、工作和家庭的樂趣，視為上帝的恩賜（傳2:24, 5:18）。\n'
             '    *   **神學信息：** 上帝是慷慨的賜予者，即使在墮落世界中，祂仍賜予人類體驗簡單美好的能力。\n'
             '    *   **聖經連結：** 聖經肯定上帝是良善的賜予者（提摩太前書6:17），真正的喜樂源於上帝（詩篇16:11）。\n'
             '\n'
             "9.  **敬畏神 (Yir'at Elohim / Fear God) 與 謹守他的誡命 (Shamar Mitzvotav "
             '/ Keep His Commandments)：**\n'
             '    *   **意義：** 傳道書最終的結論和解決方案，是人生的總結和意義的錨點（傳12:13）。\n'
             '    *   **神學信息：** 這是與上帝建立正確關係的基石，體現了對上帝的尊崇、敬重和順服，以及對祂道德律法的遵行。\n'
             '    *   **聖經連結：** '
             '智慧的開端（箴言9:10），蒙福的條件（申命記10:12-13），新約中愛上帝和愛人的總綱（馬太福音22:37-40，約翰福音14:15）。\n'
             '\n'
             '### 四、 《傳道書》在聖經正典中的地位與應用\n'
             '\n'
