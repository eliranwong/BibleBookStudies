             '### 五、 關鍵詞彙與神學信息\n'
             '\n'
             '《路得記》透過以下關鍵詞彙和概念，傳達了豐富的神學信息：\n'
             '\n'
             '1.  **恩慈 / 忠誠的愛 (Hesed)：**\n'
             '    *   **闡述：** 希伯來文 *חֶסֶד* (hesed) '
             '指基於盟約關係的堅定不移的忠誠、委身和憐憫。它體現在路得對拿俄米的捨己之愛（路得記 '
             '1:16-17），以及波阿斯對路得和拿俄米家族的慷慨與責任感（路得記 2:20；3:10）。\n'
             '    *   **聖經意義：** *Hesed* 是描述神性情的核心詞彙（出埃及記 '
             '34:6），強調神對祂子民的信實和不變的愛。它呼召信徒在人際關係中活出這種超越義務的忠誠之愛（彌迦書 6:8）。\n'
             '\n'
             "2.  **至近的親屬 / 救贖者 (Go'el)：**\n"
             "    *   **闡述：** 希伯來文 *גֹּאֵל* (go'el) "
             "指有義務為親屬贖回產業、家族名譽或親屬生命的人。波阿斯作為 *go'el*，贖回了土地並娶路得為妻，為死者留後（路得記 "
             '4:5-10）。\n'
             "    *   **聖經意義：** *Go'el* 具有深遠的神學意義，預表了神作為以色列的終極救贖者（以賽亞書 "
             "41:14），並最終指向耶穌基督。波阿斯作為 *go'el* "
             '的條件（近親、有能力、願意）完美預表了耶穌基督為人類贖罪的身份和工作（希伯來書 2:14；加拉太書 3:13）。\n'
             '\n'
             '3.  **神聖的護理 (Divine Providence)：**\n'
             '    *   **闡述：** 儘管書中沒有神直接說話，但神的手卻在幕後巧妙地引導著一切。路得「恰巧到了」波阿斯的田裡（路得記 '
             '2:3），這些看似偶然的事件都指向神精密的安排。\n'
             '    *   **聖經意義：** '
             '神的護理表明祂是主宰歷史和個人命運的至高者。祂的計劃不會因人類的苦難或看似隨機的事件而受阻，反而能將這些都納入祂更宏大的旨意中（羅馬書 '
             '8:28）。\n'
             '\n'
