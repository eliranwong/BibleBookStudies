             '*   **4.1-25 亞伯拉罕為因信稱義的榜樣：** '
             '為進一步證明因信稱義的真理，保羅引用舊約人物亞伯拉罕為例。他指出，亞伯拉罕在受割禮之前，就因「信神，這就算為他的義」（4:3）。這證明稱義不是靠行為或割禮，而是單單藉著信心。亞伯拉罕因此成為「一切未受割禮而信之人的父」（4:11），也成為所有信徒的父。保羅將亞伯拉罕的信心與信徒對「神使我們的主耶穌從死裡復活」的信心聯繫起來，指出「耶穌被交給人，是為我們的過犯；復活，是為叫我們稱義」（4:25）。\n'
             '*   **5.1-11 因信稱義的豐盛果效：** '
             '稱義帶來一系列的屬靈福氣。首先是「與神相和」（5:1），其次是「藉著祂，因信得進入現在所站的這恩典中」（5:2），並「歡歡喜喜盼望神的榮耀」（5:2）。即使在患難中，也能因知道患難生忍耐、忍耐生老練、老練生盼望而「歡歡喜喜」（5:3-4）。這盼望不至於羞恥，因為「所賜給我們的聖靈將神的愛澆灌在我們心裡」（5:5）。保羅進一步強調神愛的偉大，在我們「還作罪人的時候為我們死」（5:8），因此我們「更要藉著祂免去神的忿怒」（5:9），並「因祂的生得救了」（5:10）。\n'
             '*   **5.12-21 亞當與基督的對比：罪與恩典的傳遞：** '
             '保羅透過亞當與基督的對比，闡明了罪與恩典的普世性影響。他指出，「罪是從一人入了世界，死又是從罪來的；於是死就臨到眾人」（5:12）。亞當的過犯導致了全人類的定罪和死亡。然而，基督的「一次的義行」卻帶來了「眾人也就被稱義得生命了」（5:18）。基督的恩典遠超亞當的過犯，使「罪在哪裡顯多，恩典就更顯多了」（5:20），最終恩典「藉著義作王，叫人因我們的主耶穌基督得永生」（5:21）。這部分強調了基督救贖工作的宏大與完全。\n'
             '\n'
             '**第四階段：稱義後的生命：成聖與在聖靈裡的生活 (6:1-8:39)**\n'
             '\n'
             '既然恩典如此豐盛，是否意味著信徒可以繼續犯罪？保羅在此階段從稱義的教義自然過渡到成聖的實踐，闡明信徒在基督裡的新身份和聖靈在成聖過程中的關鍵作用。\n'
             '\n'
             '*   **6.1-14 與基督聯合：脫離罪的權勢：** '
             '保羅堅決否定「可以仍在罪中，叫恩典顯多嗎？斷乎不可！」（6:1-2）。他解釋，信徒藉著洗禮已「歸入祂的死」，與基督一同埋葬，也一同復活，因此「一舉一動有新生的樣式」（6:3-4）。我們的「舊人已經和祂同釘十字架，使罪身滅絕，叫我們不再作罪的奴僕」（6:6）。信徒應當「向罪也當看自己是死的；向神在基督耶穌裡，卻當看自己是活的」（6:11），並將肢體獻給神作「義的工具」（6:13）。\n'
             '*   **6.15-23 作義的奴僕，得著成聖的果子：** '
             '保羅再次強調，信徒既然「從罪裡得了釋放，就作了義的奴僕」（6:18）。他對比了作罪的奴僕（結局是死）和作神的奴僕（結局是「成聖的果子」，就是永生），指出「罪的工價乃是死；惟有神的恩賜，在我們的主基督耶穌裡，乃是永生」（6:23）。\n'
             '*   **7.1-6 脫離律法的束縛：** '
             '保羅用婚姻的比喻說明，信徒藉著基督的身體，已「在律法上也是死了」（7:4），從而脫離了律法的捆綁，可以歸於基督，按著「聖靈的新樣」服事神，而非「儀文的舊樣」（7:6）。\n'
             '*   **7.7-25 律法是聖潔的，但顯明罪：** '
             '保羅澄清律法本身並非罪惡，而是「聖潔、公義、良善的」（7:12）。然而，罪卻藉著律法「引誘我，並且殺了我」（7:11）。他生動地描述了人內心的掙扎：「我所願意的，我並不做；我所恨惡的，我倒去做」（7:15）。這種「立志為善由得我，只是行出來由不得我」（7:18）的困境，揭示了肉體在罪的權勢下的無力，最終發出「我真是苦啊！誰能救我脫離這取死的身體呢？」的呼喊（7:24）。這段經文為接下來聖靈的工作鋪墊了必要性。\n'
             '*   **8.1-17 在聖靈裡的新生命與兒子的名分：** '
             '保羅立即給出了第7章困境的答案：「如今，那些在基督耶穌裡的就不定罪了」（8:1）。因為「賜生命聖靈的律在基督耶穌裡釋放了我，使我脫離罪和死的律了」（8:2）。聖靈使信徒能夠「不隨從肉體、只隨從聖靈」（8:4），體貼聖靈的事，得著「生命、平安」（8:6）。聖靈內住在信徒裡面，使他們「不屬肉體，乃屬聖靈」（8:9），並引導他們作「神的兒子」（8:14），呼叫「阿爸！父！」（8:15）。聖靈與我們的心同證我們是神的兒女，是「神的後嗣，和基督同作後嗣」（8:17）。\n'
             '*   **8.18-39 未來的榮耀與神不變的愛：** '
             '保羅將信徒的盼望從現在的掙扎提升到未來的榮耀。他指出「現在的苦楚若比起將來要顯於我們的榮耀，就不足介意了」（8:18）。不僅信徒，連「一切受造之物一同歎息、勞苦」（8:22），等候「身體得贖」（8:23）。在軟弱中，「聖靈親自用說不出來的歎息替我們禱告」（8:26）。保羅進一步闡述了神永恆的救贖計畫：祂「預先所知道的人，就預先定下效法祂兒子的模樣」（8:29），並將他們「召他們來」、「稱他們為義」、「叫他們得榮耀」（8:30）。最後，保羅以一段氣勢磅礴的頌讚結束此章，宣告「神若幫助我們，誰能敵擋我們呢？」（8:31），並確信「無論是死，是生...都不能叫我們與神的愛隔絕；這愛是在我們的主基督耶穌裡的」（8:38-39）。這部分是信徒得救確據的巔峰。\n'
             '\n'
             '**第五階段：神的義與以色列的命運 (9:1-11:36)**\n'
             '\n'
             '在闡明了普世性的救恩之後，保羅必須處理一個敏感而深刻的問題：如果救恩是因信稱義，那麼神對以色列的應許是否落空了？以色列作為神的選民，其地位和命運如何？\n'
             '\n'
             '*   **9.1-5 保羅為以色列的憂傷：** '
             '保羅首先表達了他對骨肉之親以色列人極大的憂愁和傷痛，甚至願意自己被咒詛，與基督分離，以換取他們的得救（9:1-3）。他列舉了以色列作為選民所擁有的特權（兒子的名分、榮耀、諸約、律法、禮儀、應許、列祖，以及基督從他們而出），以此強調問題的嚴重性。\n'
             '*   **9.6-13 神主權的揀選：** '
             '保羅解釋「這不是說神的話落了空」（9:6）。他指出，並非所有「從以色列生的不都是以色列人」（9:6）。神的主權體現在祂的揀選上，例如從亞伯拉罕的後裔中揀選以撒而非以實瑪利，從利百加的雙子中揀選雅各而非以掃，這「不在乎人的行為，乃在乎召人的主」（9:11）。\n'
             '*   **9.14-29 神公義與主權的辯護：** '
             '保羅預見有人會質疑神是否不公義。他堅決回應「斷乎沒有！」（9:14），並引用摩西和法老的例子，證明神有絕對的主權「要憐憫誰就憐憫誰，要叫誰剛硬就叫誰剛硬」（9:18）。他用「窯匠與泥土」的比喻（9:20-21）來強調受造之物無權質疑造物主。神有權利將器皿作成「可怒預備遭毀滅的器皿」，也有權利將器皿作成「蒙憐憫早預備得榮耀的器皿」（9:22-23），這包括從猶太人和外邦人中蒙召的人，正如舊約先知何西阿和以賽亞所預言的。\n'
             '*   **10.1-13 以色列的絆跌與因信稱義的道路：** '
             '保羅再次表達他對以色列人得救的願望。他指出以色列人「向神有熱心，卻不是按著真知識」（10:2），因為他們「不知道神的義，想要立自己的義，就不服神的義了」（10:3）。他強調「律法的總結就是基督，使凡信祂的都得著義」（10:4）。救恩的道路是簡單而普世的：「你若口裡認耶穌為主，心裡信神叫祂從死裡復活，就必得救」（10:9），因為「凡求告主名的就必得救」（10:13）。\n'
             '*   **10.14-21 傳福音的必要性與以色列的悖逆：** '
             '保羅論證了傳福音的必要性：「人未曾信祂，怎能求祂呢？未曾聽見祂，怎能信祂呢？沒有傳道的，怎能聽見呢？」（10:14）。他指出福音的聲音已經傳遍天下，但以色列人卻「沒有都聽從福音」（10:16），他們是「悖逆頂嘴的百姓」（10:21），這也是舊約先知所預言的。\n'
             '*   **11.1-10 神沒有棄絕以色列：** '
             '保羅再次強調「神棄絕了祂的百姓嗎？斷乎沒有！」（11:1）。他以自己為例（他也是以色列人），並引用以利亞時代的「七千人」餘數，證明神「照著揀選的恩典，還有餘數」（11:5）。以色列的硬心是部分的，其餘的人「就成了頑梗不化的」（11:7）。\n'
             '*   **11.11-24 以色列的暫時絆跌與外邦人的得救：** '
             '保羅指出以色列的「失腳」並非要他們永遠跌倒，反而是「因他們的過失，救恩便臨到外邦人，要激動他們發憤」（11:11）。他用「橄欖樹」的比喻：原來的枝子（以色列）因不信被折下來，野橄欖枝子（外邦人）因信被接上。保羅警告外邦信徒不可自高，要「懼怕」（11:20），因為神「既不愛惜原來的枝子，也必不愛惜你」（11:21）。神有能力將原來的枝子重新接上。\n'
             '*   **11.25-32 全以色列將要得救的奧秘：** '
             '保羅揭示了一個「奧秘」：「以色列人有幾分是硬心的，等到外邦人的數目添滿了，於是以色列全家都要得救」（11:25-26）。這表明以色列的硬心是暫時的，最終神將會救贖他們。他強調「神的恩賜和選召是沒有後悔的」（11:29）。神將「眾人都圈在不順服之中，特意要憐憫眾人」（11:32），這彰顯了神終極的憐憫。\n'
             '*   **11.33-36 頌讚神的智慧與主權：** '
             '保羅以一段宏偉的頌讚結束了對以色列命運的討論，感嘆「深哉，神豐富的智慧和知識！祂的判斷何其難測！祂的蹤跡何其難尋！」（11:33）。他承認「萬有都是本於祂，藉著祂，歸於祂」（11:36），將一切榮耀歸給神。這段頌讚是對神主權和救贖計畫的最終肯定。\n'
             '\n'
             '**第六階段：基督徒的倫理生活與實踐 (12:1-15:13)**\n'
             '\n'
             '在建立起堅實的神學基礎之後，保羅將焦點轉向福音在信徒日常生活中的實際應用。這部分是從「所以」（12:1）開始，強調因著神的慈悲，信徒應當如何回應。\n'
             '\n'
             '*   **12.1-2 獻上身體為活祭與心意更新：** '
             '這是基督徒倫理的總綱。保羅勸勉信徒「將身體獻上，當作活祭，是聖潔的，是神所喜悅的」（12:1）。這是一種理所當然的事奉。同時，信徒「不要效法這個世界，只要心意更新而變化」（12:2），以便「察驗何為神的善良、純全、可喜悅的旨意」。\n'
             '*   **12.3-8 在基督身體裡的恩賜與服事：** '
             '保羅教導信徒要謙卑自省，「不要看自己過於所當看的」（12:3），並認識到在基督的身體裡，各人有不同的恩賜和功用，應當照所得的恩典彼此服事（12:4-8）。\n'
             '*   **12.9-21 愛的實踐與基督徒的品格：** '
             '這部分列舉了一系列具體的倫理勸勉，涵蓋了愛心、熱心、盼望、忍耐、禱告、款待、祝福仇敵、與人同樂、謙卑、不報惡、與人和睦、不自己伸冤、以善勝惡等。核心是「愛人不可虛假」（12:9）和「你不可為惡所勝，反要以善勝惡」（12:21）。\n'
             '*   **13.1-7 順服掌權者：** '
             '保羅教導信徒要「順服在上的權柄」（13:1），因為「沒有權柄不是出於神的」（13:1）。掌權者是「神的用人」（13:4），信徒應當納稅、懼怕、恭敬。這強調了基督徒在世俗社會中的公民責任。\n'
             '*   **13.8-10 愛是完全律法的：** '
             '保羅再次強調「凡事都不可虧欠人，惟有彼此相愛，要常以為虧欠」（13:8）。他指出「愛人的就完全了律法」（13:8），因為「愛是不加害於人的，所以愛就完全了律法」（13:10）。\n'
             '*   **13.11-14 儆醒等候主再來：** '
             '保羅提醒信徒，現今是「該趁早睡醒的時候」（13:11），因為「得救，現今比初信的時候更近了」（13:11）。因此，信徒應當「脫去暗昧的行為，帶上光明的兵器」（13:12），「披戴主耶穌基督，不要為肉體安排，去放縱私慾」（13:14）。\n'
             '*   **14.1-15.6 接納信心軟弱的弟兄：** '
             '保羅處理了羅馬教會內部猶太和外邦信徒之間可能存在的爭議，特別是關於飲食和守日的問題。他教導「信心軟弱的，你們要接納，但不要辯論所疑惑的事」（14:1）。他強調「吃的人不可輕看不吃的人；不吃的人不可論斷吃的人；因為神已經收納他了」（14:3）。每個人都將「在神面前交賬」（14:12），因此不應當「再彼此論斷」（14:13），而應當避免使弟兄「跌倒」（14:13）。「神的國不在乎吃喝，只在乎公義、和平，並聖靈中的喜樂」（14:17）。剛強的信徒應當「擔代不堅固人的軟弱，不求自己的喜悅」（15:1），效法基督的榜樣。\n'
             '*   **15.7-13 基督徒的合一與福音的普世性：** '
             '保羅總結說：「你們要彼此接納，如同基督接納你們一樣，使榮耀歸與神」（15:7）。他指出基督為猶太人服務，證實神的應許，也為使外邦人因神的憐憫而榮耀神，這都是舊約所預言的。他以祝福結束此段，願神使信徒「因信將諸般的喜樂、平安充滿你們的心，使你們藉著聖靈的能力大有盼望」（15:13）。\n'
             '\n'
             '**第七階段：結語：保羅的宣教計畫、問安與頌讚 (15:14-16:27)**\n'
             '\n'
             '保羅以個人化的方式結束書信，分享他的宣教計畫，向羅馬教會的許多成員問安，並發出最終的警告和頌讚。\n'
             '\n'
             '*   **15.14-29 保羅的宣教事工與羅馬之行的計畫：** '
             '保羅重申自己作為「外邦人作基督耶穌的僕役」的職分（15:16），並分享他在東方宣教的成果。他解釋了自己前往羅馬的願望，以及他計劃先將馬其頓和亞該亞的捐款送往耶路撒冷，然後經羅馬前往西班牙的宣教路線。\n'
             '*   **15.30-33 請求代禱：** '
             '保羅懇切請求羅馬信徒為他禱告，求神使他「脫離在猶太不順從的人」，使他在耶路撒冷的服事蒙悅納，並能「順著神的旨意，歡歡喜喜地到你們那裡」（15:30-32）。\n'
             '*   **16.1-16 個人問安：** '
             '保羅推薦送信的非比姊妹，並向羅馬教會中許多他所認識或聽聞的信徒問安，包括百基拉和亞居拉等。這部分展現了保羅對各地信徒的深厚情誼和早期教會的緊密連結。\n'
