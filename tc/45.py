             '    *   **舊約貢獻：** 舊約中神的「公義」（צדקה）常與祂的盟約信實和拯救行動相關（如**詩篇 '
             '98:2**）。保羅引用**哈巴谷書 2:4**「**義人必因信得生**」（羅馬書 1:17），將其應用於基督的福音。\n'
             '\n'
             '2.  **罪的普世性 (ἁμαρτία)：**\n'
             '    *   **神學意涵：** 所有人類都因罪而「**虧缺了神的榮耀**」（羅馬書 '
