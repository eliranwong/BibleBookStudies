             '    *   **舊約貢獻：** 從**創世記 3 章**人類墮落，到**詩篇 '
             '14:1-3**「**沒有義人，連一個也沒有**」（羅馬書 3:10-12 引用），舊約已清晰揭示罪的普遍性。\n'
