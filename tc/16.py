             '7.  **盟約 (Covenant)：** '
             '百姓認罪悔改後與上帝重新立約，承諾遵守誡命，恢復選民身份。在聖經中是上帝與人類建立關係的核心模式，新約成就舊約所有盟約應許（尼9:38; '
             '10:29; 創15:18; 耶31:31-34; 路22:20）。\n'
             '8.  **什一奉獻 (Tithes)：** '
             '盟約承諾之一，確保聖殿事工運作和利未人生計，維護對上帝的忠誠。在聖經中是支持聖殿事奉，提醒一切來自上帝恩賜（尼10:37; '
             '13:10, 12; 瑪3:10; 林後9:7）。\n'
             '9.  **安息日 (Sabbath)：** '
             '盟約承諾，尼希米嚴厲改革以恢復其聖潔性，強調信仰原則高於經濟利益。在聖經中是上帝創造的紀念和盟約標誌，新約中耶穌是安息日的主（尼10:31; '
             '13:15, 19; 出20:8; 可2:27）。\n'
             '10. **異族通婚 (Intermarriage)：** '
             '盟約承諾禁止，尼希米嚴厲處理以維護信仰純潔性和身份認同。在聖經中嚴禁以防信仰妥協和偶像崇拜，新約強調信徒應在主裡結婚（尼10:30; '
             '13:23, 28; 申7:3-4; 林後6:14）。\n'
