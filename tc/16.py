             '5:19; 6:3）。\n'
             '2.  **耶路撒冷城牆 (Wall of Jerusalem)：** '
             '敘事核心焦點，象徵民族羞辱、脆弱與安全、尊嚴的恢復。在聖經中代表保護、安全、力量，新約預表新耶路撒冷（尼1:3; 4:6; '
             '6:15; 啟21:12-14）。\n'
             '3.  **上帝施恩的手 (The Good Hand of God)：** '
             '尼希米將一切成功歸因於上帝的直接干預和恩惠，強調上帝的主權和護理。在聖經中象徵上帝的能力、權柄和主動作為（尼2:8, 18; '
             '出3:20; 徒11:21）。\n'
             '4.  **禱告 (Prayer)：** '
             '尼希米領導力的核心和力量泉源，貫穿每個重大決定和危機，展現對上帝的絕對依賴。在聖經中是人與上帝溝通的基礎，信徒生命不可或缺（尼1:4; '
