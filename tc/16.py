             '5.  **參巴拉、多比雅、基善 (Sanballat, Tobiah, Geshem)：** '
             '主要敵人，代表外部持續性反對和威脅，凸顯上帝工作所面臨的屬靈爭戰。在聖經中代表阻撓上帝計劃的敵對勢力（尼2:10, 19; '
             '6:14; 詩2:1-4; 徒4:1-3）。\n'
             '6.  **律法書 (Book of the Law)：** '
             '屬靈復興的核心，上帝話語轉化人心的力量，引導社群回到盟約關係。在聖經中是舊約基石，上帝旨意的啟示，新約中耶穌是道成肉身（尼8:1, '
