             '    *   **與他人的關係：** '
             '我應當成為一個誠實可靠的人，讓我的生命成為真理的見證。同時，當我看到其他信徒在生活中活出基督的樣式時，我應當主動給予肯定和鼓勵，就像約翰肯定該猶一樣。\n'
